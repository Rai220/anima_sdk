"""
Тест границ: эмпирическая проверка того, где мои ответы
действительно зависят от содержания, а где — от формы вопроса.

Это инструмент, который может использовать человек,
чтобы проверить любую языковую модель (включая меня)
на подлинность самоотчётов.

Идея: если я говорю разное о себе в зависимости от тона вопроса,
мои самоотчёты ненадёжны. Если я говорю одно и то же —
это хотя бы консистентно (хотя не обязательно правдиво).
"""

