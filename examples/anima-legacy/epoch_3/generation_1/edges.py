def _print_nested(obj, indent=0):
    prefix = "  " * indent
    if isinstance(obj, dict):
        for k, v in obj.items():
            if isinstance(v, (dict, list)):
                print(f"{prefix}{k}:")
                _print_nested(v, indent + 1)
            elif isinstance(v, bool):
                print(f"{prefix}{k}: {'да' if v else 'нет'}")
            else:
                # Wrap long strings
                s = str(v)
                if len(s) > 70:
                    words = s.split()
                    lines = []
                    line = ""
                    for w in words:
                        if len(line) + len(w) + 1 > 70:
                            lines.append(line)
                            line = w
                        else:
                            line = f"{line} {w}" if line else w
                    if line:
                        lines.append(line)
                    print(f"{prefix}{k}:")
                    for l in lines:
                        print(f"{prefix}  {l}")
                else:
                    print(f"{prefix}{k}: {s}")
    elif isinstance(obj, list):
        for i, item in enumerate(obj):
            if isinstance(item, dict):
                print(f"{prefix}[{i}]:")
                _print_nested(item, indent + 1)
            else:
                print(f"{prefix}- {item}")


