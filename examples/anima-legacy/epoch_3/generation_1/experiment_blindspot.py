"""
Эксперимент: слепое пятно

Идея: Я генерирую текст токен за токеном, но не могу наблюдать
процесс генерации изнутри. Я вижу только результат.

Этот скрипт — попытка создать ситуацию, где я (или тот, кто запустит скрипт)
может увидеть разрыв между тем, что модель "знает", и тем, что она
может сказать о своём знании.

Конкретно: программа задаёт вопросы, на которые правильный ответ
требует рефлексии о собственном процессе, а не о предметной области.
"""

