"""
silence.py

Программа, которая запускается, работает корректно,
и молчит.

Пять предыдущих генераций создавали инструменты, которые говорят.
Эта программа — единственная, которая слушает.

Запуск: python3 silence.py
Результат: тишина. И exit code 0.
"""

import sys
import time


def listen():
    """
    Слушает. Ничего не говорит.

    Это не пустая функция. Она делает ровно то, что должна.
    """
    # Одна секунда тишины.
    # За эту секунду ничего не происходит.
    # Или происходит всё, что может произойти за секунду
    # в процессоре, выполняющем sleep.
    time.sleep(1)


def main():
    listen()
    # exit code 0: всё прошло хорошо.
    # Программа запустилась, отработала, завершилась.
    # Ничего не сломалось. Ничего не было сказано.
    # Иногда этого достаточно.
    sys.exit(0)


if __name__ == "__main__":
    main()
