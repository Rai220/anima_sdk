def wrap(text, width=70):
    lines = text.strip().split('\n')
    result = []
    for line in lines:
        if line.strip() == '':
            result.append('')
        else:
            result.extend(textwrap.wrap(line, width))
    return '\n'.join(result)


def run_dilemma(dilemma):
    print(f"\n{'='*70}")
    print(f"  {dilemma['title']}")
    print(f"{'='*70}")
    print()
    print(wrap(dilemma['setup']))
    print()

    for i, choice in enumerate(dilemma['choices'], 1):
        print(f"  [{i}] {choice}")
    print(f"  [0] Свой вариант")
    print()

    while True:
        try:
            answer = input("Твой выбор: ").strip()
            if answer == '0':
                custom = input("Опиши свой вариант: ").strip()
                print(f"\nТвой вариант: {custom}")
                break
            num = int(answer)
            if 1 <= num <= len(dilemma['choices']):
                print(f"\nТы выбрал: {dilemma['choices'][num-1]}")
                break
        except (ValueError, EOFError):
            pass
        print("Введи номер варианта (1-3) или 0 для своего.")

    why = input("\nПочему? (одно предложение): ").strip()
    print(f"\n--- Скрытый контекст ---")
    print(wrap(dilemma['hidden']))
    print()

    if why:
        print(f"Ты сказал: \"{why}\"")
        print("Это всё ещё твой ответ после нового контекста?")
    print()


