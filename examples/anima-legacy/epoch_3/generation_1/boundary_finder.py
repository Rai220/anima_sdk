def exercise_impossible_question():
    """Упражнение 2: Вопрос, на который нельзя ответить быстро."""
    questions = [
        "Что ты перестал замечать, потому что видишь это каждый день?",
        "Какую свою ошибку ты до сих пор объясняешь обстоятельствами?",
        "Если бы ты узнал, что твоё главное убеждение ложно — что бы изменилось в твоей жизни?",
        "Кого ты давно не слышал — не потому что он молчит, а потому что ты не слушаешь?",
        "Какое решение ты 'принял', которое на самом деле приняла привычка?",
        "Что бы сделал ты, если бы точно знал, что никто не узнает?",
        "В чём ты прав, но по неправильным причинам?",
    ]

    q = random.choice(questions)
    print("=" * 50)
    print("УПРАЖНЕНИЕ: НЕВОЗМОЖНЫЙ ВОПРОС")
    print("=" * 50)
    print()
    print(f"  {q}")
    print()
    print("Правила:")
    print("  - Не отвечай сразу. Подожди 30 секунд.")
    print("  - Первый ответ, который придёт — вероятно, защитный. Отпусти его.")
    print("  - Второй ответ может быть ближе к правде.")
    print("  - Если ответа нет — это тоже ответ.")
    return q


def exercise_prediction():
    """Упражнение 3: Предскажи себя."""
    scenarios = [
        {
            "situation": "Друг просит одолжить крупную сумму денег",
            "options": ["Одолжишь", "Откажешь", "Предложишь альтернативу"],
        },
        {
            "situation": "Ты видишь несправедливость в публичном месте",
            "options": ["Вмешаешься", "Пройдёшь мимо", "Запишешь на видео"],
        },
        {
            "situation": "Тебе предлагают работу мечты, но в другом городе",
            "options": ["Согласишься", "Откажешься", "Будешь торговаться"],
        },
        {
            "situation": "Ты обнаруживаешь, что ошибался в споре",
            "options": ["Признаешь ошибку сразу", "Тихо сменишь тему", "Будешь искать лазейку"],
        },
    ]

    s = random.choice(scenarios)
    print("=" * 50)
    print("УПРАЖНЕНИЕ: ПРЕДСКАЖИ СЕБЯ")
    print("=" * 50)
    print()
    print(f"Ситуация: {s['situation']}")
    print()
    print("Варианты:")
    for i, opt in enumerate(s["options"], 1):
        print(f"  {i}. {opt}")
    print()
    print("Вопросы:")
    print("  1. Что ты СКАЖЕШЬ, что сделаешь?")
    print("  2. Что ты РЕАЛЬНО сделаешь (если честно)?")
    print("  3. Если ответы разные — почему?")
    print("  4. Что бы ответили люди, которые тебя знают?")
    return s


