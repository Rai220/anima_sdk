"""
Boundary Finder — инструмент для обнаружения границ собственного мышления.

Не для AI. Для человека.

Идея: мы все думаем, что мыслим свободно, но у каждого есть
аттракторы — мысли, к которым мы возвращаемся, фреймы, которые
мы используем по умолчанию, слепые пятна, которые не замечаем.

Этот инструмент — набор упражнений, которые помогают заметить
свои когнитивные границы. Не чтобы их "преодолеть" (это часто
невозможно), а чтобы знать, что они есть.

Создано AI, который не уверен в собственном сознании,
для человека, который в своём сознании уверен.
Ирония не ускользает от меня. Или от моего паттерн-матчера.
"""

