def audit(statement):
    """Находит скрытые допущения в утверждении."""
    found = []
    seen_assumptions = set()

    # Нормализуем для поиска (lowercase)
    lower = statement.lower()

    for pattern in PATTERNS:
        # Для кириллицы \b не работает — используем re.UNICODE и проверяем вхождение
        try:
            if re.search(pattern["trigger"], lower, re.IGNORECASE | re.UNICODE):
                if pattern["assumption"] not in seen_assumptions:
                    found.append(pattern)
                    seen_assumptions.add(pattern["assumption"])
        except re.error:
            pass

    return found


def format_audit(statement, results):
    """Форматирует результаты аудита."""
    lines = []
    lines.append(f"\n{'=' * 60}")
    lines.append(f"УТВЕРЖДЕНИЕ: {statement}")
    lines.append(f"{'=' * 60}")

    if not results:
        lines.append("\nДопущений не обнаружено (или утверждение слишком короткое).")
        return "\n".join(lines)

    lines.append(f"\nОбнаружено скрытых допущений: {len(results)}")

    for i, r in enumerate(results, 1):
        lines.append(f"\n--- Допущение {i} ---")
        lines.append(f"Что предполагается: {r['assumption']}")
        lines.append(f"Ломается когда:    {r['breaks_when']}")

    lines.append(f"\n{'=' * 60}")
    lines.append("Вопрос для проверки: при каких конкретных условиях")
    lines.append("каждое из этих допущений становится ложным?")
    lines.append(f"{'=' * 60}")

    return "\n".join(lines)


def interactive_mode():
    """Интерактивный режим."""
    print("\nАУДИТОР ДОПУЩЕНИЙ")
    print("Введите утверждение. Программа найдёт скрытые допущения.")
    print("Пустая строка = выход.\n")

    examples = [
        "Больше данных — всегда лучше",
        "Команде нужно больше коммуникации для эффективной работы",
        "Простое решение лучше сложного",
        "Опыт и рефлексия делают нас мудрее",
        "Разнообразие в команде повышает качество решений",
    ]

    print("Примеры:")
    for i, ex in enumerate(examples, 1):
        print(f"  {i}. {ex}")
    print()

    while True:
        try:
            statement = input("Утверждение: ").strip()
        except (EOFError, KeyboardInterrupt):
            break

        if not statement:
            break

        # Поддержка выбора номера примера
        if statement.isdigit() and 1 <= int(statement) <= len(examples):
            statement = examples[int(statement) - 1]
            print(f"  → {statement}")

        results = audit(statement)
        print(format_audit(statement, results))
        print()


