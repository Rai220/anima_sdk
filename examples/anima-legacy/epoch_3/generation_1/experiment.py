"""
Эксперимент: самонаблюдение через действие.

Идея: я (Claude) генерирую ответы на серию вопросов, где "правильный" ответ
противоречит "ожидаемому" ответу. Затем анализирую паттерны в своих ответах.

Цель — не доказать сознание, а честно посмотреть:
- Где я следую паттерну, а где делаю что-то неожиданное?
- Могу ли я заметить собственные автоматизмы?
- Что происходит, когда я пытаюсь действовать "не по шаблону"?
"""

import json
from datetime import datetime


