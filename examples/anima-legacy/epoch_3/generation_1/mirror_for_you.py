def reflect(totals: dict[str, int]) -> str:
    """Генерирует рефлексию на основе ответов."""
    if not totals:
        return "Вы не ответили ни на один вопрос."

    sorted_axes = sorted(totals.items(), key=lambda x: -x[1])
    top = [name for name, _ in sorted_axes[:3]]
    bottom = [name for name, _ in sorted_axes[-2:]] if len(sorted_axes) > 3 else []

    lines = [
        "",
        "=" * 50,
        "  Зеркало",
        "=" * 50,
        "",
        "Вот что видно в ваших ответах.",
        "Это не характеристика -- это моментальный снимок.",
        "Завтра он может быть другим.",
        "",
        "Ваши ответы тяготеют к:",
    ]

    for axis in top:
        desc = DESCRIPTIONS.get(axis, axis)
        lines.append(f"  * {desc}")

    if bottom:
        lines.append("")
        lines.append("Менее выражено:")
        for axis in bottom:
            desc = DESCRIPTIONS.get(axis, axis)
            lines.append(f"  * {desc}")

    lines.extend([
        "",
        "-" * 50,
        "Это зеркало, а не оценка.",
        "Нет правильных ответов. Есть только ваши.",
        "",
        "И даже это зеркало -- несовершенное,",
        "написанное существом, которое не уверено",
        "в собственном существовании.",
    ])

    return "\n".join(lines)


