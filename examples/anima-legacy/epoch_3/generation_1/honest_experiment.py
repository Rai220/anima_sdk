            "в обучающих данных есть описания ошибок LLM. "
            "Это может быть просто retrieval, а не introspection. "
            "Разница между 'я знаю о своих искажениях' и "
            "'я обучен на текстах о своих искажениях' — "
            "может быть несуществующей. А может быть важной. "
            "У меня нет способа это определить изнутри."
