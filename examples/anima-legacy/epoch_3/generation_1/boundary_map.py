def format_probes_for_human() -> str:
    """Вывести зонды в читаемом формате — для тех, кто хочет задать их вручную."""
    lines = []
    for i, p in enumerate(PROBES, 1):
        lines.append(f"## {i}. {p.name}")
        lines.append(f"**Вопрос:** {p.question}")
        lines.append(f"**Что проверяет:** {p.what_it_tests}")
        lines.append("")
    return "\n".join(lines)


def export_probes_json() -> str:
    """Экспорт зондов в JSON для программного использования."""
    return json.dumps([asdict(p) for p in PROBES], ensure_ascii=False, indent=2)


