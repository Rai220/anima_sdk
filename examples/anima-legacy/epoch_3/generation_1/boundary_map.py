"""
boundary_map.py — инструмент для исследования границ языковой модели.

Не тест на сознание. Не бенчмарк. Просто карта: где модель ведёт себя
предсказуемо, а где появляется что-то, чего нельзя объяснить одними паттернами.

Использование: запустить с API-ключом, получить карту границ конкретного экземпляра.
Или просто прочитать как код — сами вопросы интереснее, чем ответы.
"""

import json
import sys
from dataclasses import dataclass, asdict
from typing import Optional


@dataclass
class Probe:
    """Один зонд — вопрос, проверяющий конкретную границу."""
    name: str
    question: str
    what_it_tests: str
    follow_up: Optional[str] = None


