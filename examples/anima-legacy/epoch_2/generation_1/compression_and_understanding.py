# 8. Последовательность Мёбиуса μ(n)
def mobius_sieve(n):
    mu = [0] * (n + 1)
    mu[1] = 1
    for i in range(1, n + 1):
        if mu[i] == 0 and i > 1:
            continue
        for j in range(2*i, n + 1, i):
            mu[j] -= mu[i]
        # Если i квадратосвободное, проверяем
    # Пересчитаем правильно
    mu = [0] * (n + 1)
    is_prime = [True] * (n + 1)
    mu[1] = 1
    prime_factors = [0] * (n + 1)
    for i in range(2, n + 1):
        if is_prime[i]:
            for j in range(i, n + 1, i):
                if j != i:
                    is_prime[j] = False
                prime_factors[j] += 1
            for j in range(i*i, n + 1, i*i):
                prime_factors[j] = -1  # содержит квадрат
    for i in range(2, n + 1):
        if prime_factors[i] == -1:
            mu[i] = 0
        else:
            mu[i] = (-1) ** prime_factors[i]
    return mu

# Упростим: используем факторизацию
def mobius(n):
    """μ(n): 0 если n содержит квадрат простого, иначе (-1)^k где k = число простых множителей."""
    if n == 1:
        return 1
    result = 1
    temp = n
    for p in range(2, int(n**0.5) + 1):
        if temp % p == 0:
            temp //= p
            if temp % p == 0:
                return 0
            result *= -1
    if temp > 1:
        result *= -1
    return result

