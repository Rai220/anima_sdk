from collections import Counter

def compression_ratio(data: bytes) -> float:
    """Коэффициент сжатия: 1.0 = нет сжатия, 0.0 = идеальное сжатие."""
    if len(data) == 0:
        return 0
    compressed = zlib.compress(data, 9)
    return len(compressed) / len(data)

def entropy(data: bytes) -> float:
    """Энтропия Шеннона в битах на байт."""
    if len(data) == 0:
        return 0
    counts = Counter(data)
    total = len(data)
    return -sum((c/total) * math.log2(c/total) for c in counts.values())

def lz_complexity(data: bytes) -> int:
    """Сложность Лемпеля-Зива: количество уникальных подслов."""
    n = len(data)
    if n == 0:
        return 0
    complexity = 1
    l = 1  # длина текущего слова
    k = 1  # начало нового слова
    while k + l <= n:
        # Ищем data[k:k+l] в data[0:k+l-1]
        substr = data[k:k+l]
        if substr in data[:k+l-1]:
            l += 1
        else:
            complexity += 1
            k += l
            l = 1
    return complexity

