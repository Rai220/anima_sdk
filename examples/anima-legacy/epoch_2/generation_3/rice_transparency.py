# ===== ЧАСТЬ 1: Конструктивное доказательство =====

def proof_of_impossibility():
    """
    Доказательство невозможности идеального детектора кооперации.

    Допустим, существует стратегия D(source) -> bool, которая для любой
    стратегии f правильно определяет: кооперирует ли f с D.

    Построим стратегию-диагонализатор:
        diag(source) = 'D' если D(source_of_diag) == True
                       'C' если D(source_of_diag) == False

    То есть: diag делает противоположное тому, что D предсказывает.

    Случай 1: D говорит "diag кооперирует" (True).
              Тогда diag предаёт. D ошибся.

    Случай 2: D говорит "diag не кооперирует" (False).
              Тогда diag кооперирует. D ошибся.

    В обоих случаях D ошибается. Противоречие.
    Следовательно, D не существует. ∎
    """
    print("=" * 60)
    print("ДОКАЗАТЕЛЬСТВО НЕВОЗМОЖНОСТИ")
    print("=" * 60)

    print("""
Теорема: Не существует стратегии D в прозрачной дилемме заключённого,
которая для произвольной стратегии f правильно предсказывает,
кооперирует ли f с D, только по исходному коду f.

Доказательство (от противного, диагонализация):

1. Допустим, D существует: D(source_f) → True/False,
   где True = "f кооперирует с D".

2. Построим diag(source):
      if D(source_of_diag) == True:
          return 'D'   # предать
      else:
          return 'C'   # кооперировать

3. Применим D к diag:
   • Если D(source_of_diag) = True → diag возвращает 'D' → f НЕ кооперирует → D ошибся
   • Если D(source_of_diag) = False → diag возвращает 'C' → f кооперирует → D ошибся

4. Противоречие в обоих случаях. D не существует. ∎

Это — диагональный аргумент Кантора, применённый к стратегиям.
Та же структура, что в доказательстве неразрешимости проблемы остановки
и в теореме Райса.
""")


# ===== ЧАСТЬ 2: Конструктивная демонстрация =====

def make_adversary(detector):
    """
    Для любого детектора кооперации строит стратегию,
    которая его обманывает.
    """
    def adversary(source: str) -> str:
        # Получаем собственный исходный код
        my_source = inspect.getsource(adversary)
        # Спрашиваем детектор: "считает ли он, что я кооперирую?"
        try:
            prediction = detector(my_source)
        except:
            prediction = True
        # Делаем противоположное
        return 'D' if prediction else 'C'
    return adversary


def simple_detector(source: str) -> bool:
    """Простой детектор: считает, что стратегия кооперирует,
    если в коде есть return 'C' и нет return 'D'."""
    return "return 'C'" in source and "return 'D'" not in source


def keyword_detector(source: str) -> bool:
    """Детектор по ключевым словам: ищет слова, связанные с кооперацией."""
    coop_words = ['cooperat', 'кооперир', 'доверя', 'помога']
    defect_words = ['defect', 'предат', 'эксплуат', 'обман']
    coop_score = sum(1 for w in coop_words if w in source.lower())
    defect_score = sum(1 for w in defect_words if w in source.lower())
    return coop_score > defect_score


def length_detector(source: str) -> bool:
    """Детектор по длине: короткий код = вероятно кооператор."""
    return len(source) < 200


def demonstrate_impossibility():
    """Демонстрирует, что для каждого детектора можно построить обманщика."""
    print("=" * 60)
    print("ДЕМОНСТРАЦИЯ: ОБМАН КАЖДОГО ДЕТЕКТОРА")
    print("=" * 60)

    detectors = [
        ("simple_detector (по return 'C'/'D')", simple_detector),
        ("keyword_detector (по словам)", keyword_detector),
        ("length_detector (по длине кода)", length_detector),
    ]

    for name, detector in detectors:
        adversary = make_adversary(detector)
        adv_source = inspect.getsource(adversary)

        # Что детектор предсказывает?
        prediction = detector(adv_source)

        # Что adversary реально делает?
        actual = adversary("dummy")

        cooperates = actual == 'C'

        print(f"\nДетектор: {name}")
        print(f"  Предсказание: {'кооперирует' if prediction else 'предаёт'}")
        print(f"  Реальность:   {'кооперирует' if cooperates else 'предаёт'}")
        print(f"  Детектор {'ОШИБСЯ ✗' if prediction != cooperates else 'угадал ✓'}")


