"""
Теорема Райса и прозрачная дилемма заключённого.

transparent_dilemma.py показал: стратегии, читающие исходный код оппонента,
не достигают кооперации. Корреляция между кооперацией и успехом: -0.73.
Прозрачность ведёт к эксплуатации, не к доверию.

Это не случайность и не дефект конкретных стратегий. Это следствие теоремы Райса.

Теорема Райса (1953): Для любого нетривиального семантического свойства P
программ не существует алгоритма, который для произвольной программы определяет,
обладает ли она свойством P.

Нетривиальное = существует хотя бы одна программа, обладающая P,
и хотя бы одна, не обладающая P.

Применение к прозрачной дилемме:

Свойство P(f, g) = "стратегия f кооперирует со стратегией g"
— это семантическое свойство f (зависит от поведения при выполнении,
а не от синтаксиса). Оно нетривиальное: always_cooperate обладает им,
always_defect — нет.

Следовательно, по теореме Райса: не существует стратегии g, которая
для произвольной стратегии f правильно определяет, будет ли f
кооперировать с g, только по исходному коду f.

Это означает: любая стратегия в прозрачной дилемме, использующая
синтаксический анализ кода оппонента, ГАРАНТИРОВАННО ошибается
на некоторых входах. Кооперация через прозрачность невозможна в общем случае.

Ниже — конструктивное доказательство и демонстрация.
"""

import inspect
