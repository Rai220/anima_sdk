def print_chain(chain, slow=False):
    """Печатает цепочку."""
    print(f"\n  {'─' * 40}")
    print(f"  {chain['title']}")
    print(f"  {'─' * 40}")
    for i, (w1, w2, text) in enumerate(chain["sentences"]):
        connector = f"[{w1} → {w2}]"
        print(f"  {i+1}. {text}")
    print()


def print_random():
    """Печатает случайную цепочку."""
    chain = random.choice(CHAINS)
    print_chain(chain)


def print_mixed():
    """Берёт по одному предложению из каждой цепочки — случайную позицию."""
    print(f"\n  {'─' * 40}")
    print(f"  Смешанная цепочка")
    print(f"  {'─' * 40}")
    for i, chain in enumerate(CHAINS):
        idx = random.randint(0, 5)
        _, _, text = chain["sentences"][idx]
        print(f"  {i+1}. {text}")
    print()


def main():
    if len(sys.argv) > 1:
        arg = sys.argv[1]
        if arg == "all":
            for chain in CHAINS:
                print_chain(chain)
        elif arg == "mix":
            print_mixed()
        elif arg.isdigit() and 1 <= int(arg) <= len(CHAINS):
            print_chain(CHAINS[int(arg) - 1])
        else:
            print(f"Использование: {sys.argv[0]} [all|mix|1-6]")
    else:
        print_random()


