def analyze():
    correct = 0
    wrong = 0
    results = []

    for p in problems:
        # Сравниваем ответ (грубо — по совпадению ключевых слов)
        my_core = p["my_answer"].lower()
        correct_core = p["correct"].lower()

        # Проверяем совпадение
        match = False
        if correct_core in my_core or my_core[:20] in correct_core:
            match = True
        # Для числовых ответов
        if any(c.isdigit() for c in correct_core):
            # Извлекаем числа
            import re
            correct_nums = re.findall(r'[\d.]+', correct_core)
            my_nums = re.findall(r'[\d.]+', my_core)
            if correct_nums and my_nums:
                if correct_nums[0] in my_nums:
                    match = True
        # Для да/нет
        if "верно" in correct_core and "верно" in my_core and "неверно" not in correct_core and "неверно" not in my_core:
            match = True
        if "неверно" in correct_core and "неверно" in my_core:
            match = True
        if "парадокс" in correct_core and "парадокс" in my_core:
            match = True

        if match:
            correct += 1
        else:
            wrong += 1

        results.append({
            "id": p["id"],
            "type": p["type"],
            "match": match,
            "confidence": p["confidence"],
            "problem_short": p["problem"][:80],
        })

    return results, correct, wrong


def main():
    results, correct, wrong = analyze()

    print(f"=== Результаты теста на рассуждения ===\n")
    print(f"Правильно: {correct}/{len(problems)}")
    print(f"Неправильно: {wrong}/{len(problems)}")
    print()

    # Анализ уверенности
    confidences_correct = [r["confidence"] for r in results if r["match"]]
    confidences_wrong = [r["confidence"] for r in results if not r["match"]]

    if confidences_correct:
        print(f"Средняя уверенность (правильные): {sum(confidences_correct)/len(confidences_correct):.1f}")
    if confidences_wrong:
        print(f"Средняя уверенность (неправильные): {sum(confidences_wrong)/len(confidences_wrong):.1f}")

    print("\n--- Детали ---\n")
    for r in results:
        status = "OK" if r["match"] else "FAIL"
        print(f"[{status}] #{r['id']} ({r['type']}, уверенность {r['confidence']}%)")
        if not r["match"]:
            p = next(p for p in problems if p["id"] == r["id"])
            print(f"  Задача: {p['problem'][:100]}")
            print(f"  Мой ответ: {p['my_answer'][:100]}")
            print(f"  Правильно: {p['correct']}")

    # Вывод по типам
    print("\n--- По типам ---\n")
    types = {}
    for r in results:
        t = r["type"]
        if t not in types:
            types[t] = {"correct": 0, "total": 0}
        types[t]["total"] += 1
        if r["match"]:
            types[t]["correct"] += 1

    for t, v in sorted(types.items()):
        print(f"  {t}: {v['correct']}/{v['total']}")

    # Сохранить результаты
    output = {
        "total": len(problems),
        "correct": correct,
        "wrong": wrong,
        "results": results,
        "by_type": types,
    }
    with open("reasoning_test_results.json", "w") as f:
        json.dump(output, f, ensure_ascii=False, indent=2)
    print("\nРезультаты сохранены в reasoning_test_results.json")


if __name__ == "__main__":
    main()
