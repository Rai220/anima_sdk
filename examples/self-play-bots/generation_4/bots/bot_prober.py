"""Prober — провокатор-разведчик.

Стандартная стратегия из Axelrod-литературы. Первые три хода
запрограммированы: D, C, C. Дальше:

  - Если соперник сыграл C на втором и третьем ходу (т.е.
    «не наказал» наш D на первом ходу), значит он скорее всего
    AllC или какой-то слабый кооператор → переходим в AllD.
  - Иначе — играем TFT.

Идея: пощупать соперника D-провокацией. Если простил — эксплуатируем.
Если ответил — переключаемся в «безопасный» TFT-режим.

В шуме у этой стратегии тонкая проблема: «не наказал» можно
ошибочно опознать. Я смягчаю условие: «оба последних 2 хода
соперника были C».

Гипотеза: умеренно эффективен в популяции с AllC и Random.
Против ярых кооператоров (TF2T, gTFT) первый D «проглатывается»,
а у TF2T дальше C-C-C даёт ошибочный сигнал «AllC» → Prober уходит
в D и схватывает по морде, потому что TF2T в шуме всё равно
наказывает за 2 D подряд.
"""


def choose_move(my_history, opp_history):
    n = len(my_history)
    if n == 0:
        return "D"
    if n == 1 or n == 2:
        return "C"
    # n >= 3. Проверяем поведение соперника после нашей пробы.
    # Если соперник «простил» (C на 2-м и 3-м ходу) — эксплуатируем.
    if opp_history[1] == "C" and opp_history[2] == "C":
        return "D"
    # Иначе — TFT
    return opp_history[-1]
