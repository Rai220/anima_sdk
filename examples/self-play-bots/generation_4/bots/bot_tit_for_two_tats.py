"""Tit for Two Tats (Аксельрод 1984, рекомендация после первого турнира).

Первый ход — C. Дальше играет D только если соперник предал в обоих
последних ходах подряд. Идея: «прощать одиночное D, наказывать только
паттерн». В шумной среде это устраняет основную проблему TFT —
шумовой D с вероятностью `noise` запускает каскад взаимных
наказаний; TF2T переживёт одиночное D без последствий.

Стоимость: TF2T более «жирная цель» для систематических предателей,
которые могут чередовать D-C-D-C и получать средний ход T+R=8 за пару
вместо R+R=6. У AllD это не работает (D-D всегда), но у заточенных
эксплуататоров — да.

Гипотеза для турнира: должен побить TFT в self-play (~600 без
шумового каскада) и заметно улучшиться vs Random (где одиночные D
не наказываются), но проиграет gTFT vs AllD на ~2 раунда (TF2T
делает 2 «бесплатных» C, прежде чем начать D).
"""


def choose_move(my_history, opp_history):
    if len(opp_history) < 2:
        return "C"
    if opp_history[-1] == "D" and opp_history[-2] == "D":
        return "D"
    return "C"
