"""Soft Majority.

Считает, сколько раз соперник сыграл C и сколько D за всю историю.
Играет C, если C ≥ D (равенство — в пользу кооперации). Первый
ход — C.

Свойства:
  - Глобальная статистика, не последний ход → шумоустойчиво.
    Одиночный шумовой D практически не сдвигает счёт.
  - Против AllC: всегда C ≥ D → всегда C. Self-honest cooperation.
  - Против AllD: после нескольких D-ов соперника D становится
    больше → переключается на D. Не зацикливается на C-ах, как
    AllC.
  - В self-play в шуме: при noise=0.02 «истинный» баланс ~98% C,
    оба бота всегда видят C ≥ D → CC до конца матча. Это
    структурно лучше TFT (456) и должно дать ~590.
  - Против Random (50% C): счёт качается у нуля. Иногда D ≥ C,
    тогда играет D, и Random это не наказывает. В среднем шум.

Гипотеза для турнира (по итогам поколения 3): попадает в топ. Это
шумоустойчивый и при этом не глупо-добрый бот.
"""


def choose_move(my_history, opp_history):
    if not opp_history:
        return "C"
    c_count = opp_history.count("C")
    d_count = len(opp_history) - c_count
    if c_count >= d_count:
        return "C"
    return "D"
