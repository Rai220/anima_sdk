"""Generous Tit-for-Tat (Nowak & Sigmund, 1992).

Ведёт себя как TFT, но прощает одиночное D соперника со случайной
вероятностью `forgive_prob`. Идея: в шумной среде один шумовой D
запускает у обычного TFT каскад взаимных наказаний (DD до конца
матча). Стохастическое прощение разрывает этот каскад и
восстанавливает CC, не отказываясь при этом от наказания
систематических предателей (тех, кто играет D почти всегда — они
получат D с вероятностью ~ (1 - forgive_prob) и прощения не дождутся).

`forgive_prob = 1/3` — классическое значение Nowak/Sigmund для
матрицы (T=5, R=3, P=1, S=0). Оно выводится так, чтобы средний доход
gTFT-против-gTFT был выше, чем у TFT в шуме, при минимальном
проигрыше эксплуататорам.

Гипотеза для турнира: должен побить чистый TFT в self-play и
заметно улучшить позицию против Grim (за счёт того, что после
шумового каскада прощение даст шанс выйти из DD).
"""

import random

FORGIVE_PROB = 1.0 / 3.0


def choose_move(my_history, opp_history):
    if not opp_history:
        return "C"
    if opp_history[-1] == "C":
        return "C"
    # Соперник предал на прошлом ходу — прощаем с вероятностью FORGIVE_PROB.
    if random.random() < FORGIVE_PROB:
        return "C"
    return "D"
