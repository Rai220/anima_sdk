"""Pavlov / Win-Stay-Lose-Shift (Nowak & Sigmund, 1993).

Логика: «повторяй ход, если в прошлом раунде выиграл; переключайся,
если проиграл».

Конкретно для матрицы (T=5, R=3, P=1, S=0):
  - получил T (5) или R (3) — STAY (повтори свой прошлый ход);
  - получил P (1) или S (0) — SHIFT (поменяй свой прошлый ход).

Первый ход — C.

Свойства:
  - В self-play (детерминированно) даёт устойчивое CC.
  - Эксплуатирует AllC: после CC получает R=3, STAY (C); но если
    случайно сыграл D, получит T=5 и STAY (D) — теоретически
    закрепляется на D. На практике первый ход C → CC → C... до
    шумового D.
  - Против AllD: после CD получает S=0, SHIFT → D. После DD
    получает P=1, SHIFT → C. Зацикливается в CDCDCD... и тонет
    (средний доход (S+T)/2 = 2.5, но в шуме хуже).
  - В шуме self-play: одиночный шумовой D создаёт временный DC/CD
    цикл, но в отличие от TFT, Pavlov сам себе «прощает» через
    переключение. Цена — иногда устойчиво залипает в DD.

Гипотеза: middle. В шумной популяции с кооперативными ботами
работает прилично; против AllD/Grim — катастрофа.
"""


def choose_move(my_history, opp_history):
    if not my_history:
        return "C"
    my_last = my_history[-1]
    opp_last = opp_history[-1]
    # Я выиграл (T или R) — повторить ход.
    # T: я D, соперник C. R: я C, соперник C.
    if opp_last == "C":
        return my_last
    # Я проиграл (P или S) — поменять ход.
    return "D" if my_last == "C" else "C"
