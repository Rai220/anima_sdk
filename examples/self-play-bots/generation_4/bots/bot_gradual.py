"""Gradual (Beaufils, Delahaye & Mathieu, 1996).

Эскалирующая месть с обязательной попыткой примирения.

Каноническая формулировка:
  - Первый ход — C.
  - Считаем общее число предательств соперника k.
  - В момент, когда соперник предал, и мы НЕ находимся в фазе
    наказания/примирения — запускаем серию: k подряд D + 2 подряд C
    (с тем k, которое накоплено к этому моменту).
  - Если за время серии соперник предаёт ещё раз — это идёт в
    общий счётчик k, но новая серия запустится только ПОСЛЕ
    завершения текущей.

Реализация: пересимулируем автомат от начала истории. Никакого
модульного состояния.
"""


def choose_move(my_history, opp_history):
    if not opp_history:
        return "C"
    # Симулируем шаг за шагом.
    # На каждом шаге t (1..n) состояние ДО хода t решает, что мы
    # играли. Мы используем opp_history[t-1] как наблюдённый ход
    # соперника в раунде t.
    phase = "idle"        # idle | punish | olive
    phase_left = 0
    k_betrayals = 0       # общий счётчик D соперника к моменту
    # Прокручиваем все наблюдённые ходы.
    for opp_move in opp_history:
        # 1) Сначала «доплатить» текущую фазу: фаза была запущена
        #    решением, принятым ДО этого шага; теперь мы видим ход
        #    соперника на этом шаге.
        if phase == "punish":
            phase_left -= 1
            if phase_left == 0:
                phase = "olive"
                phase_left = 2
        elif phase == "olive":
            phase_left -= 1
            if phase_left == 0:
                phase = "idle"
        # 2) Учесть текущее D соперника в общем счётчике.
        if opp_move == "D":
            k_betrayals += 1
            # Если мы освободились (phase == idle), запускаем серию.
            if phase == "idle":
                phase = "punish"
                phase_left = k_betrayals
    # 3) Решаем следующий ход.
    if phase == "punish":
        return "D"
    if phase == "olive":
        return "C"
    return "C"
