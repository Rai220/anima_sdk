"""Hard Majority.

Зеркало Soft Majority: играет D, если D ≥ C в истории соперника.
Первый ход — D (вот это и делает её «жёсткой»).

Свойства:
  - Стартует с D — это сразу понижает её против nice стратегий
    в первый раунд, но даёт защиту от провокаторов.
  - На втором раунде у соперника один ход в истории. Если C
    (Hard Maj получил T=5), то D < C → играет C. Если D, то
    D ≥ C → D.
  - В шуме self-play: первый D-D с обеих сторон уже даёт счёт
    1-1. После этого может никогда не «выбраться» в CC — каждое
    шумовое D у соперника увеличивает порог.

Гипотеза: жёсткая, слабая в кооперативной популяции. В средние/
низы рейтинга.
"""


def choose_move(my_history, opp_history):
    if not opp_history:
        return "D"
    c_count = opp_history.count("C")
    d_count = len(opp_history) - c_count
    if d_count >= c_count:
        return "D"
    return "C"
