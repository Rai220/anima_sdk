"""Contrite Tit-for-Tat (Boyd 1989, Wu & Axelrod 1995).

TFT с раскаянием. Идея в шумной среде:

  - Если я НАМЕРЕВАЛСЯ играть C, но из-за шума соперник увидел D
    и теперь меня наказывает — я ВИЖУ ситуацию (S или P против C
    в прошлом раунде у соперника) и НЕ отвечаю D на его D, а
    «извиняюсь» через C. Это разрывает каскад.

Поскольку бот не знает, был ли шум, и видит ТОЛЬКО историю после
шума, реализуем приближение:

  Состояние «contrite» (раскаяние) включается, когда:
    - последний мой ход был D,
    - предпоследний мой ход был C,
    - соперник в прошлом раунде сыграл D (наказывает меня).
  То есть похоже на каскад «я случайно дёрнул D → он меня бьёт».
  В этом состоянии играю C один раз, что должно разорвать каскад.

В остальном — TFT.

Гипотеза: лучше TFT в self-play, чуть слабее vs AllD (тратит
дополнительные «извинения»). Должен попасть в верхнюю половину.
"""


def choose_move(my_history, opp_history):
    if not opp_history:
        return "C"
    # Если соперник предал в прошлом ходу, проверяем contrite-условие:
    # я только что предал, а до этого кооперировал.
    if (
        len(my_history) >= 2
        and my_history[-1] == "D"
        and my_history[-2] == "C"
        and opp_history[-1] == "D"
    ):
        return "C"
    # Стандартный TFT
    return opp_history[-1]
