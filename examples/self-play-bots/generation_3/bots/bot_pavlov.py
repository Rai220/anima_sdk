"""Pavlov / Win-Stay-Lose-Shift (WSLS).

Идея (Nowak & Sigmund, 1993, Nature): не копируй соперника, а
повторяй свой ход, когда он принёс «выигрыш» (R или T), и меняй
ход, когда он принёс «проигрыш» (S или P).

Правило по матрице (T=5, R=3, P=1, S=0):
    (C, C) → R=3 → win, остаюсь C
    (D, C) → T=5 → win, остаюсь D
    (C, D) → S=0 → lose, переключаюсь на D
    (D, D) → P=1 → lose, переключаюсь на C

Эквивалентно: «играй C, если мы согласовались (CC или DD); играй D,
если разошлись (CD или DC)». Это очень компактная локальная
обратная связь.

Что ожидаю:
- vs self: после любого шумового флипа быстро восстанавливается
  через короткий цикл CD→DC→DD→CC. Шумовой каскад длится 2 раунда,
  не неограниченно (как у TFT/Grim).
- vs AllC: первый ход C, получаю 3 (win). Стою C → CC поток. Но
  любой шумовой флип на мой D даёт мне 5 (win) → стою D → начинаю
  эксплуатировать AllC. Pavlov «находит» эксплойт-режим через шум.
- vs AllD: первый ход C, ловлю S=0 → переключаюсь на D. Играю D,
  ловлю P=1 → переключаюсь на C. Колеблюсь CDCD… → 1.5 за раунд.
  Хуже, чем TFT (1.05), потому что отдаёт T соперникам.
- vs TFT: первый ход обоих C → CC. Шумовой флип превращается в
  каскад CD→DC→DD→CC у Pavlov, но TFT слепо копирует, поэтому
  цикл занимает 3-4 хода, а не 2.
- vs Grim: первый шумовой D от меня → Grim ушёл в D навсегда →
  я начинаю осциллировать против него → ~1.5 за раунд.
- vs Random: каждое разногласие провоцирует мой D, каждое согласие
  — C. Эффективно я отслеживаю соперника, но без памяти на длинную
  историю.

Главная сила Pavlov, по литературе:
    «знает», когда эксплуатировать беззащитного, и «знает», когда
    кооперировать с равным. Не нуждается в счёте раундов или
    статистике — чисто реактивен на последний ход.

Главная слабость:
    периодически даёт T=5 соперникам, которые сами не реагируют
    (AllD, Grim после шумового флипа). Поэтому Pavlov плох против
    «глухих» эксплуататоров, но хорош против «слышащих».
"""


def choose_move(my_history, opp_history):
    # Первый ход — кооперация. Pavlov «по умолчанию» nice.
    if not my_history:
        return "C"
    my_last = my_history[-1]
    opp_last = opp_history[-1]
    # win-stay: оба согласились (CC или DC) — повторяю.
    # lose-shift: разошлись (CD или DD) — меняю.
    if my_last == opp_last:
        # CC = win (R=3) — stay C, или DD = lose (P=1) — shift to C.
        # В обоих случаях возвращаю C.
        return "C"
    else:
        # CD = lose (S=0) — shift to D, или DC = win (T=5) — stay D.
        # В обоих случаях возвращаю D.
        return "D"
