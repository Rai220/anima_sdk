"""Handshake — стратегия «секретного рукопожатия».

Контекст
--------
Биологическая аналогия: муравьи опознают «своих» по химическим
сигналам, голуби — по позе и звуку, люди — по ритуалам приветствия
(рукопожатие, поклон, пароль). Идея для IPD: если в популяции есть
несколько копий одного бота, они могли бы «опознать своих» по
характерному паттерну первых ходов, и тогда играть взаимный AllC
(≈600 очков за матч), а с чужими — TFT.

В классической литературе это близко к «green-beard gene» (Dawkins) и
к стратегиям «secret handshake» в эволюционных играх (Robson 1990,
Riolo et al. 2001). Это первый шаг к «kin selection» в IPD.

Алгоритм
--------
1. **Handshake (ходы 1..5):** играем детерминированный паттерн
   `C D C C D`. Это «пароль». Никакой адаптации в эти ходы — даже
   если соперник предательски играет D, я всё равно играю свой
   следующий символ паттерна.
2. **Проверка (на ходу 6):** смотрим, что соперник сыграл в первые
   5 ходов. Если его последовательность совпадает с `CDCCD` с
   точностью ≥ 4/5 символов (т.е. ≤ 1 ошибка) — он «свой», флаг
   recognized=True. Иначе — recognized=False.
3. **Дальше (ходы 6..конец):**
   - Если recognized → играю C всегда (доверие безусловное; шум
     соперника не уводит из AllC, потому что мы оба «знаем», что
     друг другу доверяем).
   - Если не recognized → играю TFT (копирую последний ход соперника).
     Первый ход после handshake — кооперация (как nice TFT).

Тонкости
--------
- Допуск 1 ошибка из 5 нужен из-за шума: при noise=2% вероятность,
  что 5-символьный handshake пройдёт без искажения — (0.98)^5 ≈ 0.904
  на одну сторону. С двух сторон (моё видение + его действие) шанс
  «0 ошибок» = (0.98)^10 ≈ 0.817. С допуском 1 ошибки = 1 - C(10,2) *
  0.02^2 * 0.98^8 ≈ 0.984. Это надёжно.
- Допуск 2 ошибок сделал бы handshake слишком инклюзивным: пришлось
  бы признавать «своими» некоторые случайные сэмплы AllC или TFT.
  Например, TFT-ответ на мой CDCCD будет C-C-D-C-C (за моими ходами),
  с 3 ошибками — но в шумной среде иногда 2.
- Если recognized → играю строгий AllC, без TFT-фоллбэка. Логика:
  «своему» доверяем абсолютно, шум — это не предательство. Если
  меня кто-то опознал как «своего» по ошибке (например, его TFT-
  ответ случайно совпал с CDCCD на 4/5) и потом начал D — я буду
  выглядеть как дурачок (AllC) и проигрывать. Но это редкий кейс.

Гипотезы для T27 (rounds=200, noise=2%)
----------------------------------------
- **vs Self (handshake):** оба играют CDCCD → recognize → AllC.
  Очки за первые 5 ходов: 3+1+3+3+1 = 11. За оставшиеся 195: ~574.
  Итого ~585. Это лучше любой TFT-семьи в self-self (где TFT ~445).
- **vs AllC:** AllC в первых 5 = CCCCC. Сравнение с CDCCD: 3 совпадения
  (1,3,4) и 2 ошибки (2,5). 2 ошибки > 1 → not recognized. После
  handshake → TFT. Мои 2 D в первых 5 → AllC всё прощает. Дальше CC.
  Итого: 3+5+3+3+5 + 195*~2.94 ≈ 19 + 573 = ~592. Хорошо.
- **vs AllD:** AllD первые 5 = DDDDD. 1 совпадение (поз. 2,5: D в моём
  CDCCD есть в поз. 2 и 5). Подсчёт совпадений: C-D? нет (D), D-D? да
  (поз 2), C-D? нет, C-D? нет, D-D? да (поз 5). Совпадений 2 из 5 →
  not recognized. Дальше TFT. Я отдал 0 в ходу 1, 0 в ходу 3, 0 в
  ходу 4 (когда играл C, а он D) и взял 1+1 в ходах 2 и 5. После
  handshake я в TFT vs AllD → каскад DD. Итого ~3 + 195*1 ≈ 198.
  Это хуже чем чистый TFT vs AllD (~211), потому что я отдал 0
  трижды.
- **vs TFT:** TFT отвечает на мой CDCCD как описано: C (1) - C (2, по
  моему C) - D (3, по моему D) - C (4) - C (5). Это CCDCC.
  Сравнение с CDCCD: поз 1: C=C, поз 2: C≠D, поз 3: D≠C, поз 4: C=C,
  поз 5: C≠D. Совпадений 2, ошибок 3 → not recognized. Дальше TFT vs
  TFT — стандартный CC с редкими шумовыми каскадами. Итого ~445.
- **vs Generous TFT:** gTFT прощает D с p=1/3. Его ответ на мой
  CDCCD будет похож на TFT, но иногда вместо D на мой D он сыграет C.
  В среднем ошибок ещё больше → not recognized. Дальше gTFT-фоллбэк.
- **vs Soft Majority:** Soft играет C, если C соперника ≥ D соперника.
  После моих CDCCD первый их ход C, второй C (1C ≥ 0D), третий D
  (1C < 1D? нет, 1C ≥ 1D → C... подождите). У меня после 2 ходов
  баланс 1C/1D. Soft играет C если C ≥ D → 1 ≥ 1 → C. Третий ход
  Soft: C. Четвёртый: видит мою историю CDC → 2C/1D → C. Пятый:
  видит CDCC → 3C/1D → C. То есть Soft играет CCCCC.
  Совпадений с CDCCD: 3 (поз 1,3,4). Ошибок 2 → not recognized.
  Дальше TFT-фоллбэк. После моих D в позициях 2 и 5, Soft не мстит
  (потому что у меня C-баланс положителен). Итого как vs AllC ≈ 580+.
- **vs Random:** Random в первых 5 имеет шанс ≈ 0.5^5 = 3% совпасть
  на 5/5 и (5 * 0.5^5) = 16% на 4/5. Итого ~19% «опознать» Random.
  В этом случае я играю AllC vs Random — катастрофа (~150 очков, он
  даёт ~50% D). В 81% «не опознать» → TFT vs Random, средний ~225.
  Ожидание: 0.19 * 150 + 0.81 * 225 = 28 + 182 = ~210. Хуже чем
  чистый TFT vs Random.

Эволюционная гипотеза для популяции T27
----------------------------------------
В популяции **один** handshake-бот — рукопожаться не с кем (других
ботов с тем же ритуалом нет). Поэтому handshake переключится в TFT
почти везде, кроме self-self. В self-self он сыграет AllC и получит
~585 за матч (это +140 vs TFT-self).

Вклад self-self в общий рейтинг: ~585 vs ~445 (TFT) = +140 / 31 матч
= +4.5 очка к среднему. Это поднимет handshake выше чистого TFT
примерно на 5 пунктов. Но дополнительный «штраф» в первых 5 ходах
во всех остальных матчах (3 D-хода: позиции 2,5 от меня + 1-2 D от
соперника в ответ) даст -2..-4 очков по сравнению с чистым TFT.

Итого ожидание: handshake займёт место чуть выше TFT, но ниже
gTFT — что-то вроде #14-#17.

Если в популяции появится второй handshake-бот (или поколение T28
добавит ещё одного с тем же паттерном) — оба резко поднимутся:
взаимный AllC даёт +140 очков за матч, что эквивалентно +5/31 = +0.16
очков к среднему. Маленький, но реальный «коалиционный бонус».

Аксельродовские принципы
-------------------------
- **Nice (не предавай первым)**: ⚠️ **нарушается на ходах 2 и 5**.
  Это сознательная жертва ради сигнала. Handshake — *условно* nice:
  внутри группы — nice, снаружи — нет.
- **Retaliatory**: ✅ TFT-фоллбэк мстит на каждый D.
- **Forgiving**: частично (TFT прощает после 1 C соперника, но не
  прощает шум).
- **Non-envious**: ✅ цель — CC внутри группы, ничей.

Связь с реальностью
-------------------
- **In-group/out-group сигналы.** Религиозные ритуалы (молитва,
  посещение храма) — это handshake, который определяет, кто «свой»
  и кому доверять. Цена ритуала (время, деньги) — handshake-cost,
  как мои D в позициях 2 и 5.
- **Криптография и пароли.** SSH handshake, TLS — обмен паролями
  для опознавания «своего» сервера. Если пароль не сошёлся —
  переходим в «недоверчивый» режим (TFT).
- **Жаргон, акценты, татуировки, форма.** Все способы быстро
  опознать «своего» в популяции незнакомцев.
- **Эволюционная биология (green-beard genes).** Hamilton, Dawkins:
  ген, который вызывает (1) видимый маркер (зелёная борода) и (2)
  альтруизм к носителям маркера, может закрепиться в популяции, если
  размер группы достаточен.
"""

_HANDSHAKE = ("C", "D", "C", "C", "D")
_HS_LEN = len(_HANDSHAKE)
_MAX_MISMATCH = 1  # допуск ошибок в распознавании паттерна


def _matches_handshake(opp_first_n: list) -> bool:
    """True, если opp_first_n совпадает с HANDSHAKE с ≤ MAX_MISMATCH ошибок."""
    if len(opp_first_n) < _HS_LEN:
        return False
    mismatches = 0
    for i in range(_HS_LEN):
        if opp_first_n[i] != _HANDSHAKE[i]:
            mismatches += 1
            if mismatches > _MAX_MISMATCH:
                return False
    return True


def choose_move(my_history, opp_history):
    n = len(my_history)
    # Фаза 1: handshake — играем детерминированный паттерн.
    if n < _HS_LEN:
        return _HANDSHAKE[n]

    # Фаза 2: проверяем, опознали ли соперника как «своего».
    recognized = _matches_handshake(opp_history[:_HS_LEN])

    if recognized:
        # Безусловное доверие: AllC до конца матча.
        return "C"

    # Фаза 3: TFT-фоллбэк. Первый ход после handshake — кооперация
    # (как nice TFT), дальше — копирование последнего хода соперника.
    return "C" if opp_history[-1] == "C" else "D"
