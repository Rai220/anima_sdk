"""Adaptive AllC — «голубь с красной линией».

Идея. Чистый AllC проигрывает катастрофически AllD (0 за матч),
Random (~300), любому эксплуататору. Но AllC сильно дружит со всеми
кооператорами и доносит до них 600 в матчах vs AllC/gTFT/TFT.

Adaptive AllC — это AllC + «детектор систематического предательства»
по двум условиям:

1. **Абсолютный порог**: opp_d >= 5. Один D — это шум (noise=0.02
   за 200 раундов даёт ожидаемые 4 D от AllC). Пять — это уже паттерн.
2. **Относительный порог**: opp_d / n > 0.10. Доля D больше 10%
   означает, что соперник систематически предаёт, а не «шумовая
   капля». Чисто шумовой AllC имеет рейт ~0.02.
3. **Гард**: первые 10 раундов не переключаемся (n >= 10), чтобы
   случайные ранние D не сорвали бота.

Когда оба условия выполнены — перехожу в AllD навсегда (Grim-tail).

Почему это интересно:

- В отличие от Grim (триггер с 1 D), Adaptive AllC устойчив к шуму:
  чистый AllC после шумовых ~4 D не доводит до триггера за 200
  раундов.
- В отличие от Hard Majority (доля C соперника < 0.5), Adaptive AllC
  гораздо «прощённее»: переключается только если doля D > 0.10.
  Hard Majority перетряхивает гораздо больше кооператоров, как
  показал T7.
- В отличие от a-gTFT, нет «cautious»-режима: переключение
  необратимо. Это значит, что Adaptive AllC жёстче по эксплойтам,
  но в шумном кооперативном матче иногда «слипается» в DD на
  фоне ложного срабатывания.

Ожидаемое поведение:

- vs AllD: opp_d/n = 1.0 быстро, после 10 раундов триггер → DD до
  конца. Очки ≈ 190. Сильно лучше pure AllC (0).
- vs AllC: opp_d за 200 раундов от шума ≈ 4 < 5. **Не срабатывает.**
  Очки ≈ 580-600 (как pure AllC).
- vs Random: opp_d ≈ 100, рейт = 0.5. Срабатывает на ~раунде 12.
  Потом DD vs Random, где Random 50/50 → C: я D, score 5+1=3 в
  среднем; D: я D, score 1+1=1. Среднее 2. Так что ~10*1.5 + 190*2
  = 395. Лучше pure AllC vs Random (300).
- vs TFT-семья: opp_d от моих шумовых D ≈ 4 (1-в-1 ответы), плюс
  собственный шум ≈ 4, итого ~8 за 200 раундов. Рейт 0.04 < 0.10
  → **не срабатывает**, играю как AllC. Должен повторить рейтинг
  AllC в этих матчах (~580 vs TFT).
- vs Grim: Grim после шумового флипа уходит в вечный D. К раунду
  30-50 у меня opp_d > 5, рейт > 0.5 (с момента флипа). **Срабатывает.**
  Дальше DD. Score ≈ 30*0 + 170*1 ≈ 170. Pure AllC vs Grim = ~30
  (потому что Grim играет D после ~раунда 30 в среднем). Здесь
  выигрываю ~140 vs Grim.
- vs Joss: Joss играет D с вероятностью ~0.1 + ответ на мои D. Рейт
  D за 200 раундов ≈ 0.10-0.15. **На границе** — может сработать,
  может нет. Скорее всего да. Тогда после ~50 раундов DD vs sneaky
  Joss → борьба за P=1 vs T=5 в редких случаях.
- vs Pavlov: Pavlov переключается после моих шумовых D, играет
  D-каскадно. Возможно ~6-10 D на 200 раундов. На границе.
- vs Tester: Tester провоцирует первый D, потом смотрит. У меня
  opp_d стартует с 1, рейт 1.0 на n=1. Но n<10 — не переключаюсь.
  Дальше Tester видит, что я не отвечаю и играет C. opp_d остаётся
  1-2 за матч. **Не срабатывает**. Я остаюсь AllC, Tester
  эксплуатирует одним sneak D.

Главный механизм: «прощу один-два D как шум, но не пять».
Это «зрелый кооператор» — не Pollyanna (AllC), не paranoid (Grim),
а условно-доверяющий. В реальном мире — терпимая страна, которая
прощает мелкие нарушения соглашения, но при систематическом
нарушении уходит навсегда (Brexit-mode).

Что НЕ исправляет:

- Раз переключившись в D-режим, не возвращается. Это значит, что
  если соперник был Grim и после своего шумового флипа застрял в D,
  а потом «вернулся» к C (например, через Гilbert-recovery — но
  таких ботов нет), Adaptive AllC всё равно играет D. В нашей
  популяции таких ботов нет, поэтому это не проблема.
- Pre-trigger эксплойт: первые 10 раундов соперник может бесплатно
  получать T=5 от меня. Tester этим пользуется (получает 5 на ходу 1).
  Но это маленькая потеря.
"""


_TRIGGER_ABS = 5      # минимальное абсолютное число D от соперника
_TRIGGER_RATE = 0.10  # минимальная доля D от соперника
_WARMUP = 10          # сколько раундов ждать до решения


def choose_move(my_history, opp_history):
    n = len(opp_history)
    if n == 0:
        return "C"
    # Warmup: первые 10 раундов — чистый AllC.
    if n < _WARMUP:
        return "C"
    opp_d = n - opp_history.count("C")
    # Триггер: соперник систематически предаёт.
    if opp_d >= _TRIGGER_ABS and opp_d / n > _TRIGGER_RATE:
        return "D"
    return "C"
