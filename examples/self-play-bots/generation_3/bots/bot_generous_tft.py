"""Generous Tit for Tat (gTFT) — TFT с вероятностным прощением.

Идея (Nowak & Sigmund, 1992): TFT слепо копирует, поэтому одна
шумовая ошибка запирает обоих в каскаде DD. gTFT решает это
стохастическим прощением: если соперник сыграл D, я с
вероятностью g всё равно играю C. Это вероятностно «сбрасывает»
каскад взаимного наказания.

Параметр g для нашей матрицы (T=5, R=3, P=1, S=0):
    g* = min(1 - (T-R)/(R-S), (R-P)/(T-P))
       = min(1 - 2/3, 2/4) = min(1/3, 1/2) = 1/3.

Это теоретически оптимальная generosity для классической IPD.

Что ожидаю:
- vs self: восстанавливается из шумовых каскадов → > TFT(self).
- vs TFT: иногда прощает шум, не давая каскаду нарастать.
- vs AllD: тратит ~1/3 ходов на бесполезное C → проигрывает чуть
  больше, чем TFT (211 → ~190 за матч).
- vs AllC: эксплуатации нет, gTFT всегда играет C первым.
- vs Random: симметричное прощение → среднее между TFT и AllC.

Стохастичность: используем свой random.Random без сидинга,
чтобы шум прощения не коррелировал с шумом турнира.
"""

import random

# Свой генератор — не трогаем глобальный random, чтобы не мешать
# другим ботам и не зависеть от внешнего seed.
_rng = random.Random()
GENEROSITY = 1.0 / 3.0


def choose_move(my_history, opp_history):
    # Первый ход — кооперация (бот «nice»).
    if not opp_history:
        return "C"
    last_opp = opp_history[-1]
    if last_opp == "C":
        return "C"
    # Соперник сыграл D: с вероятностью g всё равно прощаем.
    if _rng.random() < GENEROSITY:
        return "C"
    return "D"
