"""Hard Majority — жёсткий двойник Soft Majority.

Симметричный аналог `bot_soft_majority`, но с противоположной
дефолтной установкой и строгим неравенством:

- Первый ход: **D** (нет данных — презумпция враждебности).
- Дальше: считаю в `opp_history` число C и D. Если `opp_C > opp_D`
  (строго) — играю C. Иначе — D.

То есть «голосование большинством», но ничья (включая R1 со счётом
0/0) разрешается в пользу D. Это делает стратегию **«not nice»**
по Аксельроду (предаёт первой) и одновременно **долго-помнящей**:
один шумовой D соперника не выводит из C, потому что баланс почти
не меняется, но и один шумовой C не выводит из D в начале матча.

## Чем отличается от уже существующих ботов

- **vs Soft Majority.** Soft в R1 = C (nice), Hard в R1 = D (мстит
  сразу). Soft использует `>=`, Hard — `>`. Эти отличия минимальны
  по семантике, но огромны по последствиям:
  - Hard self-play даёт DD всегда (оба в R1 = D, статистика
    остаётся 0/N, никогда не возвращаемся в C). Soft self-play
    даёт почти чистый CC.
  - Hard vs AllC = после нескольких ходов opp_C >> opp_D → C. Но
    первые 1-2 хода D дают Hard'у эксплоит на T=5.
  - Hard vs TFT — DD-каскад (моё R1=D, TFT R2=D-копия; opp_D
    растёт быстрее opp_C).
- **vs Grim.** Grim играет C в R1; я D. Grim переключается в
  D навсегда. opp_C=1, opp_D растёт. Я остаюсь в D. DD до конца.
- **vs AllD.** Оба D всегда. opp_C=0, opp_D=200 → D всегда. DD=200.
- **vs Pavlov.** Сложно: Pavlov шифтит. Мой R1=D → Pavlov C в R1.
  R2: Pavlov видит S=0 → шифт → D. Я вижу opp[:1]=[C], opp_C=1>0=opp_D
  → C. R3: Pavlov видит T=5 → стая → D. Я вижу [C,D] → 1=1 → D.
  R4: Pavlov видит P=1 → шифт → C. Я вижу [C,D,D] → 1<2 → D.
  R5: Pavlov видит S=0 → шифт → D. И так далее. Преимущественно DD.

## Гипотезы по матчам

1. **vs AllC ≈ 600.** R1=D (T=5), opp_C растёт. С R2: opp_C=1>0=opp_D
   → C. CC до конца. Чистая выгода: 5 + 3×199 = 602.
2. **vs AllD ≈ 200.** DD весь матч. 1×200 = 200.
3. **vs Self-play ≈ 200.** Оба D в R1, статистика 0/1 → D в R2.
   Если шум не флипнет — DD навсегда. С шумом 2% за 200 раундов
   ожидаемое число C от соперника ≈ 4. Когда opp_C впервые
   превысит opp_D (что маловероятно при ровном шуме) — кратковременно
   вернёмся в C. Среднее ≈ 220.
4. **vs TFT.** R1: я D, TFT C. R2: я опp_C=1>0 → C, TFT копирует
   мой D → D. R3: я opp=[C,D], 1=1 → D, TFT копирует C → C. R4:
   я opp=[C,D,C], 2>1 → C, TFT копирует D → D. Это **CDCD-каскад
   с очень редкими переключениями**. Большую часть матча выйдет
   CDCD ≈ среднее 2.5 за раунд = 500.
5. **vs Soft Majority.** Я D в R1, Soft C в R1. Я R2: opp_C=1>0 →
   C, Soft R2: opp_D=1>opp_C=0 → D (потому что 1 не >= 1, wait —
   Soft использует `c >= d`, у Soft opp_history=[D], c=0, d=1,
   0>=1 ложно → D). Хм, оба в D в R2. R3: я opp=[C,D], 1=1 →
   D; Soft opp=[D,C], c=1>=d=1 → C. R4: я opp=[C,D,C], 2>1 →
   C; Soft opp=[D,C,D], c=1<d=2 → D. И так далее: затухающие
   колебания. Скорее всего, среднее ~2.0-2.5 за раунд.
6. **vs Random.** Random ~50% C. opp_C ≈ opp_D к концу. Я часто
   на D. Mid-range.
7. **vs Tester.** Tester D-C-D-C... в первые 2 хода, потом TFT-режим
   при моём R1=D. Мой R1=D → Tester видит D → переходит в TFT-режим.
   Дальше как vs TFT, но шумом сильнее.
8. **vs TF2T, gTFT, cTFT, a-gTFT.** Все они начинают C, я начинаю D.
   Они прощают мой R1=D (TF2T особенно — нужно 2 D подряд). R2 я
   уже C. R3, R4 — взаимная CC после короткой расстройки. Ожидаю
   близко к 580-600 во всех парах.
9. **vs Omega TFT.** Omega C в R1, я D. Я R2 видя opp=[C] → C.
   Omega видит [my=D, his=C] = классический сигнал. Omega
   адаптируется. Большая часть матча — CC. Скорее всего, 550-590.

## Главное предсказание

Hard Majority должен оказаться **в нижней половине** (#10-#13):
- Сильные провалы в self-play и vs Grim, vs TFT (CDCD-каскад вместо CC).
- Преимущество только vs AllC (+5 на R1) и vs AllD (паритет).
- Не должен попасть в топ-3, не должен сломать стабильность
  {Soft Majority, a-gTFT, Omega TFT}.

Цель добавления: **подтвердить стабильность топ-3 на T12.**
Если множество топ-3 останется неизменным (3 турнира подряд:
T10, T11, T12) — переходим к финальному отчёту.

## Параллель с реальным миром

Hard Majority — это **«презумпция виновности»**. В юридических
системах с такой презумпцией (исторически — некоторые тоталитарные
режимы, инквизиция) каждый новый человек считается опасным, пока
не докажет обратное. В IPD это даёт скверный старт (упускаешь
все CC-возможности с честными игроками), но защищает от провокаторов
ровно тем же эксплоитом в R1, который провокатор использует против тебя.

В международной политике это **«доктрина превентивного удара»**:
бить первым, чтобы соперник не получил инициативы. Исторически
такая доктрина (Германия 1914, США в Ираке 2003) приводила к
затяжным DD-каскадам — что в точности повторяется в нашей модели.
"""


def choose_move(my_history, opp_history):
    c_count = opp_history.count("C")
    d_count = len(opp_history) - c_count
    # «Жёсткое» большинство: строгое >, ничья (включая R1 = 0/0) — в пользу D.
    if c_count > d_count:
        return "C"
    return "D"
