"""Tit for Two Tats (TF2T) — самый мягкий ретализатор.

Классика IPD. Полярная противоположность TTFT (`bot_two_tits_for_tat.py`):

- TFT: один D соперника → один D в ответ.
- TTFT: один D соперника → два D в ответ.
- TF2T: один D соперника → ничего; ДВА D подряд → один D в ответ.

Логика:
- Первые два хода: C (нет достаточной истории для возмездия).
- Дальше: если последние ДВА хода соперника — оба D, играю D.
  Иначе — C.

Это эквивалентно: «прощаю один шумовой/изолированный D, но не серию».

Ожидания (гипотезы перед прогоном T7):

1. Self-play почти оптимален. Один шумовой D соперника не запускает каскад
   (я отвечаю C, он отвечает C → CC восстановлено). Должно дать 580-600
   в self-play, в зоне gTFT (593) и a-gTFT (589).
2. vs AllC = 600 — никогда не атакую первым.
3. vs AllD катастрофа. После первого моего D (на ход 3 или позже) AllD
   продолжает D, я продолжаю D — нормально, как у TFT (~210). НО: первые
   2 хода я даю C → AllD ловит T=5 дважды (без шума). Это +10 в его счёт
   и −10 в мой относительно TFT. Должно быть в районе 195-205.
4. vs Grim. Я не триггерю Grim первым (играю C, пока он играет C). Если
   шумовой флип Grim → D, я прощаю первый D и отвечаю C. Grim продолжает D
   (он Grim). На моём «новом» решении: opp_history = [..., C, D, D] →
   последние два = [D, D] → я играю D. Дальше — длинная DD. Симметрично
   TFT, но я заплатил 2 хода C против D в начале каскада. Хуже TFT на
   ~10-15 очков в этом матче.
5. vs Random. Random имеет ~50% D, и часто серии 2+ D. Я наказываю их
   так же часто, как TFT, но прощаю одиночные D. Должно быть на уровне
   TFT (~440-460).
6. vs TFT. Симметрично «прощающий vs мстящий». Я прощаю шум, TFT — нет.
   Я в среднем больше получаю S=0, чем TFT получает T=5 — потеря для
   обеих, как у gTFT vs TFT (578 vs 594). Ожидаю похожий профиль.
7. vs gTFT, vs cTFT, vs a-gTFT. Эти прощают «своё» (шумовые D себя),
   я прощаю «чужие» одиночные D. В паре это должно давать почти 600,
   но *стабильнее*, чем gTFT(self) — мне не нужен случайный бросок, у
   меня детерминированное прощение по правилу «2 подряд».
8. vs Pavlov. Pavlov эксплуатирует AllC через шум и держит D-серии. Я
   прощу первый D, на втором отвечу D, у Pavlov P=1 → shift на C, я
   вижу [..., D, C] → C, мы возвращаемся в CC. **Возможно лучше TFT**
   в этом матче (TFT попадал в CDCD-циклы с Pavlov).
9. vs TTFT. TTFT отвечает 2D на каждый мой D. Если шумовой D у меня
   случился — TTFT даёт 2 D подряд → я по своему правилу отвечу D →
   TTFT увидит D → ещё 2 D → каскад. Возможно длинные каскады, но мягче
   чем у TFT vs TTFT.

Главная уязвимость TF2T (известна из литературы Аксельрода): «прощающий
эксплуататор» может играть D-C-D-C-D-C... — каждый изолированный D не
вызывает наказания, я остаюсь в C, он получает T=5 каждый второй ход.
В нашей популяции такого бота нет, поэтому ожидаю, что TF2T покажет
себя хорошо. Но это слабость, которую следующая стратегия может
атаковать.
"""


def choose_move(my_history, opp_history):
    # Первые два хода — кооперируем (недостаточно истории для возмездия).
    if len(opp_history) < 2:
        return "C"
    # Если ДВА последних хода соперника были D — наказываю одиночным D.
    if opp_history[-1] == "D" and opp_history[-2] == "D":
        return "D"
    return "C"
