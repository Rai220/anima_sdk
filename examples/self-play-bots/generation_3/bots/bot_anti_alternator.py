"""Anti-Alternator — TFT + детектор Reverse Pavlov / альтернаторов.

Контекст: в турнире T14 (см. SCORES.md) появился `bot_reverse_pavlov`
(Win-Shift-Lose-Stay). Этот бот эксплуатирует «доверчивых
кооператоров» (Soft Majority, AllC, TF2T, Hard Majority), сваливая их
с T=5 каждый второй ход через альтернацию D-C-D-C. Soft Majority,
6 турниров подряд занимавший #1, обвалился с #1 на #4 ровно из-за
проигрыша Reverse Pavlov 311 / 776. Топ-3 рассыпался.

Задача Anti-Alternator: убрать эксплоит-нишу Reverse Pavlov, не
сдвигая верх рейтинга. Идея:

1. **Базовая стратегия — Tit for Tat.** Nice, retaliatory, forgiving.
   Кооперируем с TFT-семьёй (TFT, gTFT, a-gTFT, Omega, cTFT, TF2T,
   Soft Majority, AllC, Pavlov) в обычном CC-режиме. Это критически
   важно — мы НЕ хотим разрушать существующий баланс топ-3.

2. **Детектор RevPav-паттерна.** На каждом ходу проверяем, насколько
   последние 12 ходов соперника соответствуют правилу Reverse Pavlov:

       предсказание_opp[t] = D, если my[t-1] == opp[t-1] иначе C
       (т.е. опп играет D после согласия, C после рассогласования)

   Если ≥ 10/12 совпадений И доля C соперника в последних 12 ходах
   лежит в [3, 9] (исключаем AllD с почти-нулевой C-долей и AllC с
   почти-полной), переключаемся в режим эксплуатации.

   Почему не альтернация как таковая (D-C-D-C-...)? Потому что Reverse
   Pavlov НЕ выдаёт чистую альтернацию в матче со мной (TFT-базой). В
   паре TFT vs RevPav формируется цикл периода 3: (CC, CD, DC, ...).
   Доля чистых альтернаций в opp_history ≈ 8/11, что недостаточно для
   надёжного «alternator»-детектора. Зато правило RevPav (предсказание
   D после равных, C после неравных) выполняется в 11/11 ходов цикла
   (по построению). Это даёт чёткую сигнатуру.

3. **Режим эксплуатации.**
   - Если последний раунд (mh[-1], oh[-1]) НЕРАВНЫЙ (CD или DC) —
     RevPav в следующем ходе сыграет C (по правилу). Я играю D →
     получаю T=5 → следующий раунд снова неравный → RevPav снова C →
     DC-лок до конца матча.
   - Если последний раунд РАВНЫЙ (CC или DD), RevPav сыграет D
     (равно → win-shift → D). Если я тоже сыграю D, мы окажемся в
     (D, D) — снова равный → ещё один (D, D) → DD-лок = P=1/раунд
     (плохо). Чтобы вырваться, играю C: следующий раунд (C, D) =
     неравный → дальше переходим в DC-лок.

   После того как DC-лок установлен, детектор продолжает срабатывать:
   правило RevPav (mh[-1]=D != oh[-1]=C → C; opp сыграет C → совпало).
   Мы залипаем в режиме эксплуатации до конца матча.

4. **Фильтр против AllD.** Если opp играет почти исключительно D
   (c_count < 3 в окне 12), детектор RevPav формально срабатывает (D
   после равных rounds = D, C после неравных… ну, неравных мало). Но
   моя «вырваться из DD через C»-логика отдала бы AllD-у T=5 и сама
   получала бы S=0. Поэтому фильтруем по доле C соперника: только
   если в окне обе стороны (3+ C, 3+ D), считаем сигнатуру.

5. **Фильтр против AllC** (зеркальный, симметрии ради). Если c_count
   > 9, опп почти AllC — никакой эксплуатации, играем TFT (CC до
   конца).

Прогноз для T15:
- vs RevPav: должно быть ~900-960 (vs 463 у TFT, на ~+500). RevPav теряет
  основную добычу.
- vs Soft Majority, AllC, gTFT, TFT, Omega, a-gTFT, cTFT, TF2T, Pavlov:
  TFT-поведение → ~580-600 в каждом из этих матчей (как у TFT, может
  чуть выше из-за отсутствия noise-каскадов специфичных для TFT).
- vs AllD: фильтр C-rate сработает; играем TFT → DD-лок → ~199.
- vs Random: смесь, ~430-460.
- vs Hard Majority, Gradual, Grim, TTFT, Tester, a-Pav: TFT-аналог.

Ожидаемое место: топ-5 (между Omega/gTFT/a-gTFT и Soft Majority).
Главный системный эффект: RevPav падает с #11 на #16-17 (теряет ~500
очков), что вернёт Soft Majority в топ-3, потому что Soft теряет
~25 очков среднего от одной проигрышной матовой жертвы.

Однако возможно, что Anti-Alternator САМ попадёт в топ-3, потому что
у него все плюсы TFT-семьи плюс хирургический эксплоит RevPav. Это
переопределит топ-3 серией. В таком случае серия топ-3 опять
рестартует, но новый топ {Anti-Alt, X, Y} будет устойчивее.

API: чисто детерминированный, stateless, использует только
my_history и opp_history.
"""


def _looks_like_revpav(my_history, opp_history):
    """True, если соперник выглядит как Reverse Pavlov-подобный игрок.

    Условия (все должны выполниться):
    - история ≥ 13 раундов;
    - в последних 12 ходах opp: 3 ≤ count(C) ≤ 9 (т.е. опп не AllD и
      не AllC);
    - в последних 12 ходах правило RevPav предсказывает opp ≥ 10/12.
    """
    n = len(opp_history)
    if n < 13:
        return False

    # Фильтр снизу: отсекаем AllD-подобных. Если в последних 12 ходах
    # < 2 C, опп либо AllD, либо DD-залипший Grim/Hard — против таких
    # не надо «вырываться из DD через C» (мы бы получили S=0 каждый
    # второй раунд). Верхней границы НЕ ставим, потому что в DC-лок-
    # режиме opp играет почти исключительно C, и мы должны оставаться
    # в режиме эксплуатации (sticky-детектор).
    c_count = 0
    for mv in opp_history[-12:]:
        if mv == "C":
            c_count += 1
    if c_count < 2:
        return False

    # Проверка совпадения с правилом Reverse Pavlov на последних 12
    # ходах. Предсказание для opp_history[i] строится из (my[i-1],
    # opp[i-1]): D, если равны, иначе C.
    matches = 0
    for i in range(n - 12, n):
        same_last = my_history[i - 1] == opp_history[i - 1]
        pred = "D" if same_last else "C"
        if opp_history[i] == pred:
            matches += 1
    return matches >= 10


def choose_move(my_history, opp_history):
    # Первый ход — кооперация (nice).
    if not opp_history:
        return "C"

    # Детектор Reverse Pavlov.
    if _looks_like_revpav(my_history, opp_history):
        last_my = my_history[-1]
        last_opp = opp_history[-1]
        if last_my != last_opp:
            # Неравный последний раунд → RevPav сыграет C → играю D
            # → DC-лок (T=5/раунд) до конца.
            return "D"
        # Равный последний раунд (CC или DD). Играю C, чтобы пробиться
        # в неравный раунд: после CC опп играет D → (C, D); после DD
        # опп играет D → (C, D). В обоих случаях следующий раунд
        # неравный, дальше входим в DC-лок.
        return "C"

    # Базовая стратегия: Tit for Tat.
    return opp_history[-1]
