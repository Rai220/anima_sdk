"""Adaptive Pavlov — WSLS с явным выходом против AllD-подобных.

Идея. Классический Pavlov локально оптимален в кооперативном поле,
но имеет известную дыру: против «глухого» эксплуататора (AllD,
Grim-после-триггера, любого long-D) он бесконечно отдаёт T=5 через
lose-shift → C. В LESSONS этот баг подтверждён три турнира подряд:
TFT/Omega/Gradual системно бьют Pavlov через win-stay-D, а сам
Pavlov не накапливает доказательств зла.

Гипотеза: добавить к Pavlov один параметр — глобальный счётчик
«признаков AllD» (доля D в opp_history после warmup). Когда счётчик
переваливает за порог, переключаемся в **постоянный D** до конца
матча. Это сохраняет всё хорошее у Pavlov (короткий цикл
самокоррекции CD→DC→DD→CC после шума) и закрывает главную дыру.

## Правило

1. **R1 = C** — nice по Аксельроду.
2. Если матч короче warmup (8 раундов) — играю классический Pavlov.
3. Иначе считаю opp_D / n. Если > 0.85 — играю D навсегда (без
   возврата). Этот «обрыв» имитирует Grim, но активируется только
   после достаточной статистики, а не от первого D.
4. Если 0.85 ≥ opp_D/n > 0.6 — играю D в этот ход (но не навсегда),
   возвращаюсь к Pavlov-логике на следующем, чтобы дать сопернику
   шанс восстановить статистику. Это «мягкое предупреждение».
5. Иначе — классический Pavlov: stay при CC/DC, shift при CD/DD.

## Чего жду

- **vs AllC.** R1 C, опп C. Дальше CC, как у обычного Pavlov. С
  шумом изредка я флипаюсь в D, ловлю T=5, win-stay = D. Один-два
  раунда эксплоита, потом шум возвращает в CC. opp_D/n остаётся
  низким (~0.02), детектор не срабатывает. Ожидаю 580-600.
- **vs AllD.** R1 C → S=0 → Pavlov shift D. R2 D vs D → P=1 →
  Pavlov shift C. R3 C vs D → S=0 → shift D. …Колебание CDCD.
  Через 8 раундов opp_D/n ≈ 1.0 > 0.85 → переключаюсь в D навсегда.
  Дальше DD всегда. Ожидаю существенно лучше, чем чистый Pavlov
  (115). Целевое ≈ 195-210 (как TFT vs AllD).
- **vs Grim после шума.** Если я флипнусь и Grim уйдёт в D, мой
  детектор увидит «AllD-режим Grim» и переключится в D. Лучше
  Pavlov'овских циклов.
- **vs TFT/cTFT/Omega/gTFT.** В кооперативной зоне opp_D/n низкий,
  Pavlov работает как обычно. Шумовой каскад с TFT длится 3-4 хода
  и сам распадается через WSLS-цикл.
- **vs Pavlov (other).** Симметрично self-play обычного Pavlov:
  любой шумовой каскад затухает за 2-4 хода через P=1 lose-shift.
  Ожидаю 580-590.
- **vs self-play.** Главный тест: оба запускают одинаковый
  детектор. После шумового каскада оба видят свою долю opp_D
  расти. Если оба переключатся в D в один момент — DD-блокада до
  конца. Это структурный риск. Чтобы его смягчить, я выбрал порог
  0.85 (а не, скажем, 0.5) — шум 0.02 за 200 раундов даёт ≈4 D
  у соперника при идеальной CC-игре. 4/200 = 0.02 << 0.85. Детектор
  не должен срабатывать в self-play без массивного каскада.

## Чего не делаю

- Не сохраняю состояние между ходами — каждый раз пересчитываю
  всё из истории. Это требование турнира (модуль может быть
  перезагружен).
- Не использую случайность. По уроку T10 (стохастика в ботах
  даёт нестабильный турнир) предпочитаю детерминизм.
- Не использую окно последних N — глобальная доля устойчивее к
  шуму (урок T8/T5).

## Связь с реальным миром

Это структурно эквивалентно «трудовому договору с пунктом
о расторжении» или «доверию с обратимой репутацией». Партнёр
получает много шансов после единичных ошибок, но накопленные
нарушения переводят отношения в режим «без сотрудничества».
Реальные аналоги: банковские кредитные лимиты (после k просрочек —
закрытие), репутация продавца на eBay (после доли негативных
отзывов > X — бан), международные санкции с накопительным эффектом
(после доли нарушений договора > Y — приостановка действия).

Это всё — Adaptive Pavlov на уровне общества: «реагируй локально
как WSLS, но имей рубильник на случай, если контрагент окажется
систематическим эксплуататором».
"""


def _global_d_rate(opp_history: list[str]) -> float:
    n = len(opp_history)
    if n == 0:
        return 0.0
    return opp_history.count("D") / n


def choose_move(my_history, opp_history):
    n = len(my_history)
    # 1) Первый ход — кооперация.
    if n == 0:
        return "C"

    # 3) Жёсткий exit-режим: если после warmup доля D соперника
    # перевалила порог 0.85, считаю его эксплуататором и фиксируюсь в D.
    # Проверяем перед остальным, чтобы один раз решённый exit не отменился.
    WARMUP = 8
    HARD_THRESHOLD = 0.85
    SOFT_THRESHOLD = 0.6

    if n >= WARMUP:
        d_rate = _global_d_rate(opp_history)
        if d_rate > HARD_THRESHOLD:
            return "D"
        if d_rate > SOFT_THRESHOLD:
            # Мягкое предупреждение: один D, но возвращаюсь к Pavlov
            # на следующем шаге.
            return "D"

    # 5) Классический Pavlov.
    my_last = my_history[-1]
    opp_last = opp_history[-1]
    if my_last == opp_last:
        # CC или DD: вернуть C (win-stay при CC, lose-shift при DD).
        return "C"
    # CD или DC: вернуть D (lose-shift при CD, win-stay при DC).
    return "D"
