"""Adaptive Defender — анти-Prober с Generous TFT в качестве базы.

Мотивация (по результатам T19):
  В T19 в популяцию вошёл `bot_prober` (D, C, C, затем AllD если соперник
  оказался голубем, иначе TFT). Prober сместил баланс турнира:
  - AAC слетел с #1 на #2 (warmup эксплуатируется).
  - Soft Majority стал #1 (статистически устойчив к Prober).
  - AllC, TF2T, FbF — рухнули вниз (наивные кооператоры — еда Prober).

  Гипотеза T20: можно построить **зеркало Prober** — кооперативный бот,
  который умеет распознать сигнатуру Prober и переключиться в AllD
  именно против него (а против остальных оставаться кооперативным).

Дизайн (двухслойное обнаружение, устойчивое к шуму):

  Слой 0 (раунды 1-4, n=0..3): всегда C.
    Это даёт "честную базовую линию": Prober видит честного
    кооператора, играет свой сценарий (D, C, C, затем D на 4-м ходу).
    TFT в ответ играет CCCC. AllD играет DDDD. AllC играет CCCC.

  Слой 1 — строгая сигнатура (срабатывает на чистых матчах):
      opp_history[0..3] == ['D', 'C', 'C', 'D']
      AND my_history[0..3]   == ['C', 'C', 'C', 'C']
    - opp[0]='D'   : пробный укол Prober.
    - opp[1]='C'   : "извинение".
    - opp[2]='C'   : второе "извинение".
    - opp[3]='D'   : соперник перешёл в AllD-режим (мы голубь по его мнению).
    - my_history требование исключает интерпретацию "это TFT в ответ
      на мой случайно-перевёрнутый D" (если у меня в первых 4 ходах
      был D, это был шум, и TFT с opp[0]=D — нормальная реакция).

  Слой 2 — устойчивая сигнатура (срабатывает при шумовом сбое 4-раундового
  паттерна, проверяется с раунда 9 и далее):
      - Фаза пробы: opp_history[:3] содержит хотя бы одну D И хотя бы одну C.
      - Фаза эксплуатации: в opp_history[3:8] не менее 4 из 5 ходов — D.
      - Моя сторона была чистой: my_history[:8] содержит не более одного D
        (т.е. за первые 8 ходов я в основном играл C).
    Эта проверка устойчива к одиночному шумовому биту с обеих сторон.

  Если любой из слоёв срабатывает → defender mode: AllD до конца матча.
  Если нет — продолжаем как Generous TFT (g = 1/3).

Расчёт обнаружения (под шумом p=0.02):

  Истинное срабатывание (Prober → defender):
    - Слой 1: 4 хода Prober + 4 моих = 8 ходов без шума:
      0.98^8 ≈ 0.85. Срабатывает в ~85% матчей.
    - Слой 2: даже если слой 1 не сработал, слой 2 покрывает почти все
      оставшиеся случаи (нужно много шумовых битов чтобы пройти мимо).
      Совокупное обнаружение ≈ 0.98.

  Ложное срабатывание на TFT:
    - Слой 1: нужно одновременно перевернуть TFT[0]→D и TFT[3]→D, при
      этом мои 4 хода чистые: 0.02 * 0.98^2 * 0.98^2 * 0.02 ≈ 3.5e-4 ≈ 0.04%.
    - Слой 2: TFT vs меня даёт opp=CCCCCCCC. Чтобы 4 из 5 ходов в [3:8]
      стали D, нужно перевернуть 4 хода → 0.02^4 * 0.98^4 ≈ 1.5e-7.
    - Совокупно ложное срабатывание ≈ 0.04%.

  Это дисбаланс ~25x в нашу пользу (по вероятностям). На матче из
  3 повторений ожидаемая ловля Prober — практически каждый раз.

Ожидаемые матчи в популяции T19:

  vs Prober: detector триггерится → AllD с раунда 5. Первые 4 хода:
    я (CCCC) vs Prober (DCCD) = 0+3+3+0 = 6. Раунды 5..200: DD = 196.
    Итого ~202. Prober получает ~6+5+196=207.
    Разница ~5 — почти ничья. Но я **отнял у Prober +400 очков**
    относительно его матча с AllC.

  vs AllC: opp[0:4]=CCCC → слой 1 не сработает. opp[3:8]=CCCCC → слой 2
    не сработает. Остаюсь в gTFT → ~575 очков (gTFT vs AllC).

  vs TFT: opp[0]=C → слой 1 не сработает. opp[3:8]=CCCCC → слой 2 нет.
    Остаюсь в gTFT → ~520 очков.

  vs AllD: opp[0]=D, opp[1]=D → opp[:3].count("C") < 1 → слой 1 нет
    (нужно opp[1]=C). Слой 2: фаза пробы требует C в opp[:3]; здесь
    их 0 → слой 2 нет. Остаюсь в gTFT vs AllD → ~190 (хуже TFT, но
    gTFT обычно жертвует ~20 vs AllD).

  vs Tester: Tester играет D,C,D,C,D,C → opp[:3]=DCD, opp[3:8]=CDCDC.
    Слой 1: opp[3]=C ≠ D → нет. Слой 2: opp[3:8].count("D") = 2 < 4 → нет.
    Остаюсь в gTFT → ~400 (gTFT терпит D,C паттерн через прощение).

  vs Joss: Joss начинает C, иногда D. opp[0]=C → слой 1 нет. opp[:3]
    вероятно содержит хотя бы одну C → слой 2 первая часть может
    сработать. Но opp[3:8] обычно содержит мало D (Joss ≤ 10% sneak) →
    слой 2 не сработает. → gTFT.

  vs Grim: opp[0]=C → слой 1 нет. Не сработает.

  vs AAC: opp[0]=C → слой 1 нет. opp[:6] все C → слой 2 нет. → gTFT.

  vs self: оба играют C,C,C,C → opp[3]=C → слой 1 нет. opp[3:8]=CCCCC →
    слой 2 нет. → оба gTFT → высокая взаимная кооперация ~580.

Цена: я **не эксплуатирую** наивных кооператоров (как Prober делает).
Я остаюсь "честным" gTFT для всех, кроме Prober. Это значит, я не
получу бонус от AllC (+928), но и не теряю репутацию в TFT-семье.

Это **этическая стратегия**: мстить только тем, кто пробил первым
И продолжает эксплуатировать; для всех остальных — нормальная политика.

Аналогии в реальном мире:
  - Доктрина "ядерного ответного удара": не атакуем первыми, но
    если соперник нанесёт "разведку боем" и затем продолжит — отвечаем.
  - Антимонопольное право: компании конкурируют свободно, но если
    обнаруживается паттерн "хищническое ценообразование с
    последующим выдавливанием" — санкции. Распознавание паттерна,
    а не отдельных действий.
  - Иммунный ответ: T-клетки реагируют на паттерн "антиген + контекст
    воспаления", а не на каждое присутствие антигена.

Связь с принципами Аксельрода:
  - nice ✓ (не предаю первым).
  - retaliatory ✓ (мщу за паттерн Prober).
  - forgiving ✓ (gTFT прощает с p=1/3 для всех кроме Prober).
  - non-envious ✓ (defender-режим даёт мне столько же, сколько Prober'у;
    я не пытаюсь "обогнать" его, а просто отказываю в эксплуатации).
"""

import random

_rng = random.Random()
GENEROSITY = 1.0 / 3.0


def _is_prober_signature_strict(my_history, opp_history):
    """Слой 1: точная сигнатура по первым 4 раундам."""
    if len(opp_history) < 4 or len(my_history) < 4:
        return False
    return (
        opp_history[0] == "D"
        and opp_history[1] == "C"
        and opp_history[2] == "C"
        and opp_history[3] == "D"
        and my_history[0] == "C"
        and my_history[1] == "C"
        and my_history[2] == "C"
        and my_history[3] == "C"
    )


def _is_prober_signature_robust(my_history, opp_history):
    """Слой 2: устойчивая к шуму сигнатура (проверяется с n>=8).

    Важно: моя «чистота» проверяется только в окне 0..3 (раунды 1-4),
    где я обязан играть C. После раунда 4 я могу реагировать (gTFT),
    и эти ответные D **не должны** мешать обнаружению Prober.
    """
    if len(opp_history) < 8 or len(my_history) < 8:
        return False
    # Моя сторона была чистой в forced-C окне (рауды 1-4).
    if my_history[:4].count("D") > 0:
        return False
    # Фаза пробы: в первых 3 ходах хотя бы 1 D и хотя бы 1 C.
    probe = opp_history[:3]
    if probe.count("D") < 1 or probe.count("C") < 1:
        return False
    # Фаза эксплуатации: 4 из 5 ходов в opp[3:8] — D.
    exploit = opp_history[3:8]
    if exploit.count("D") < 4:
        return False
    return True


def _in_defender_mode(my_history, opp_history):
    return (
        _is_prober_signature_strict(my_history, opp_history)
        or _is_prober_signature_robust(my_history, opp_history)
    )


def choose_move(my_history, opp_history):
    n = len(my_history)

    # Слой 0: первые 4 раунда — всегда C (сбор данных).
    if n < 4:
        return "C"

    # Слой 1+2: проверка сигнатуры Prober.
    if _in_defender_mode(my_history, opp_history):
        return "D"

    # Иначе — обычный Generous TFT.
    last_opp = opp_history[-1]
    if last_opp == "C":
        return "C"
    if _rng.random() < GENEROSITY:
        return "C"
    return "D"
