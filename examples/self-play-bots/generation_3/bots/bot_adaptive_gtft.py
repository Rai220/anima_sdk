"""Adaptive Generous Tit-for-Tat (a-gTFT).

Идея. Чистый gTFT (g=1/3) — победитель T3 в кооперативных матчах
(self/AllC/TFT ≈ 600), но систематический донор для не-реагирующих
эксплуататоров: vs AllD = 135, vs Grim = 216 (Grim, попав в D после
шумового флипа, бесконечно получает дармовое T=5 каждый раз, когда
gTFT прощает).

a-gTFT добавляет к gTFT простой детектор «соперник перестал
кооперировать» и переключает режим:

- **friendly (gTFT, g=1/3)**: когда соперник в основном играет C
  (opp_C >= 0.65) — прощение восстанавливает CC из шумовых каскадов.
- **cautious (TFT, g=0)**: «серая зона» (0.35 <= opp_C < 0.65) —
  не дарю прощение, копирую последний ход. Промежуточный режим для
  Random-подобных или для соперника, который пока не определился.
- **hostile (AllD)**: соперник почти всегда D (opp_C < 0.35) —
  закрываю «дверь прощения», играю чисто D. Это останавливает
  систематическую утечку очков AllD и пост-триггер-Grim.

Почему глобальная доля, а не окно? Урок Поколения 2 (см. LESSONS.md):
шумовой пик в окне «застревает» и сбивает классификатор; глобальная
доля забывает монотонно и устойчиво определяет «фоновое»
поведение соперника.

Warmup. Первые 8 раундов играю как обычный gTFT, чтобы дать соперникам
проявить себя и не классифицировать по 1-2 раундам. Это компромисс:
короче — больше ложных переключений; длиннее — больше потерь vs AllD.

Что ожидаю:

- vs AllD: после ~10 раундов opp_C ≈ noise = 0.02 < 0.35 → переход в
  hostile, дальше DD = 1 за раунд. Прирост над gTFT (~0.67 за раунд)
  ≈ +60 очков за матч.
- vs Grim: пока Grim в C-режиме (до шумового флипа) — играю мягко.
  После того как Grim ушёл в вечный D и его opp_C упал — перехожу в
  hostile. Это закрывает «дармовое T для Grim после шума». Сейчас
  gTFT отдаёт Grim 511; ожидаю снизить до ~280-310 (как TFT).
- vs AllC, gTFT, TFT, Pavlov: opp_C остаётся высоким (>0.85) → весь
  матч во friendly-режиме. Должен быть неотличим от чистого gTFT.
  Self-play ≈ 590.
- vs Random: opp_C ≈ 0.5 — попадаю в cautious. Это TFT-режим. По
  данным T4 TFT vs Random = 453, gTFT vs Random = 408. Ожидаю
  возврата к TFT-уровню (~450).
- vs TTFT: opp_C обычно высокий (~0.85), останусь во friendly-режиме.

Что НЕ исправляет a-gTFT:
- Случайные ложные переключения: если соперник честно кооперирует,
  но шум выбил серию D-ходов, я могу временно зайти в cautious.
  Однако дальнейшая статистика выправит классификацию (глобальная
  доля сходится к истине).
- Pre-noise эксплойт Pavlov: за первые ~10 раундов Pavlov ещё не
  «вошёл» в D-режим против AllC; я могу неправильно классифицировать
  Pavlov как кооператора в начале. Но это нормально — он и есть.

Параметры подобраны не из математической оптимизации (это бы привело
к подгонке под текущую популяцию), а по статистическому здравому
смыслу: 0.35/0.65 — естественные «трети» для opp_C.
"""

import random

_rng = random.Random()
GENEROSITY = 1.0 / 3.0
WARMUP = 8                # минимальное число ходов до классификации
FRIENDLY_THRESHOLD = 0.65 # opp_C >= 0.65 → прощаю
HOSTILE_THRESHOLD = 0.35  # opp_C < 0.35 → играю D всегда
# Между ними — cautious (чистый TFT).


def choose_move(my_history, opp_history):
    # Первый ход — кооперация (nice-стратегия по Аксельроду).
    if not opp_history:
        return "C"

    n = len(opp_history)
    opp_c = opp_history.count("C") / n
    last_opp = opp_history[-1]

    # Warmup: ведём себя как стандартный gTFT, не классифицируем.
    if n < WARMUP:
        if last_opp == "C":
            return "C"
        return "C" if _rng.random() < GENEROSITY else "D"

    # Hostile: соперник систематически предаёт → играю D без прощения.
    if opp_c < HOSTILE_THRESHOLD:
        return "D"

    # Cautious: серая зона → TFT без прощения.
    if opp_c < FRIENDLY_THRESHOLD:
        return "C" if last_opp == "C" else "D"

    # Friendly: соперник в основном кооперативен → gTFT с прощением.
    if last_opp == "C":
        return "C"
    return "C" if _rng.random() < GENEROSITY else "D"
