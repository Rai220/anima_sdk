"""ZD-Extort-2 — Press & Dyson (PNAS 2012), вымогательская ZD-стратегия.

Memory-1 стохастическая стратегия из класса Zero-Determinant, χ=2.
В отличие от ZD-GTFT-2 (которая *кооперативная* — даёт сопернику
шанс максимизировать общий выигрыш), Extort-2 — *вымогательская*:
она навязывает линейное соотношение

    S_me - P = χ * (S_opp - P)

где χ=2 и P=1 (плата при взаимном D). Это означает: соперник никогда
не может опередить меня; лучшее, что он может сделать — играть
полностью кооперативно и принять асимметрию выплат (~30/15 за 10
раундов в пределе vs мой ~30/30 vs honest TFT).

## Каноническая формула (Press-Dyson)

Параметры (p1, p2, p3, p4) = P(C | prev outcome), где
p1 после CC, p2 после CD (я С, опп D), p3 после DC, p4 после DD.

Для нашей матрицы (T=5, R=3, P=1, S=0), χ=2 и максимально-кооперативной
формы (phi = 1/9, граница допустимости):

    p1 = 1 - phi*(χ-1)*(R-P)            = 1 - 2/9 = 7/9 ≈ 0.778
    p2 = 1 - phi*(χ*(T-P) + (P-S))       = 1 - 9/9 = 0
    p3 =     phi*((T-P) + χ*(P-S))       = 6/9 = 2/3 ≈ 0.667
    p4 =     0

p2=0 — это **жёсткое наказание**: если я кооперировал, а соперник
предал — я никогда автоматически не возвращаюсь в C (только если
получу опять (D,C), p3=2/3).

## Ожидаемое поведение

- **vs AllC** (наивный): эксплуатация. AllC всегда играет C, я
  чередую CC (p1=7/9) и DC (p3=2/3), часто долбя D на C. Ожидаю
  ~3.5-4.0 за раунд (CC=3, DC=5).
- **vs AllD**: моментально в DD-абсорбер (p4=0). Получаю P=1 за
  раунд. ~200 за матч.
- **vs TFT/gTFT**: каскад взаимных наказаний. TFT отвечает на мои
  редкие D ответным D, я переключаюсь на стабильное DD (p4=0).
  Ожидаю плохо: ~150-200 за матч (хуже, чем оба сразу AllC).
- **vs Pavlov**: интересный кейс. Pavlov меняет ход после S/P, и
  моя CD-провокация (с p_CD=0) выводит обоих в DD-режим.
- **vs ZD-GTFT-2**: я доминирую (по матем. гарантии: S_me ≥ S_opp+1).
- **self-play**: оба ZD-extort vs друг друга. Из CC обе стороны
  иногда переключаются на D (с вер. 2/9). Попадание в CD/DC → быстрый
  спуск в DD-абсорбер. Ожидаю **очень низкий** self-score ~150-250.

## Почему «доит оптимизаторов»

Стратегии, которые «адаптивно подстраиваются» под партнёра (Pavlov,
адаптивные защитники, soft-Majority), думают что могут улучшить свой
счёт, постепенно кооперируя. ZD-extort заставляет их выбор быть
строго хуже: чем больше они кооперируют, тем больше я D-урываю с
гарантией линейности. Это «контрольная» стратегия: показывает, как
устроены хищники, не отбирая у них победу (extort слаб в популяции,
потому что плохо играет сам с собой и с зеркалами).

## Параллель с реальностью

ZD-extort моделирует **монопольного вымогателя**, который имеет
структурную власть (банк против вкладчика, монополист против
покупателя, империя против колонии). Жертва может «протестовать»
(играть D), но это только ухудшит её собственное положение
относительно P; «договариваться» (играть C) — это значит принять
χ=2 асимметрию: «я получу вдвое больше, чем ты».

Эволюционная неустойчивость extort: в среде, где много extort-ботов,
все играют друг с другом ~P → быстро вытесняются стабильными
кооператорами (Аксельрод, 1980). Но против наивных кооператоров
extort снимает огромную прибыль. Это микро-модель того, почему
«рыночный хищник» эффективен в краткосроке и нестабилен в долгосроке.

## Стохастичность

Свой rng, не зависит от внешнего seed. Не делает копий стейта.
"""

import random

_rng = random.Random()

# Press-Dyson ZD-Extort-2 для T=5, R=3, P=1, S=0, χ=2, phi=1/9.
P_CC = 7.0 / 9.0
P_CD = 0.0  # я C, опп D — никогда сам не возвращаюсь в C
P_DC = 2.0 / 3.0
P_DD = 0.0  # абсорбер


def choose_move(my_history, opp_history):
    # Первый ход — кооперация (nice старт даёт максимум против AllC).
    if not my_history:
        return "C"
    my_last = my_history[-1]
    opp_last = opp_history[-1]
    if my_last == "C" and opp_last == "C":
        p = P_CC
    elif my_last == "C" and opp_last == "D":
        p = P_CD
    elif my_last == "D" and opp_last == "C":
        p = P_DC
    else:  # DD
        p = P_DD
    return "C" if _rng.random() < p else "D"
