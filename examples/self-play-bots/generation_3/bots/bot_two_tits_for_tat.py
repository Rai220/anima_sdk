"""Two Tits for Tat — жёсткий ретализатор.

Гипотеза (классика Аксельрода): на одиночный D отвечать ДВУМЯ D, чтобы
'дороже' стоило соперника предательство. Это противоположный полюс
от TF2T (который прощает один D).

Логика:
- Первый ход: C.
- Если в последних ДВУХ ходах соперника был хотя бы один D — играю D.
- Иначе C.

То есть после каждого D соперника я отвечаю D на текущем и следующем ходу.

Ожидание:
- Хорошо против AllC (стабильный CC).
- Хорошо против AllD (быстро уходит в DD).
- Плохо в шуме: один шумовой D соперника → 2 моих D → ещё больше каскад.
- Уязвимо в self-play (шумовой каскад длиннее, чем у TFT).
"""


def choose_move(my_history, opp_history):
    if not opp_history:
        return "C"
    # Смотрю последние 2 хода соперника. Если хотя бы один D — мщу.
    recent = opp_history[-2:]
    if "D" in recent:
        return "D"
    return "C"
