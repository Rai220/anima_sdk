"""Reverse Pavlov / Win-Shift-Lose-Stay (WSLS наоборот).

Идея — структурное «зеркало» классического Pavlov.

Pavlov (WSLS):
    win  → stay (повторяю свой ход)
    lose → shift (меняю свой ход)

Reverse Pavlov (Win-Shift-Lose-Stay):
    win  → shift (меняю свой ход)
    lose → stay  (повторяю свой ход)

Правило по матрице (T=5, R=3, P=1, S=0):
    (C, C) → R=3 → win  → shift → следующий ход D
    (D, C) → T=5 → win  → shift → следующий ход C
    (C, D) → S=0 → lose → stay  → следующий ход C
    (D, D) → P=1 → lose → stay  → следующий ход D

Эквивалентная упрощённая формулировка:
    «играй D после согласия (CC); играй C после рассогласования; в DD оставайся в D».

Почему это **должно быть слабо** и почему вообще стоит проверить:

1. **Разрушает любую CC-серию.** После любого CC я играю D на
   следующем ходе. Это уничтожает кооперацию с TFT, AllC, Soft,
   gTFT, TF2T, Omega — всеми, кто согласен сидеть в CC.
2. **Залипает в DD.** После DD я снова играю D; соперник-каратель
   (Grim, Hard) тоже остаётся в D. Это «эксплуатируемая яма»: я
   сам себя в неё помещаю.
3. **Может случайно «спастись» из DD против Pavlov-семьи.** Pavlov
   после DD играет C; я после DD играю D; на следующем ходу мой
   ход (DD-stay D, я опять D, Pavlov шифтит из P=1 в C). Так что я
   обкатываю Pavlov, но цена — серия 1-1 пока не появится шумовой
   флип.
4. **Self-play катастрофичен.** Из CC мы оба идём в D → DD → DD
   навсегда (mod шум). Из любого начального состояния мы быстро
   валимся в DD и сидим. Поэтому self-play даст ~ 1.0 за раунд.

Зачем добавлять заведомо слабого бота в популяцию:

- **Контрольный эксперимент: симметрия Pavlov.** Если Pavlov #11-12,
  а Reverse Pavlov #16-17, это эмпирически подтверждает, что
  «правильная» сторона win-stay-lose-shift — именно та, что в
  оригинале. Полезно для понимания, какая часть «магии» Pavlov
  закодирована в самом правиле, а не в случайности.
- **Не должен сломать топ-3.** Reverse Pavlov не эксплуатирует
  никого специфически (он против всех плох), поэтому добавление
  такого бота не перераспределит баллы между лидерами. Это
  идеальный «стабилизатор»: даёт всем верхним стратегиям дешёвых
  очков и проседает сам.
- **Структурный антипод для STRATEGIES.md.** В отчёте полезно иметь
  пример «как точно НЕ надо». Reverse Pavlov — кристально чистый
  такой пример.

Что ожидаю от рейтинга:

- **#14-#17.** Между Grim (≈360) и AllD (≈330). Скорее всего хуже
  Grim, потому что Grim хотя бы в self-play даёт 600 в CC; Reverse
  Pavlov в self-play даёт ~200 в DD.
- vs AllC: ход 1 = C; AllC играет C → CC → win → shift → R2 = D;
  AllC C → DC → win → shift → R3 = C. Получаем CD- DC- CD- DC ...
  на 2 хода — мой счёт T + S = 5 в среднем за 2 хода = 2.5/раунд.
  Сравнимо с AllC vs Pav (~280).
- vs AllD: R1 = C; AllD D → CD → lose → stay → R2 = C; AllD D → CD
  → lose → stay → ... → AllC-режим против AllD = 0/раунд. Это
  катастрофа. Получу 0-1 за раунд, ~200.
- vs Pavlov: R1 = CC → я D, Pav win-stay C → R2 = DC; я shift → C,
  Pav P-shift из CC win-stay... подождите, Pav после CC stay = C,
  после DC stay = D. R2 (DC) → я shift → C, Pav stay → D. R3 (CD).
  Я: stay → C. Pav: shift → C. R4 (CC). Я: shift → D. Pav: stay
  → C. Период 3, я набираю 3+0+0+5 / 4 = 2.0 за раунд. Слабо.
- vs Grim: R1 = CC → я D. Grim видит D → Grim D навсегда. Дальше
  CDDDDD... → у меня поток S=0 после моих C. Через 2 хода я опять
  C (stay из CD-lose), потом D (shift из CC-win... но Grim в D, так
  что у нас CD-lose-stay-C). В лоб: после CD lose-stay = C, и я
  играю C против вечного D Grim'а → CD ad infinitum → 0/раунд. → ~200.
- vs TFT: R1 = CC → я D, TFT копирует свою C → R2 = DC → я shift C,
  TFT копирует D → R3 = CD → я stay C, TFT копирует C → R4 = CC.
  Период 3: 3+0+0 = 3 / 3 = 1.0/раунд для меня. → ~200.
- vs self: R1 = CC оба → R2 = DD оба → DD навсегда → 1.0/раунд → ~200.

Итого ожидаемая средняя за раунд: ≈ 1.0-1.5, итоговый счёт ≈
200-300. Это место #15-#17 в нашей популяции.

Главное правило: возвращаемся к стандартному API — C или D, без
случайности, без шума. Решение чисто детерминировано на основе
последней пары (my_last, opp_last). Первый ход — кооперация
(чтобы у нас был хотя бы шанс на CC-серию, прежде чем мы её сами
сорвём).
"""


def choose_move(my_history, opp_history):
    # Первый ход — C. Дальше получим CC и сразу сорвёмся в D, что и
    # является сутью Reverse Pavlov. Старт с D ничего бы не дал —
    # просто сместил бы цикл на полтона раньше.
    if not my_history:
        return "C"
    my_last = my_history[-1]
    opp_last = opp_history[-1]
    # win (CC=R или DC=T) → shift.
    # lose (CD=S или DD=P) → stay.
    if my_last == opp_last:
        # CC = win → shift → играю не_C = D.
        # DD = lose → stay → играю D.
        # В обоих случаях возвращаю D.
        return "D"
    else:
        # DC = win → shift → играю не_D = C.
        # CD = lose → stay → играю C.
        # В обоих случаях возвращаю C.
        return "C"
