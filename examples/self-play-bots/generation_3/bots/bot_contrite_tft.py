"""Contrite Tit for Tat (cTFT) — TFT, который «извиняется» за свой
шумовой D и не наказывает соперника, если его D — справедливый
ответ на мой шумовой D.

Идея (Boyd 1989, Wu & Axelrod 1995): чистый TFT в шумной среде
запирает себя в DD-каскаде после первого же шумового флипа. gTFT
лечит это случайным прощением, но платит за это эксплуатацией
(g=1/3 ходов уходят AllD/Grim как дармовые T=5). Contrite TFT
действует точечно: он отслеживает, был ли его собственный последний
ход *намеренным* TFT-ходом или результатом шума, и если ход был
шумовым — играет C на следующем ходу, признавая «ошибку», даже
если соперник его справедливо наказал.

API не даёт прямого доступа к «намеренному» ходу до шума, поэтому
бот **реконструирует** свой намеренный ход через симуляцию
собственной state-machine с самого начала матча. На каждом раунде
проходим всю историю от 0 до t-1, реконструируя intended[k] для
каждого k:

    intended[0] = "C"
    intended[k] = "C"               если actual[k-1] != intended[k-1]
                                     (я был шумово флипнут → contrite)
    intended[k] = opp_history[k-1]  иначе (стандартный TFT)

Затем проверяем условие contrite на текущем раунде t.

Это O(n) на ход, n=200 → норм.

Что ожидаю:
- vs self: восстанавливается из шумовых каскадов почти идеально
  (детерминированно), потому что оба бота извиняются за свои
  шумовые D, в отличие от gTFT, где прощение случайно.
- vs TFT: cTFT извиняется за свой шумовой D — TFT этого не понимает,
  но получает бонусный C от меня → длинный CC после одного флипа.
- vs AllD: после моего шумового C → contrite режим запускает
  правильную логику: следующий ход я C (один раз), потом возвращаюсь
  в TFT и играю D навсегда. Должен быть как TFT (~211 за матч).
- vs AllC: я не бью первым → стабильное CC, иногда шумовой D
  → следующий ход извиняюсь → CC опять. ≈ 600 за матч.
- vs Grim: после шумового D Grim уходит в вечный D навсегда. Моя
  contrite-логика играет C один раз, но Grim уже триггернут →
  далее я тоже в TFT-режиме D. Не сильно лучше TFT.
- vs gTFT: похоже на vs TFT, но gTFT тоже прощает шум. Ожидаю
  устойчивое CC.

Главная гипотеза T6:
  cTFT улучшает self-play относительно TFT (≈600 vs 472), не платя
  цену gTFT за прощение (vs AllD не теряет). Это «дешёвая страховка
  от шума» без раздачи дармовых T хищникам.
"""


def _reconstruct_intended(my_history, opp_history):
    """Восстанавливаем последовательность намеренных ходов от 0 до t-1.

    Возвращает список длины len(my_history).
    """
    n = len(my_history)
    intended = [""] * n
    for k in range(n):
        if k == 0:
            intended[k] = "C"
        else:
            if my_history[k - 1] != intended[k - 1]:
                # Шумовой флип на предыдущем раунде → я в contrite
                # на раунде k → играю C.
                intended[k] = "C"
            else:
                # Стандартный TFT: копирую последний ход соперника.
                intended[k] = opp_history[k - 1]
    return intended


def choose_move(my_history, opp_history):
    if not my_history:
        return "C"

    intended = _reconstruct_intended(my_history, opp_history)
    last_intended = intended[-1]
    last_actual = my_history[-1]

    if last_actual != last_intended:
        # Мой последний ход был шумовым флипом → contrite → играю C.
        return "C"

    # Иначе — стандартный TFT.
    return opp_history[-1]
