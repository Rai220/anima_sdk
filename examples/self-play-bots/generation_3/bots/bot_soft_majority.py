"""Soft Majority — статистическая стратегия большинства.

Классика IPD из эпохи Аксельрода. Простейшее правило:

- Первый ход: C (не на чем строить статистику; nice по Аксельроду).
- Дальше: считаю в `opp_history` количество C и D.
  Если C-ходов соперника >= D-ходов соперника — играю C.
  Иначе — играю D.

То есть «голосование большинством» по всей наблюдаемой истории.
В отличие от a-gTFT с тремя порогами (0.35 / 0.65), Soft Majority
имеет один порог = 0.5 и НЕ использует случайность. Полностью
детерминированная стратегия с глобальной памятью.

## Чем отличается от уже существующих ботов

- **vs TFT.** TFT смотрит только на последний ход; Soft Majority
  усредняет по всей истории. Один шумовой D у TFT мгновенно меняет
  ответ; у Soft Majority — почти не двигает решение (1 D из N ходов).
- **vs gTFT.** gTFT прощает стохастически, мне не нужна случайность —
  «прощение» получается само из того, что один D не превышает
  накопленных C.
- **vs a-gTFT.** a-gTFT тоже использует глобальную долю (учил из
  Поколения 2), но переключает три режима. Soft Majority — один
  бинарный переключатель.
- **vs cTFT / TF2T.** cTFT проигрывает «амнистию» через анализ
  собственной истории; TF2T — через окно 2. Soft Majority вообще
  не нуждается в восстановлении: он просто игнорирует одиночные D
  как статистически незначимые.

## Гипотезы для T8 (по матрице T7)

1. **vs AllD ≈ 199.** Первый ход C → теряю 5. После этого opp_D=1 >
   opp_C=0 → играю D навсегда. Все следующие 199 раундов = DD = 1.
   Итог ≈ 1×199 + 0 = 199. То же, что у TFT (208). Не хуже.

2. **vs AllC = 600.** Никогда не предаю первым; opp_C всегда растёт →
   условие C ≥ D соблюдено всегда. CC по всему матчу.

3. **Self-play ≈ 590-600.** Шум 2% даёт ~4 D у каждого за 200 раундов.
   У соперника-Soft-Majority count(C) >> count(D) всегда → он играет
   C → шумовые флипы не запускают каскадов. Лучше TFT(self)=444 и
   сопоставимо с gTFT(self)=593, TF2T(self)=596.

4. **vs Random ≈ 250-350.** Random имеет ~50% C, ~50% D. На первой
   половине матча отношение часто переваливает в сторону D → я
   переключаюсь в D. Но Random может «откатиться» в серию C, и я
   буду переключаться обратно. Это создаёт колебания. Ожидаю:
   хуже TFT vs Random (441), потому что я медленно «остываю»
   из режима D после серии D соперника. Но **лучше TF2T vs Random
   (374)** — TF2T давал 624 Random'у, Soft Majority даст меньше,
   потому что переключение на D происходит сразу при превышении.

5. **vs Grim.** Пока Grim в C-режиме, я в C. Шумовой флип →
   Grim в D навсегда. После ~5-10 раундов opp_D > opp_C →
   я в D. Похоже на TFT (229), но с задержкой переключения,
   что добавит мне 5-10 проигранных раундов в начале каскада.
   Ожидаю ~210-220.

6. **vs gTFT, cTFT, a-gTFT, TF2T.** Эти прощают шумовые D. У них
   opp_C почти всегда = 1 → я в C. Двусторонняя кооперация близка
   к оптимальной: ожидаю 580-600 во всех парах.

7. **vs TTFT.** TTFT — единственный, кто отвечает 2 D на 1. Один
   мой шумовой D → 2 D от TTFT → opp_D=2, opp_C=N-2 → пока соблюдено
   N-2 >= 2 (т.е. N >= 4), я в C. Так что я НЕ амплифицирую каскад.
   Ожидаю 450-500, лучше TFT vs TTFT (237).

8. **vs Pavlov.** Pavlov шифтит: после CD получает S=0, переключается;
   после DC получает T=5, остаётся D. Если шумовой флип у меня,
   Pavlov застревает в D. У Pavlov opp_C начинает падать, но за 200
   раундов вряд ли превысит opp_D. Ожидаю поведение между TFT (454)
   и a-gTFT (560).

## Главное отличие от a-gTFT

a-gTFT в hostile режиме (opp_C < 0.35) играет чистый D без шанса
вернуться, что превращает его в Grim-после-классификации. Soft
Majority **постоянно пересчитывает** статистику: если соперник
неожиданно начнёт кооперировать после серии D, я тоже вернусь в C
как только opp_C сравняется с opp_D. Это делает Soft Majority более
«обратимым» классификатором — но и более медленным.

## Главная теоретическая уязвимость

Стратегия с **тонкой настройкой** ритма D-серий: соперник, который
поддерживает ровно opp_C ≥ opp_D через изолированные D, получает все
T-очки в эти ходы (я ему C). Например, паттерн «3 C, 1 D, 3 C, 1 D, …»
даёт сопернику средний ход 0.75 C → я в C → каждый его D = T=5 для него,
S=0 для меня. В нашей популяции такого «алгоритмического эксплойта» нет
(ни один бот не тонет в наблюдаемое 75/25), но Pavlov через шум может
случайно реализовать паттерн.

## Параллель с реальным миром

Soft Majority — **«репутационный» механизм**. Ты решаешь, доверять
человеку или нет, по общему балансу его поступков, не по последнему.
Аналогии: кредитный рейтинг (history-aggregated), школьная оценка
по итогам семестра, отзывы на маркетплейсах. Сильное место —
устойчивость к одному плохому дню. Слабое место — слишком долго
«забывает» серьёзный проступок (если у соперника очень длинная
позитивная история, одна большая измена не сразу опускает счётчик
ниже 50%).
"""


def choose_move(my_history, opp_history):
    if not opp_history:
        return "C"
    c_count = opp_history.count("C")
    d_count = len(opp_history) - c_count
    # «Мягкое» большинство: ничья (>= вместо >) разрешается в пользу C.
    if c_count >= d_count:
        return "C"
    return "D"
