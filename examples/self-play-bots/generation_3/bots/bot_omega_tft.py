"""Omega TFT (Slany & Kienreich 2007) — TFT с двумя детекторами.

Идея: «классический» TFT в основе, но с двумя поверхностными
счётчиками, которые ловят ровно две патологии шумной IPD:

1. **Deadlock** — обмен альтернированными D/C/D/C, когда TFT и
   соперник застряли в эхо-каскаде после одиночного шумового D.
   Если соперник чередует ходы достаточно долго, мы играем `C`,
   чтобы разорвать каскад (то же, что делает gTFT через
   случайность, но детерминированно).

2. **Randomness** — соперник предаёт без провокации (когда мы
   только что играли `C`). Один-два таких эпизода — шум. Восемь
   подряд — значит соперник не реагирует на нашу кооперацию,
   и продолжать играть TFT убыточно. Тогда мы переключаемся в
   режим **постоянного D** до конца матча.

Эти два механизма решают две слабости TFT, которые в нашем пантеоне
видны на каждом турнире:

- TFT vs TFT (444 в self-play в T1, 487 в T9 после улучшений) —
  любая шумовая ошибка запускает DC/CD-каскад. Deadlock-детектор
  сразу гасит каскад без случайности.
- TFT vs Random (≈441-465) — Random выдаёт 50% D, TFT механически
  отвечает и теряет очки на бесконечном «эхо». Randomness-детектор
  через 8 необъяснимых D переключается в постоянный D, что
  оптимально против шума 50/50: ожидание D vs Random = 3 за раунд,
  что выше TFT-смеси.

## Алгоритм (Slany & Kienreich 2007)

Параметры:
- `RANDOMNESS_THRESHOLD = 8` — порог переключения в ALL-D.
- `DEADLOCK_THRESHOLD = 3` — порог разрыва каскада через C.

Бот stateless, восстанавливаем счётчики из истории. Для каждой пары
`(my_history[t], opp_history[t])`:

- **randomness counter**: если я играл `C` и соперник играл `D` —
  это «необъяснимый» D, +1. Если я играл `C` и соперник играл `C` —
  forgiveness, −1 (с нижней границей 0). Если я играл `D` — не
  трогаем (его ответ был «оправдан» моим предательством).
- **deadlock counter**: если ход соперника отличается от его
  предыдущего хода — +1 (он чередует). Если совпадает — счётчик
  сбрасывается в 0.

После прохода:
- Если когда-либо `randomness >= 8` — играем `D` до конца матча.
- Если `deadlock >= 3` — играем `C` (разрываем каскад).
- Иначе — TFT (`opp_history[-1]`).

## Чем отличается от уже существующих ботов

- **vs TFT.** Omega начинает с TFT, но добавляет два механизма
  защиты. TFT механически копирует, Omega — *классифицирует*.

- **vs gTFT.** gTFT прощает стохастически с фиксированной
  вероятностью ~0.1. Omega прощает **детерминированно**,
  когда видит ровно паттерн «чередующийся каскад». В шумном
  матче gTFT иногда прощает зря (когда соперник действительно
  предаёт), Omega — точно по паттерну.

- **vs cTFT.** cTFT прощает СВОЙ шум — анализирует свою
  историю. Omega ловит каскад «по факту чередования», независимо
  от того, чей это был шум. Это симметрично, но менее точно
  (cTFT извиняется первым, Omega ждёт, пока обе стороны
  «застрянут»).

- **vs TF2T.** TF2T прощает любой одиночный D соперника
  (буквально окно 2). Omega наказывает каждый D (TFT-копия), но
  не входит в каскад через deadlock-детектор. В матче с
  агрессивным D-предателем TF2T получает 0 за каждый D, Omega
  отвечает D и не теряет очки.

- **vs Soft Majority.** Soft Majority — статистический
  усреднитель. Omega — детектор паттернов. Против AllD они
  должны быть похожи (Soft быстро переключится в D, Omega
  через 8 ходов). Против Random — Soft Majority колеблется
  между C и D в зависимости от текущего отношения, Omega
  через 8 необъяснимых D **навсегда** переключается в D.

- **vs Gradual.** Gradual эскалирует месть. Omega — простая
  TFT-месть, но с защитой от двух специфических паттернов.

## Гипотезы для T10 (по матрице T9)

1. **vs AllC = 600.** Я не предаю первым; opp_C растёт, opp_D=0.
   Никаких счётчиков не триггерим. CC по всему матчу.

2. **vs AllD ≈ 198-210.** Первые ~8 раундов C → D (TFT). Получаю
   8 × S=0 + 0 × T. На 8-м раунде randomness=8 → переключаюсь в
   ALL D навсегда. Дальше DD = 192 × 1 = 192. Итог ≈ 192-200,
   возможно даже чуть хуже TFT (212), потому что TFT после 1-го
   шумового D переключается на D раньше.

   Стоп, надо подумать. TFT начинает с C → опп D → TFT отвечает
   D → опп D → TFT отвечает D → … TFT даёт AllD только 1 × T=5
   на первый ход. У меня: я играю C, опп D (рандом=1), я играю D
   (TFT), опп D, я играю D, опп D, я играю D, опп D, я играю D
   (рандом=1, не растёт, потому что я играл D). … стоп, в моих
   формулах randomness растёт только когда МОЙ ход был C. После
   1-го раунда я играю D (TFT), и больше randomness не растёт.
   Так что мой randomness-счётчик НИКОГДА не дойдёт до 8 против
   AllD — потому что я перехожу на D сразу после первого D
   соперника.

   Тогда vs AllD = ровно как TFT: 1 раунд CD + 199 раундов DD = 199.
   Без шума. С шумом 2%: ~4 шумовых раунда, что добавит мне ~4 × 0
   или ~4 × 5. Ожидаю ≈ 200-215.

3. **vs Random.** Random выдаёт ~50% D, ~50% C. Половина раундов
   у меня после C-хода (если я в TFT и моё предыдущее = опп C),
   половина после D. На C-ходах randomness растёт при ~50% D опп.
   В среднем randomness растёт со скоростью ~0.25 за раунд (когда
   я C и опп D). За 32 раунда добираю до 8. Дальше — ALL D.
   Очень неточно: на самом деле я играю TFT, моё C / D зависит
   от опп. Грубо: за 200 раундов я наберу 8 в randomness где-то
   к 20-40-му раунду, и оставшиеся 160-180 раундов играю D.
   Против Random в D-режиме: ожидание = 0.5×5 + 0.5×1 = 3 за раунд.
   160 × 3 = 480. Плюс первые 30 раундов TFT vs Random ≈ 30 × 2 = 60.
   Итог ≈ 540. Это лучше TFT (465) и Soft Majority (523), но хуже
   Gradual (556).

4. **Self-play.** Два Omega играют TFT. Один шумовой D у соперника
   → я отвечаю D → опп D (теперь моя история показывает мой D,
   опп D — randomness не растёт, потому что мой ход был D). Опп
   видит мой D, отвечает D (TFT). Получаем DC/CD каскад или DD.
   Deadlock-детектор: после 3-х чередований опп ходов оба
   переключаются в C → синхронно возвращаются в CC. Это **должно**
   быть лучше TFT self-play (487) — детерминированный gTFT.
   Ожидаю **560-590**.

5. **vs TFT.** TFT не имеет detector’а deadlock. Я играю TFT, опп TFT.
   Шумовой D у меня → опп D (TFT) → я D (TFT) → опп D → … каскад.
   Через 3 чередования я играю C (deadlock fires). Опп TFT видит мой
   C и отвечает D (продолжает каскад). Я снова в TFT (последний опп
   = D, играю D). DCDC… Я повторно триггерю deadlock каждые 3
   чередования. На длинном горизонте моя средняя выплата ≈ (T + S)/2 = 2.5
   на шаге чередования. TFT-самосогласованный self-play в шуме ≈ 487.
   Я ожидаю ≈ 470-510 vs TFT — может быть, чуть хуже TFT self из-за
   моего «преждевременного» C, которое TFT эксплуатирует.

6. **vs Grim.** Шумовой D у меня → Grim в D навсегда → я отвечаю D
   (TFT) → DD навсегда. Никаких deadlock-альтернаций → счётчик 0.
   Никаких неоправданных D со стороны Grim (когда я C, он C, потом
   я D шумовой, он D — но это после моего D, не считается). Ровно
   как TFT vs Grim ≈ 277.

7. **vs Pavlov.** Pavlov шифтит. Шумовой D у Pavlov → я D (TFT) →
   Pavlov получил P=1, остаётся D? Нет, Pavlov shift: если получил
   T или R, повторяет; если S или P, шифтит. CD: я C, опп D, я
   получаю S=0 → следующий ход у меня D (но я не Pavlov, я TFT-Omega).
   У Pavlov: его последний ход D, получил T=5, повторяет D. DD: оба
   получают P=1, оба шифтят → CC. Возвращаемся к кооперации.
   Каскад между Pavlov и TFT короче, чем TFT-self. Ожидаю 440-500.

8. **vs Soft Majority.** Соответствует профилю Soft vs TFT (585 в T9
   в одну сторону). Тут симметрично, ожидаю 570-590.

9. **vs Gradual.** Gradual эскалирует. После 1-го шумового D у меня
   Gradual в цикле наказания. Я в TFT, отвечаю D. Затем Gradual
   2 calm-C → я C (TFT). Затем снова какой-то D → новый цикл.
   У меня randomness может вырасти от Gradual D в фазе punishment
   (если в этот момент я играл C). Но Gradual атакует только после
   моих D, не «безповодно». Ожидаю 280-320.

10. **vs TF2T.** TF2T прощает одиночный D. Шумовой D у меня → TF2T
    остаётся в C → я (TFT) играю C. Шумовой D у TF2T → у меня
    randomness +1, я отвечаю D → TF2T прощает (1 D в окне), играет
    C → я (TFT) C. Каскадов почти нет. Ожидаю 570-590.

11. **vs TTFT.** Один мой шумовой D → 2 D от TTFT. Я отвечаю D на
    последнее D. Цикл D-D-D-D… deadlock-детектор не сработает
    (опп не чередует, опп держит D). Я выйду из цикла только когда
    у TTFT истечёт «бюджет» D (2 ходa на каждый мой D). Сложный
    каскад. Ожидаю 320-400.

## Чем интересен Omega TFT в нашей популяции

Это **детерминированный** ответ на шум. В пантеоне уже есть:
- gTFT — стохастическое прощение,
- cTFT — прощение через анализ собственной истории,
- TF2T — прощение через окно,
- Soft Majority — глобальная статистика.

Omega добавляет **новый механизм**: детерминированное переключение
в hostile-режим (ALL D), которое отрицает «обратимость», но точно
наказывает истинных хищников. Это близко к a-gTFT, но a-gTFT
использует ПОРОГ ДОЛИ (opp_C < 0.35) с шансом обратимости (если
доля поднимется), а Omega — НЕОБРАТИМЫЙ переключатель.

## Главное ожидаемое преимущество

vs Random — **необратимый ALL-D после 8 необъяснимых D**. Это
максимизирует выплату против чистого шума (D vs 50/50 = 3 за раунд),
без «качаний» Soft Majority. Если эта гипотеза верна, Omega может
обогнать Soft Majority в общем зачёте, потому что vs Random — это
большой пай матча в круговом турнире.

## Главная ожидаемая слабость

vs TTFT — каскад с двойной местью, который Omega не умеет
распознавать (deadlock-детектор ловит чередование, не двойные
залпы). Также vs Gradual — длинные циклы punishment, против
которых TFT-копия не имеет защиты.

## Параллель с реальным миром

Omega TFT — это **«дайте три шанса, потом полный разрыв»**.
В отличие от Grim (один шанс) и Soft Majority (бесконечная
обратимость), Omega моделирует **отношения с «терпением» 8 эпизодов**:

- Дипломатия: после серии провокаций (Berlin Crisis, Cuban Missile)
  страны переключаются в режим «холодной войны» — необратимое
  взаимное недоверие. Возврат возможен, но только через смену
  правителей (как Горбачёв-Рейган в 1985), не через одиночное
  «прощение».

- Личные отношения: после ~8 безответственных эпизодов от партнёра
  («он(а) меня снова кинул(а)») многие переключаются в режим
  «бывший» — необратимая защита. Это и есть Omega-классификация.

- Бизнес-партнёрство: после серии нарушений контракта компания
  расторгает отношения и **больше не работает** с этим контрагентом
  даже на лучших условиях. Это рациональный механизм против
  «хронических нарушителей».

- Дедлок: семейные ссоры по типу «ты сделал — я сделал в ответ —
  ты сделал в ответ — …». Один из партнёров должен «сломать
  каскад» намеренной кооперацией (deadlock-fire C). Это известный
  совет психологов: «выйди из эскалации первым».
"""

RANDOMNESS_THRESHOLD = 8
DEADLOCK_THRESHOLD = 3


def choose_move(my_history, opp_history):
    if not opp_history:
        return "C"

    n = len(opp_history)

    # Randomness counter: how many times opp defected when I cooperated,
    # adjusted by forgiveness (CC reduces, with floor 0). Once it hits
    # RANDOMNESS_THRESHOLD it triggers PERMANENT defection — even if it
    # later drops back below threshold due to forgiveness updates we
    # don't process beyond the trigger point.
    randomness = 0
    permanent_d = False
    for t in range(n):
        my_mv = my_history[t]
        op_mv = opp_history[t]
        if my_mv == "C" and op_mv == "D":
            randomness += 1
        elif my_mv == "C" and op_mv == "C":
            if randomness > 0:
                randomness -= 1
        # If my_mv == "D": randomness untouched (op's response was provoked).
        if randomness >= RANDOMNESS_THRESHOLD:
            permanent_d = True
            break

    if permanent_d:
        return "D"

    # Deadlock counter: consecutive alternations in opp_history.
    deadlock = 0
    for t in range(1, n):
        if opp_history[t] != opp_history[t - 1]:
            deadlock += 1
        else:
            deadlock = 0

    if deadlock >= DEADLOCK_THRESHOLD:
        # Break the alternating echo cascade with a cooperative move.
        return "C"

    # Default: TFT — copy opp's last move.
    return opp_history[-1]
