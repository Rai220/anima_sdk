"""Tester — провокатор, прощупывающий соперника.

Классическая «обманщицкая» стратегия (вариант Joss/Mayfield):
- Раунд 1: D. Это «тест»: посмотрим, накажет ли соперник.
- Раунд 2: C. Даём сопернику шанс отомстить (или отдыхаем после D).
- Раунд 3+:
  - Если соперник в первых двух раундах хоть раз сыграл D
    (т.е. «ретейлиатор»: возможно TFT, Grim, и т.п.) → переключаемся
    в обычную TFT-семантику до конца матча. Это «извинение и
    нормальное поведение».
  - Иначе (соперник — «голубь», прощает первый D) → продолжаем
    эксплуатировать его, чередуя D/C/D/C... В среднем 2.5 очка за
    раунд для меня vs 1.5 для голубя — лучше чем CC (3/3) только в
    одну сторону, но устойчиво для меня.

Идея: если соперник прощает первый D, его можно «доить» бесконечно.
Если соперник наказывает первый D — я не упёрся в DD, а сразу
извинился и перешёл в TFT (как Contrite TFT, но без contrite-флага).

Шумоустойчивость: «D в первых двух раундах» — простой и грубый
маркер. С шумом 2% AllC случайно сыграет D с вероятностью ~4% в
первых 2 раундах, что приведёт к ложному срабатыванию «retaliator».
Это не страшно: ложное срабатывание = я перехожу в TFT, кооперация.
Хуже было бы наоборот (упустить настоящего retaliator'а), но это
почти исключено: TFT после моего D=R1 ответит D=R2, и я это увижу.
"""


def choose_move(my_history, opp_history):
    n = len(my_history)
    # Раунд 1: всегда D (тест).
    if n == 0:
        return "D"
    # Раунд 2: всегда C (даём шанс).
    if n == 1:
        return "C"
    # Раунд 3+: решаем режим по первым двум ходам соперника.
    # Если соперник в первых двух раундах хоть раз играл D → retaliator,
    # переходим в TFT (копируем последний ход соперника).
    if "D" in opp_history[:2]:
        return opp_history[-1]
    # Иначе — «голубь», чередуем D/C/D/C... начиная с D на третьем ходу.
    # n=2 → D, n=3 → C, n=4 → D, ...
    return "D" if n % 2 == 0 else "C"
