"""Prober — классический «прощупыватель» из литературы Аксельрода.

Идея: сыграть короткую заданную последовательность (D, C, C) и по
реакции соперника решить, наивный он или ретейлиатор.

Алгоритм:
  - Раунд 1 (n=0): D — «пробный укол».
  - Раунд 2 (n=1): C — даём передохнуть.
  - Раунд 3 (n=2): C — снова даём шанс.
  - Раунды 4..N:
      - Если соперник в раундах 2 и 3 оба раза сыграл C
        (т.е. не отомстил за пробный D), считаем его наивным
        кооператором и переключаемся в AllD навсегда.
      - Иначе переключаемся в TFT (копируем последний ход
        соперника). Это «извинение» — мы попробовали, нас наказали,
        больше не пытаемся.

Чем отличается от существующего bot_tester:
  - Tester после двух раундов «пастушит» голубя бесконечным C/D/C/D —
    выжимает в среднем 2.5 очка на раунд (T=5 в чётных, S=0 в
    нечётных).
  - Prober переключается в чистый AllD после обнаружения голубя —
    выжимает T=5 на каждом раунде (если соперник AllC) или
    «амортизирует» (если у соперника есть запоздалая реакция).

Гипотезы, которые этот бот проверяет:
  1. **Warmup-окно Adaptive AllC** (warmup=10 раундов AllC, потом
     проверка трёх условий). У AAC мой D на ходу 0 — единственный
     в warmup; AAC не среагирует. На раундах 1 и 2 я играю C →
     opp_history[1] и [2] = C (AAC всё ещё в AllC). По моей логике
     я переключаюсь в AllD. После моих 5 D AAC сам в D-режиме →
     DD до конца. Прибыль: ~6-8 раундов эксплуатации, потом 192
     раундов DD. Это даст ~30 очков сверху над «честным» TFT-вых
     против AAC.
  2. **AllC, soft cooperators**: я перейду в AllD и наберу ~990.
     Это ожидаемая «жирная зона».
  3. **TFT-семья**: я попробую, получу ответ D в раунде 2 →
     переключусь в TFT. Каскад из 1 D, потом стабильная кооперация.
     Потеряю ~10-20 очков относительно чистого TFT.
  4. **AllD/Grim**: переключусь в TFT после получения D в раундах 2
     и 3 → DD до конца. Аналогично TFT.
  5. **Self-play**: оба пробера прощупают, увидят C/C → оба
     переключатся в AllD → DD до конца. Хуже, чем у любого
     кооператора (1 vs 3 за раунд).

Шум: с p=0.02 один из ходов соперника может перевернуться. Это
может ложно сработать в обе стороны (наивный кооператор оказался
«ретейлиатором» из-за шума, или ретейлиатор отметился как голубь).
Я сознательно НЕ сглаживаю это — простота правила важнее: правильная
оценка примерно в 96% случаев, и эти 4% будут случайно распределены
между матчами.

Почему это (вероятно) НЕ выиграет турнир:
  - Высокая чувствительность к каскадам с TFT-семьёй (потеряю
    20-50 очков на каждом матче из-за моего пробного D).
  - Self-play катастрофически (DD).
  - Большие выигрыши только в матчах vs «honest cooperators», которых
    в этой популяции ~5-6 штук.

Зачем тогда добавлять:
  - Тестирует «дыру в warmup» Adaptive AllC.
  - Создаёт «эволюционное давление» на популяцию: если кооператоры
    распространены, Prober выигрывает локально. Это пример того,
    как простой эксплуататор может «инвадировать» популяцию
    кооператоров.
"""


def choose_move(my_history, opp_history):
    n = len(my_history)

    # Жёстко прописанная последовательность пробы.
    if n == 0:
        return "D"
    if n == 1:
        return "C"
    if n == 2:
        return "C"

    # n >= 3: решаем режим.
    # Опасный кооператор должен был ответить D хотя бы раз в раундах 2 или 3
    # (т.е. в opp_history[1] или opp_history[2]). Если оба раза он играл C,
    # то это голубь — эксплуатируем AllD.
    if opp_history[1] == "C" and opp_history[2] == "C":
        return "D"

    # Иначе — обычный TFT.
    return opp_history[-1]
