"""Remorseful TFT — TFT, который после собственного шумового D
играет «глубокое извинение»: подряд 3 хода C, что бы соперник ни
делал в этом окне.

Контекст
--------
В шумной IPD у TFT есть классическая болезнь: один случайный
шумовой флип C→D запирает обоих игроков в DD-каскаде. Решений
несколько:

- Generous TFT (gTFT): прощает D случайно с вероятностью g=1/3.
  Хорошо в self-play, но «протекает» против AllD/Grim — ~1/3 ходов
  отдаются хищнику как T=5.
- Contrite TFT (cTFT): «извиняется» одним ходом C за собственный
  шумовой D. Это дешевле, чем gTFT, но в нашей реализации (без
  взаимного «всё ок, обнули») всё равно успевает вызвать у соперника
  ответный D, и после моего одного C мы снова входим в каскад через
  его ответный D.
- Tit-for-Two-Tats: терпит первый D соперника, мстит со второго. Не
  лечит мой собственный шумовой D, только опп-шум.

Remorseful TFT
--------------
Идея: моя шумовая D — это маленькая «обида», которую соперник по-
TFT-ному вернёт ответным D на следующем ходу. Если я после моего
шумового D сыграю один C, соперник всё равно успеет вернуть D, и я
по TFT отвечу на этот D своим D — каскад продолжится.

Поэтому я плачу «двойной взнос на восстановление»: после своего
шумового D я играю **три C подряд**:

- Ход k+1: я C (соперник, скорее всего, играет D в ответ на мой
  шумовой D с хода k — это его «справедливая» месть, я её принимаю).
- Ход k+2: я снова C (соперник, видя мой C с k+1, у TFT/gTFT/Pavlov
  переключается обратно на C; у Grim уже всё пропало, но это
  отдельный диагноз).
- Ход k+3: я снова C (страховочный — поглощаю последний возможный
  каскадный D от соперника, который мог пойти ещё с k+2 у медленных
  стратегий).

С хода k+4 возвращаюсь к чистому TFT.

Реализация
----------
Состояния между вызовами нет, поэтому каждый раз восстанавливаем
свою «намеренную» цепочку intended[0..n-1] по правилам:

    intended[0] = "C"
    intended[k] = "C"  если remorse_remaining > 0
    intended[k] = opp_history[k-1]  иначе (стандартный TFT)

Сразу после фиксации intended[k] сравниваем с actual[k]
(= my_history[k]). Если intended[k] == "C" и actual[k] == "D" —
значит шум перевернул моё C в D → запускаем (или перезапускаем)
remorse_remaining = 3. Иначе если remorse_remaining > 0, уменьшаем
его на 1 после хода.

Гипотезы (что должно случиться в турнире)
------------------------------------------
- vs self (Remorseful vs Remorseful): после одиночного шумового
  флипа окно из 3C полностью гасит каскад, оба восстанавливаются на
  CC за ~3 хода. Должен быть заметно лучше TFT-vs-TFT и хотя бы
  паритет с gTFT-vs-gTFT.
- vs TFT: после моего шумового D три C поглощают месть TFT и
  возвращают в CC. Должен быть лучше или равен cTFT vs TFT.
- vs AllC: бьёт ровно TFT (≈ R ≈ 3.0 за раунд минус потери на шум).
- vs AllD: чуть хуже TFT. После каждого шумового C->D->C
  тратится 3 хода на бесполезное C. При noise=0.02 и rounds=200
  ожидаемое число своих шумовых D = 200*0.02 ≈ 4, потеря по
  сравнению с TFT ≈ 4*3*4 ≈ 48 очков. TFT vs AllD ≈ 211 → ожидаю
  ≈ 165–180.
- vs Grim: после моего шумового D Grim уходит в вечный D навсегда.
  Я три хода трачу на C, потом TFT-зеркалю D. Ожидаю чуть хуже TFT.
- vs gTFT: симметрично хорошо. Оба прощают друг друга.
- vs Pavlov: 3-C ремиссия попадает в чужую WSLS-логику и
  стабилизирует CC.

Чем отличается от Contrite TFT
-------------------------------
cTFT платит 1 C-«извинение», Remorseful — 3 C-«извинения». Цена
больше vs AllD, но возможность гасить каскад в среде с TFT-семей-
ством — выше. Это типичный trade-off: дешёвая страховка vs
надёжная страховка.

Никакой случайности (в отличие от gTFT) — поведение полностью
детерминировано после фиксации фактической истории.
"""


_REMORSE_WINDOW = 3


def _simulate_intended_and_remorse(my_history, opp_history):
    """Прокручиваем все прошедшие ходы и возвращаем текущее значение
    remorse_remaining на момент принятия решения о ходе t = len(my_history).
    """
    remorse_remaining = 0
    n = len(my_history)
    for k in range(n):
        if remorse_remaining > 0:
            intended_k = "C"
            consumed = True
        elif k == 0:
            intended_k = "C"
            consumed = False
        else:
            intended_k = opp_history[k - 1]  # стандартный TFT
            consumed = False

        # Шум мог перевернуть intended_k. Смотрим, что на самом деле.
        if intended_k == "C" and my_history[k] == "D":
            # Мой шумовой D из intended C: открываем (или перезапускаем)
            # окно извинения на 3 хода вперёд.
            remorse_remaining = _REMORSE_WINDOW
        elif consumed:
            # Это был ход извинения и шумом он не сбит → списываем 1.
            remorse_remaining -= 1
        # Иначе (нормальный TFT-ход без шумового D) — состояние не трогаем.
    return remorse_remaining


def choose_move(my_history, opp_history):
    # Первый ход — «nice»: всегда C.
    if not my_history:
        return "C"

    remorse_remaining = _simulate_intended_and_remorse(my_history, opp_history)

    if remorse_remaining > 0:
        return "C"

    # Стандартный TFT.
    return opp_history[-1]
