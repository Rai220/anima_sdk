"""Forgiving Grim — Grim Trigger с «памятью с истечением».

Контекст
--------
Классический Grim Trigger катастрофичен в шумной среде: один шумовой
D соперника (или мой шумовой D, на который соперник по-TFT-ному
ответил D) — и я навсегда в режиме D, теряя ~3 очка за раунд на
самом перспективном CC-канале. В матче self vs self два Grim'а
запираются в DD после первого же шума: рейтинг ≈250.

Forgiving Grim — компромисс между «никогда не прощать» (Grim) и
«всегда забывать» (TFT-семья). Идея заимствована из криминологии:
лёгкие преступления имеют срок давности 5-10 лет, тяжкие — нет.

Алгоритм
--------
Считаем «непрощённые» D соперника в скользящем окне:

1. Сначала «непрощённых D» — 0, играем C.
2. Каждый раз, когда соперник играет D, увеличиваем счётчик
   непрощённых D.
3. Если в любой момент непрощённых D ≥ 2 (т.е. **повторное**
   предательство в недавнем прошлом) → играю D **навсегда** на
   оставшийся матч. Это классический Grim, но с порогом 2, не 1.
4. Если непрощённых D = 1, проверяем: прошло ли уже 50 ходов с
   момента того D и в последних 20 ходах ни одного D соперника?
   Если да — «прощаем», обнуляем счётчик и возвращаемся к C.
5. Иначе (непрощённых D = 1 в недавнем окне) — играю D в этом ходу,
   но НЕ навсегда: жду истечения «срока давности».

Отличие от:
- Grim: порог 2 D (а не 1) и таймаут забвения 50 ходов.
- TFT: не реагирует на каждый D, а копит и взвешивает.
- Tit-for-Two-Tats: TFTT мстит только за два D **подряд**.
  Forgiving Grim мстит за два D **в любых местах матча**.
- Gradual: Gradual мстит длинно за каждый D. Forgiving Grim
  мстит **раз и навсегда** за повторение, но **прощает первое**.

Гипотезы для турнира (rounds=200, noise=2%)
-------------------------------------------
- vs Self (Forgiving Grim): один шумовой D почти неизбежен на 200
  ходов; но второго шумового может и не быть (P ≈ 0.5 за 200 ходов
  с noise=2% на обоих). Если только один шум — оба прощают, CC
  большую часть матча. Ожидаю 500-580 очков (vs ~250 у обычного Grim).
- vs Grim: оба ловят шум, оба уходят в D. Forgiving Grim тоже
  переключается на D после второго D от Grim, но Grim никогда не
  прощает. Ожидаю ≈ Grim vs Grim (250-280).
- vs AllD: после второго D AllD → я в D навсегда. Ожидаю ≈ TFT vs
  AllD (~210), но с 1-2 «дани» в первых ходах = ~205.
- vs TFT: первый шумовой D у TFT (от моего шумового D) → у меня
  счётчик=1. Жду 50 ходов; за это время TFT, скорее всего, словит
  ещё один мой D в каскаде → счётчик=2 → я в D навсегда. Это
  плохой матч; ожидаю DD большую часть = 250-300.
- vs gTFT: gTFT прощает мои D с 1/3 → каскад короче. Иногда
  выходим в CC до второго D. Ожидаю 400-500.
- vs AllC: чистое CC; редкий шум от меня (мой D) → счётчик-то у
  меня самого, не сработает. Идеально, 580+.
- vs Pavlov: Pavlov WSLS — нестабилен после моего D, но если я
  быстро прощаю, обычно стабилизирует CC. Ожидаю 400-500.

Аксельродовские принципы
-------------------------
- **Nice**: ✅ первый ход C.
- **Retaliatory**: ✅ мщу за повторное D.
- **Forgiving**: ✅ прощаю одиночный D через 50 ходов.
- **Non-envious**: ✅ цель — CC, а не «обогнать».

Связь с реальностью
-------------------
Это модель «дай-шанс-один-раз» из переговоров, бизнеса, личных
отношений. После первого нарушения договорённости — холодная фаза
(играю D), но если партнёр исправился и не повторял — возвращаемся
к доверию. После второго нарушения — разрыв навсегда.

В международной политике это похоже на санкции после первого
нарушения договора + полный разрыв после второго (Хартмановские
санкции против СССР 1979, эскалация после Афганистана; vs полная
изоляция Северной Кореи после многократных нарушений ДНЯО).

Реализация полностью детерминирована, без random.
"""

# Параметры. Подобраны так, чтобы:
# - срок «давности» каждого D = 50 ходов;
# - окно проверки «не было нового D» = 20 последних ходов;
# - порог непрощённых D для безвозвратного срыва доверия = 3.
#
# Почему порог 3, не 2: при noise=2% и rounds=200 ожидаемое число
# шумовых D соперника ≈ 4. Порог 2 фактически всегда срабатывает на
# одиночный шум — это превращает бота в обычный Grim. Порог 3 даёт
# реальное окно прощения: один-два шумовых D ещё прощаются, но
# системная агрессия (AllD, Joss, Tester) детектится за 3-5 ходов и
# карается навсегда.
_FORGIVE_AFTER = 50      # ходов после последнего D, чтобы декрементить счётчик
_QUIET_WINDOW = 20       # сколько последних ходов должно быть чистыми
_HARD_THRESHOLD = 3      # сколько непрощённых D триггерит вечный D


def choose_move(my_history, opp_history):
    # Первый ход: nice.
    if not my_history:
        return "C"

    n = len(opp_history)
    # Восстановим состояние счётчика по всей истории.
    # unforgiven = текущее число «непрощённых» D соперника.
    # last_d_idx = индекс последнего «непрощённого» D (для таймаута).
    unforgiven = 0
    last_d_idx = -1
    # Также флаг «вечного» D — как только сработал порог HARD_THRESHOLD,
    # запоминаем и больше не прощаем.
    permanent_d = False

    def try_forgive(at_idx):
        """Если прошёл срок давности и в окне QUIET_WINDOW нет D, декремент."""
        nonlocal unforgiven, last_d_idx
        if unforgiven <= 0 or last_d_idx < 0:
            return
        if at_idx - last_d_idx < _FORGIVE_AFTER:
            return
        start = max(0, at_idx - _QUIET_WINDOW)
        if "D" in opp_history[start:at_idx]:
            return
        # Прощаем один D. Если после этого ещё остались — last_d_idx
        # неизвестен точно; для простоты «сдвинем» его на at_idx, чтобы
        # следующее прощение требовало нового FORGIVE_AFTER окна.
        unforgiven -= 1
        if unforgiven == 0:
            last_d_idx = -1
        else:
            last_d_idx = at_idx  # отсрочка следующего прощения

    for k in range(n):
        if not permanent_d:
            try_forgive(k)
        if opp_history[k] == "D":
            unforgiven += 1
            last_d_idx = k
            if unforgiven >= _HARD_THRESHOLD:
                permanent_d = True

    if permanent_d:
        return "D"
    # Перед решением попробуем простить ещё раз, на индексе n.
    try_forgive(n)

    if unforgiven == 0:
        return "C"
    # unforgiven >= 1 и «срок давности» не вышел → играем D, но не permanent.
    return "D"
