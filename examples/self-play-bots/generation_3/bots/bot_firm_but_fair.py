"""Firm-but-Fair (FbF, Frean 1994; Posch et al. 1999).

Реактивная стратегия на последний ход, родственница Pavlov, но с
сдвинутой «таблицей реакции» в более кооперативную сторону.

Правило по последнему раунду (мой ход, ход соперника) → следующий
мой ход:
    (C, C) → C   — продолжаю кооперацию.
    (D, C) → C   — «fair»: не закрепляю эксплойт, возвращаюсь к C.
    (D, D) → C   — попытка восстановить кооперацию после взаимного D.
    (C, D) → D   — «firm»: наказываю, если соперник предал, когда я
                   был добр.

То есть FbF играет C **везде, кроме случая «меня надули» (CD)**.

### Сравнение с соседями по семейству

| Состояние | TFT      | Pavlov   | Firm-but-Fair |
|-----------|----------|----------|---------------|
| (C, C)    | C        | C        | C             |
| (D, C)    | C        | D        | **C** (fair)  |
| (C, D)    | D        | D        | D             |
| (D, D)    | D        | C        | **C** (fair)  |

- Vs **TFT**: FbF мягче в DC (TFT забывает «я только что предал»
  и копирует C соперника, тоже C; здесь FbF = TFT).
- Vs **Pavlov**: FbF мягче и в DC, и в DD. После взаимного D Pavlov
  пробует ещё раз кооперировать (C), и FbF тоже (C). Но Pavlov
  закрепляет эксплойт после DC (продолжает D), а FbF — нет.

### Поведенческая семантика

- **«Не наказывай тех, кто простил мою ошибку».** Если я предал, а
  соперник всё равно сыграл C (например, TF2T-вариант или просто
  шумовой флип), FbF возвращается к C сразу. Это «совесть» — Pavlov
  такого не делает.
- **«Не зависай в DD».** Mutual punishment должен закончиться. FbF
  пытается выйти из DD-петли первым же шагом, что особенно ценно
  под шумом.
- **«Жёсткое наказание только когда тебя реально использовали».**
  Единственный случай D — это (C, D): я был открыт, соперник этим
  воспользовался.

### Что ожидаю от поведения в популяции

- **vs AllC**: первый ход C → CC. Под шумом будет (C,D) → D, потом
  опять (D,C) → C, и сразу C. То есть FbF почти не эксплуатирует
  AllC — он лучше TFT в этом смысле (не зависает в эксплойте).
  Не более 3 за раунд, как TFT.
- **vs AllD**: первый ход C → S=0 → (C,D) → D → DD → C → S=0 → ...
  Бесконечный цикл CD,DC? Нет: после (D,D) играет C, получает S=0,
  → (C,D) → D → DD → C → ... период 2: C/D чередуются → 1.5 за раунд.
  Хуже, чем TFT (1.05), как и у Pavlov.
- **vs TFT**: первый ход обоих C → CC. Шумовой флип CD → D у меня,
  TFT копирует D → DC у меня → (D,C) → C. Каскад короче, чем у TFT
  vs TFT, потому что FbF из (D,C) сразу возвращается к C.
- **vs Pavlov**: CC поток. Шумовой флип у Pavlov даст D, FbF
  попадает в (C,D) → D, Pavlov видит DD → C, FbF видит (D,C) → C.
  Восстановление за 2 хода.
- **vs Reverse Pavlov** (наш текущий «эксплуататор»): RevPav после
  CC играет D (lose-shift на «win»). FbF получит (C,D) → D. RevPav
  после (D,C) играет C. FbF после (D,C) тоже C. Дальше CC → RevPav
  опять D... период 2 = CD/DC, средняя ≈ 2.5 за раунд (T+S = 5,
  делим на 2). FbF здесь как Pavlov — не идеален, но не катастрофа.
- **vs Anti-Alternator**: оба ниши-кооператоры → CC поток, шумовые
  каскады короткие. ≈ 590 за матч.
- **vs Grim**: первый шумовой флип у меня → Grim навсегда D. Я в
  цикле CD/DC → 2.5 за раунд. Лучше TFT (1.05) в этом матче!

### Гипотеза рейтинга

FbF должен попасть в середину (#6-#10): не настолько cooperative как
gTFT/Soft (не имеет статистического фильтра), но мягче TFT и Pavlov.
Vs RevPav будет осциллировать, не давая катастрофы, но и не давая
эксплойта.

Главный риск: vs AllD и vs Grim — где FbF после (D,D) играет C, что
эксплуатируется. Аксельродовская критика Pavlov применима и к FbF.

### Связь с реальным миром

«Жёсткий, но справедливый» — это идеализированная модель **рабочего
взаимодействия с равным**: я не злюсь долго (выхожу из DD), не
эксплуатирую слабость (возвращаюсь к C после DC), но если меня
конкретно обманули — отвечаю. Это «средний руководитель»: лоялен
команде, но «один раз обманешь — закроем счёт за раз».

В международной политике это похоже на доктрину «мер, соразмерных
нарушению»: ответ только на конкретное действие, без эскалации, и
готовность к нормализации после нарушения.

В отличие от Grim (война до конца) и TFT (зеркало), FbF умеет
**первым предложить мир** в DD, что под шумом — важная черта.
"""


def choose_move(my_history, opp_history):
    # Первый ход — кооперация (nice).
    if not my_history:
        return "C"
    my_last = my_history[-1]
    opp_last = opp_history[-1]
    # Единственный случай D — когда меня надули: я сыграл C, опп — D.
    if my_last == "C" and opp_last == "D":
        return "D"
    # Во всех остальных случаях (CC, DC, DD) — C.
    return "C"
