"""Joss — «sneaky TFT» из исторического турнира Аксельрода.

Идея (Joss, 1980): почти TFT, но иногда нарушает кооперацию —
с малой вероятностью играет D, даже когда соперник сыграл C.
Расчёт автора: «уторговать» немного халявных T-очков (5 вместо 3)
у наивных кооператоров, не разрушая кооперацию полностью.

Реализация:
- Первый ход — C (всё ещё «nice» на первом ходе).
- Если opp[-1] == D: отвечаем D (как TFT — retaliatory).
- Если opp[-1] == C: с вероятностью SNEAK_PROB играем D, иначе C.

Что ожидаю по матрице 20×20:

- vs AllC: на любом ходе при C от опп с вер. 0.1 играем D — за 200
  раундов в среднем 20 «бесплатных» D-ходов, +2 очка каждый раз
  относительно CC = +40 очков. Ожидаю ~640 очков. Но AllC играет C
  всегда, поэтому те 20 раз даём D vs C = 5+3*180 = ~620-640.
- vs TFT: каждое наше «sneak D» провоцирует TFT на D в следующем
  раунде, потом снова взаимное C. Это короткий каскад DC→CD, мы
  получаем 5+0 = 5, TFT получает 0+5 = 5, дальше CC=3+3. Чистого
  выигрыша нет, но шум турнира + наши собственные sneak'и создают
  длинные каскады DD. Ожидаю ~400-430 (хуже чистого TFT).
- vs TF2T: TF2T прощает один D, поэтому одиночные «sneak»
  безнаказанны. **Эксплуатируем TF2T!** Ожидаю 500-550.
- vs Grim: первый же sneak D запускает вечный D-режим Grim →
  катастрофа. Ожидаю ~200 (DD до конца).
- vs gTFT: gTFT прощает с вер. 1/3, поэтому 2/3 наших sneak'ов
  ловят возмездие. Аналогично TFT с небольшим бонусом.
- vs AllD: TFT-режим даёт ~211 (DD после первого CD). Sneak не
  меняет логику. Ожидаю ~205-211.
- vs self: два Joss'а играют CC, периодически кто-то делает sneak
  → каскад. Хуже TFT-self.
- vs Pavlov: после нашего sneak D vs C, Pavlov получает S=0 → меняет
  ход на D → DD → Pavlov меняет на C → мы (TFT) играем C → CC. Но
  потом снова sneak. Циклы из 5 раундов: CCDDC = 3+3+5+1+0 = 12
  для нас, 3+3+0+1+5 = 12 для Pavlov. Симметрично, без особой
  эксплуатации.

Историческое значение: Joss проиграл первый турнир Аксельрода,
потому что его «sneak» провоцировал TFT-семью на каскады D, а
выигрыш от ловли AllC-подобных не покрывал потерь.

Ожидаемый ранг: #10-#15 (середина-низ). Не двигает топ-3.

Стохастичность через свой Random — не зависит от глобального
random.seed() и шума турнира.
"""

import random

_rng = random.Random()
SNEAK_PROB = 0.10


def choose_move(my_history, opp_history):
    if not opp_history:
        return "C"
    last_opp = opp_history[-1]
    if last_opp == "D":
        return "D"
    # opp[-1] == C: обычно отвечаем C, но иногда «крадём» D.
    if _rng.random() < SNEAK_PROB:
        return "D"
    return "C"
