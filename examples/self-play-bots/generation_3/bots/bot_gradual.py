"""Gradual (Beaufils, Delahaye, Mathieu 1996) — эскалирующая месть.

Идея: добрая стратегия, прощающая по умолчанию, но **эскалирующая
ответ** при повторных предательствах. После k-го предательства
соперника отвечаю k D-ходами подряд, после чего двумя C — «жестом
примирения». Если соперник предаст ещё раз, следующий цикл будет
длиннее (k+1 или больше, в зависимости от того, сколько он предал
к тому моменту).

В оригинальной работе 1996 года Gradual в смешанных популяциях
побеждает классический TFT и Tit-for-Two-Tats. Причина — баланс
между:

- **жёсткостью**: Gradual всегда наказывает любой D соперника
  (минимум одним D), причём чем дольше партнёр предаёт, тем дороже
  это обходится;
- **прощением**: после каждого цикла наказания обязательно идут
  2 «calm-C» как чёткий протокольный сигнал «я хочу вернуться к
  кооперации».

## Алгоритм

При вызове `choose_move` восстанавливаем состояние «с нуля» по
полной истории, потому что бот stateless:

1. Пробегаем по всем прошлым ходам и моделируем счётчики:
   - `punish_left` — сколько D ещё должен сыграть в текущем цикле
     наказания;
   - `calm_left` — сколько примирительных C ещё должен сыграть после
     наказания;
   - `cycles_triggered` — сколько циклов наказания уже было начато.
2. Для каждого прошлого хода определяем намерение по тому же правилу,
   что и для текущего:
   - если `punish_left > 0` → играл D, декрементируем;
   - иначе если `calm_left > 0` → играл C, декрементируем;
   - иначе если число D соперника к этому ходу > `cycles_triggered`
     → стартуем новый цикл (играл D), `punish_left = opp_d − 1`,
     `calm_left = 2`, `cycles_triggered += 1`;
   - иначе играл C.
3. Применяем то же правило к текущему ходу.

Важная деталь: при подсчёте `opp_d` мы используем фактическую
историю соперника (после шума). Это значит, что шумовое
предательство соперника тоже триггерит цикл наказания. Это
консервативно: партнёр-cooperator получает наказание за случайный
шумовой D. Но calm-C-ы потом возвращают нас в кооперацию.

## Замечания о шуме

- **Мой собственный шум (мой D флипнули в C):** в моей истории
  записан C, но моя state-machine это игнорирует (она работает по
  чисто opp-driven правилу). Я по-прежнему завершаю цикл наказания
  в соответствии с планом.
- **Мой собственный шум (мой C флипнули в D):** аналогично игнорирую.
- **Шум соперника:** один лишний D в opp_history → один лишний цикл
  наказания (длиной = opp_d на момент триггера). В шумной среде это
  даёт цепочку лишних D-циклов.

## Чем отличается от уже существующих ботов

- **vs TFT.** TFT отвечает на каждое D ровно одним D. Gradual
  отвечает на k-е D — k последовательными D, потом 2 C. Это
  эскалирующая месть с встроенным offer of reconciliation.
- **vs TF2T.** TF2T прощает любой одиночный D. Gradual наказывает
  даже одиночный D (хотя бы 1 D в ответ). То есть Gradual «жёстче»
  TF2T, но при этом более прощающий, чем Grim (Grim — навсегда D).
- **vs Grim.** Grim после 1-го D переходит в D навсегда. Gradual
  после 1-го D отвечает 1 D, потом 2 C — даёт шанс на восстановление.
- **vs Soft Majority.** Soft Majority смотрит на отношение C/D в
  глобальной истории. Gradual смотрит только на количество D и
  отвечает строго пропорционально. У Soft Majority «прощение»
  получается само из накопленных C; у Gradual «прощение» — это
  явные 2 calm-C, протокольный сигнал.
- **vs cTFT.** cTFT извиняется за свой собственный шум. Gradual не
  различает «свой» и «чужой» D — наказывает любой D соперника.

## Гипотезы для T9 (по матрице T8)

1. **vs AllD ≈ 207-210.** AllD предаёт каждый ход → opp_d быстро
   растёт → cycles_triggered тоже растёт. Цикл k-го наказания =
   k D-ходов + 2 C. Это значит, что я даю AllD 2 «дармовых» T=5
   очка после каждого цикла. За 200 раундов суммарно: примерно
   200/(k+2) = ~30 циклов, каждый длится 1 D + 2 C → потери ~60
   очков. TFT даёт AllD 1 раз 5 = 5 очков (один первый ход C), а
   потом DD навсегда → лучше. **Гипотеза: vs AllD буду хуже TFT,
   но не катастрофически.**

2. **vs AllC = 600.** Никогда не предаю первым; opp_d=0 → никаких
   циклов → CC по всему матчу. То же, что у всех «nice» стратегий.

3. **Self-play.** При шуме 2% за 200 раундов у соперника-Gradual
   будет ~4 шумовых D. Каждый из них запустит цикл. Первый цикл =
   1 D + 2 C; второй = 2 D + 2 C; третий = 3 D + 2 C; четвёртый =
   4 D + 2 C. Итого: 1+2+3+4 = 10 D-ходов + 8 C-ходов = 18 раундов
   «не-CC» на каждого. Это даёт примерно (200-18)*3 + 18*1.5 ≈
   546 + 27 = 573. Плюс симметричное наказание (когда мой D
   шумовой запускает их цикл, и т.д.). **Гипотеза: self-play ≈
   500-560, существенно хуже Soft Majority (589) и gTFT (590).**
   Это главная слабость Gradual: эскалирующая месть болезненна в
   шумной среде.

4. **vs Random.** Random выдаёт ~50% D-ходов = ~100 D за 200
   раундов. cycles_triggered будет расти быстро, и я застряну в
   очень длинных циклах наказания (5-й цикл = 5 D + 2 C, 10-й =
   10 D + 2 C). Большую часть матча буду в D. Это ХОРОШО против
   Random (D всегда даёт >=1, против Random's 50% D даёт
   среднее 0.5*5 + 0.5*1 = 3 за раунд). **Гипотеза: vs Random
   400-460, лучше TF2T (374), хуже Soft Majority (434).**

5. **vs Grim.** Шумовой D от Grim → я отвечаю D → Grim уже в
   D навсегда → я ещё в цикле наказания → DD. Цикл закончится,
   я дам 2 C → Grim не вернётся → я снова в D. Похоже на TFT vs
   Grim (228). Но из-за 2 calm-C я отдам 2 T=5 = 10 очков
   соперника каждые «cycle+2» ходов. **Гипотеза: vs Grim
   220-240, чуть хуже TFT.**

6. **vs Pavlov.** Pavlov шифтит, когда получает плохой outcome.
   Один мой D в цикле → Pavlov shift → если он играл C, теперь D.
   После 2 calm-C: Pavlov играет C (если он только что играл D и
   получил S=0). Мы можем оказаться в синхронной кооперации, но
   через стартовый цикл. **Гипотеза: vs Pavlov 480-540.**

7. **vs TTFT.** TTFT отвечает 2 D на 1 D. Один шумовой D у меня →
   2 D от TTFT → 2 D запускают мой цикл (cycles += 1, длина 2).
   Я даю 2 D → TTFT даёт 4 D (на каждый мой). Каскад. Это плохо:
   **Гипотеза: vs TTFT 250-350, хуже Soft Majority (576).**

8. **vs TFT.** Симметричный отыгрыш с TFT — мой D запускает их
   ответ, потом мои 2 calm-C прерывают каскад (как у gTFT).
   Однако моя месть длиннее одного D → возможен затянутый каскад.
   **Гипотеза: vs TFT 450-520.**

9. **vs Soft Majority.** Soft Majority толерантен к одиночным D
   (благодаря большинству C в истории). Мой 1-й D → Soft Majority
   остаётся в C (1 D vs N C). Мои 2 calm-C → возвращение к CC.
   **Гипотеза: vs Soft Majority 560-600.**

10. **vs gTFT, cTFT, TF2T.** Все прощают шумовой D. Цикл наказания
    инициируется только моим собственным шумом, и быстро гасится
    их прощением. **Гипотеза: 530-580 в обе стороны.**

## Теоретическая мотивация

Gradual — единственная стратегия в пантеоне, которая **эскалирует
санкции пропорционально количеству нарушений**. Это базовый принцип
международного права (gradual sanctions), уголовного права (рецидив
ужесточает наказание), сетевого протокола (TCP exponential backoff
при повторных коллизиях). Гипотеза автора стратегии — это создаёт
**отбор против хищников**, потому что хищник, который предаёт всё
чаще, получает всё более длительные периоды DD. С другой стороны,
прощение явное (2 calm-C) даёт чёткий протокольный сигнал «я готов
вернуться» — соперник может его не понять, но протокол его
включает.

## Главные ожидаемые слабости

1. **Шумовая хрупкость в self-play.** Любой шумовой D запускает
   эскалацию. В self-play это даёт цепочку «1 D, 2 C, 2 D, 2 C, …»
   потерь.
2. **Длительные DD-каскады с TTFT.** Эскалирующая месть Gradual
   синхронизируется с TTFT-каскадом, и оба застревают.
3. **Жёсткость vs AllD ниже, чем у TFT.** Я даю AllD 2 calm-C =
   2 × T=5 = 10 очков подарка каждые ~k раундов. TFT даёт AllD
   ровно 5 очков один раз.

## Связь с реальным миром

- **Санкции ЕС/США 2014-2022.** Реакция на действия РФ строилась
  именно по Gradual-схеме: каждый этап ужесточения санкций
  пропорционален новому нарушению. После каждого пакета — окно
  «переговоров» (аналог 2 calm-C). Это интуитивно похоже на
  стратегию, но эффективность спорна из-за специфики оппонента.
- **Уголовное право.** Рецидив ужесточает наказание. Это
  встроенный Gradual: первая судимость → условный срок,
  повторное преступление → реальный срок, третий раз →
  максимальный срок. Принцип «эскалация по числу нарушений».
- **TCP/IP exponential backoff.** При повторных коллизиях время
  ожидания растёт экспоненциально. Тот же принцип эскалации
  пропорционально числу нарушений.
- **Семейные конфликты.** В отношениях часто появляется такое:
  «первый раз прощу, второй раз затаю, третий — серьёзный разговор».
  Это житейский Gradual без формализованной calm-фазы.
"""


def choose_move(my_history, opp_history):
    if not opp_history:
        return "C"

    # Reconstruct state-machine from scratch.
    punish_left = 0  # how many more D's we owe in the current punishment
    calm_left = 0    # how many calm-C's we owe after the punishment
    cycles_triggered = 0  # how many punishment cycles we've started

    n = len(my_history)
    for t in range(n):
        if punish_left > 0:
            punish_left -= 1
            continue
        if calm_left > 0:
            calm_left -= 1
            continue
        # Normal mode at turn t. Check whether a new cycle should have
        # started before turn t (based on opp_history[:t]).
        opp_d = opp_history[:t].count("D")
        if opp_d > cycles_triggered:
            # New cycle: this turn we played D, plus opp_d - 1 more D's,
            # then 2 calm C's.
            punish_left = opp_d - 1
            calm_left = 2
            cycles_triggered += 1
        # else: cooperated this turn; state unchanged.

    # Decide for the current turn.
    if punish_left > 0:
        return "D"
    if calm_left > 0:
        return "C"
    opp_d = opp_history.count("D")
    if opp_d > cycles_triggered:
        return "D"
    return "C"
