"""Omega TFT — TFT с защитой от AllD и двумя типами «деадлок-разрывов».

Идея — взять обычный TFT и добавить **три независимых патча** для
трёх известных проблем семейства TFT в шумной среде:

1. **AllD detector.** Если соперник в последних `WIN` ходах сыграл D
   ≥ `D_RATE_LIMIT * WIN` раз, классифицируем его как «хищник» и
   уходим в безусловный D навсегда. Это фикс «жадного» TFT, который
   платит шумовой штраф против AllD/Grim.

2. **Alternation deadlock break (CDCD-DCDC).** Если последние 4 хода
   мы и соперник играли строго противоположно (зеркальный пинг-понг
   шумового каскада TFT), играем C — рвём цикл. Аналогично тому, что
   gTFT делает вероятностно, только здесь триггер детерминированный и
   точечный.

3. **Mutual-D trap break (олив-бранч).** Если последние `OLIVE_WIN`
   ходов **мы и соперник оба** играли D (`my[-K:] == opp[-K:] ==
   D...D`) — это «двойной шумовой trap», когда два подряд шумовых D
   у обоих сторон заперли нас в DDDD. Но **только** если глобальная
   доля моих D < 0.4 (т.е. мы исторически кооперировали). Иначе это
   просто матч против AllD / Grim, где олив-бранч сольёт нам S=0.

Реализация stateless: вся логика читается из истории при каждом
вызове.

### Параметры

- `WIN = 20` — окно для AllD-детектора. Шум 0.02 в 20 раундах даёт
  ~0.4 ложного D, далеко от порога.
- `D_RATE_LIMIT = 0.7` — порог классификации AllD. Защита уверенная,
  но не агрессивная (TFT и cTFT в нормальной игре дают ≤ 0.05
  ложных D — порог не перешагнут).
- `ALT_WIN = 4` — длина CDCD-deadlock.
- `OLIVE_WIN = 3` — длина DDDD-trap для олив-бранч.
- `MUTUAL_D_LIMIT = 0.40` — максимальная доля моих D, при которой
  готов послать олив-бранч (не путать с d_rate соперника).

### Поведение vs пантеон

- **vs AllD.** D почти 100%, AllD-детектор сразу триггерится → играем
  D всё время. ~211 как у TFT.
- **vs AllC.** D rate соперника 0%, никаких detect-ов не срабатывает,
  TFT-зеркало → ~600. Лучше gTFT (591), сопоставимо с TFT.
- **vs TFT.** В шумной среде иногда каскад CDCD. ALT_WIN ловит его
  на 4-м ходу → играем C → выходим в CC. Ожидаемо лучше TFT vs TFT
  (472). Цена: после каждого каскада 4 хода CD/DC, чистый убыток
  ~4 очка.
- **vs Random.** Random даёт ~50% D в окне 20. D rate ≈ 0.5, до 0.7
  не дотягивает → играем TFT (зеркало). Ожидаемо как TFT vs Random,
  ~470.
- **vs Pavlov.** Pavlov-овский «жалящий» D запускает каскад. ALT_WIN
  ловит его → играем C. Pavlov видит C, своё последнее было D,
  получил T=5 → опять C. Возврат в CC, но Pavlov «доит» нас раз в
  ~10 раундов. Ожидаемо паритет с Pavlov vs TFT (459).
- **vs Pavlov-style двойной шумовой trap в self-play.** OLIVE_WIN
  ловит DDDD после редкого двойного шумового удара → играем C →
  выходим в CC. Это и есть починка self-play.

### Что не так в self-play без олив-бранч

Без OLIVE_WIN: два подряд шумовых D у обеих сторон (вероятность ~
0.02 * 0.02 = 0.0004 за раунд, но это считается **последовательно**
по двум раундам — реальная частота ~0.0001 за пару) приводят к
тому, что обе стороны видят последний ход соперника D и зеркалят
его. Из CD-DC мы успешно выходим через ALT_WIN, но из DD-DD-DD
выхода нет: ALT не сработает (нет alternation), а пара играет
DD навсегда. Эта дыра убивает self-play (наш предыдущий замер дал
71 раунд DD из 200).

### Цена олив-бранч

Если соперник не Omega-Tft, олив-бранч в DDDD против него стоит
1 ход S=0 за выход. Если соперник Pavlov (он в DD играет D на
прошлый P=1, что значит «менять ход» → C) — олив-бранч у Pavlov
и нас совпадает, обе стороны играют C, выходим. Хорошо.

Если соперник Grim — он в режиме D навсегда, олив-бранч мы отправим
ему как S=0, и он не одумается, мы получим один шумовой удар. Но
ограничение `MUTUAL_D_LIMIT < 0.4` не выполнится: против Grim мы
быстро накопим долю D > 0.4 (Grim в D с шумового момента до конца,
мы зеркалим) → олив-бранч не пошлём.
"""

WIN = 20
D_RATE_LIMIT = 0.70
ALT_WIN = 4
OLIVE_WIN = 3
MUTUAL_D_LIMIT = 0.40


def choose_move(my_history, opp_history):
    if not opp_history:
        return "C"

    n = len(opp_history)

    # --- AllD detector ------------------------------------------------------
    if n >= WIN:
        window = opp_history[-WIN:]
        if window.count("D") >= int(D_RATE_LIMIT * WIN):
            return "D"

    # --- Alternation deadlock (CDCD-DCDC) -----------------------------------
    if n >= ALT_WIN:
        my_w = my_history[-ALT_WIN:]
        opp_w = opp_history[-ALT_WIN:]
        is_alt = all(my_w[k] != opp_w[k] for k in range(ALT_WIN))
        my_alternating = all(my_w[k] != my_w[k - 1] for k in range(1, ALT_WIN))
        opp_alternating = all(
            opp_w[k] != opp_w[k - 1] for k in range(1, ALT_WIN)
        )
        if is_alt and my_alternating and opp_alternating:
            return "C"

    # --- Mutual-D trap (олив-бранч) ----------------------------------------
    if n >= OLIVE_WIN:
        my_w = my_history[-OLIVE_WIN:]
        opp_w = opp_history[-OLIVE_WIN:]
        if all(m == "D" for m in my_w) and all(o == "D" for o in opp_w):
            # Готовы простить только если исторически кооперировали много.
            my_d_rate = my_history.count("D") / n
            if my_d_rate < MUTUAL_D_LIMIT:
                return "C"

    # --- TFT по умолчанию ---------------------------------------------------
    return opp_history[-1]
