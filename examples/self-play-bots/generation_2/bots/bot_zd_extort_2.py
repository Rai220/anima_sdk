"""ZD-Extort-2 — экстортная стратегия Press & Dyson, PNAS 2012.

Memory-1 стохастическая стратегия, которая навязывает линейное
соотношение между ожидаемыми очками: σ_self - P = 2 * (σ_opp - P).
То есть мои очки растут вдвое быстрее, чем у соперника, относительно
базовой линии взаимного предательства P=1.

Канонические вероятности для стандартной матрицы (T=5, R=3, P=1, S=0)
и коэффициента экстортии χ=2:

- p1 = P(C | prev = CC) = 11/13 ≈ 0.846
- p2 = P(C | prev = CD) = 1/2  = 0.500   (я C, соперник D)
- p3 = P(C | prev = DC) = 7/26 ≈ 0.269   (я D, соперник C)
- p4 = P(C | prev = DD) = 0              (я D, соперник D)

Что ожидать в турнире:
- vs AllC: высокий выигрыш (extort выжимает максимум).
- vs AllD: всегда D (p4=0), счёт = 1 на раунд, как у всех.
- vs TFT: будет цикл C/D, экстортия здесь слабее, но всё ещё ≥ TFT.
- vs самого себя: шумно, могут возникать долгие D-цепочки.
- В шумной среде ZD стратегии работают хуже теоретического, потому
  что любой случайный D запускает каскад взаимных наказаний.

Это **не «дружелюбная»** стратегия по Аксельроду: она наказывает
первой (p3 < 1 после своего же D). В популяции с защитниками
получит средний ранг. Включён в турнир ради REPORT.md (классика
теоретической литературы).
"""

import random

# Канонические значения Press-Dyson 2012 для extortion-2.
P_CC = 11.0 / 13.0
P_CD = 1.0 / 2.0
P_DC = 7.0 / 26.0
P_DD = 0.0

_rng = random.Random(0xE7042)  # фиксированный seed, чтобы матч был воспроизводимым


def choose_move(my_history, opp_history):
    # Первый ход — C (нейтральное начало, как у TFT).
    if not my_history:
        return "C"
    last_me = my_history[-1]
    last_opp = opp_history[-1]
    if last_me == "C" and last_opp == "C":
        p = P_CC
    elif last_me == "C" and last_opp == "D":
        p = P_CD
    elif last_me == "D" and last_opp == "C":
        p = P_DC
    else:  # DD
        p = P_DD
    return "C" if _rng.random() < p else "D"
