"""Anti-Probe — кооперативная стратегия, наказывающая прощупывание.

### Идея

Стратегии типа `classifier` начинают матч с двух D подряд — это
«probe», чтобы проверить, наивный ли соперник (AllC). Anti-Probe
ловит ровно этот паттерн: если соперник сыграл `D` в раундах 0 и 1,
он либо probe-классификатор, либо AllD-хищник. В обоих случаях
выгоднее не позволять себя эксплуатировать — переключаемся в
постоянное D до конца матча.

В остальных случаях ведём себя как «миролюбивый cTFT»: первый ход C,
дальше прощаем одиночные D-всплески (шум), отвечаем на устойчивые
паттерны D.

### Phase diagram

| Раунд | Поведение                                                 |
|-------|-----------------------------------------------------------|
| 0     | C (nice first move)                                       |
| 1     | TFT: зеркалим opp_history[0]                              |
| 2+    | Если opp[0]==D и opp[1]==D → permanent D до конца матча  |
| 2+    | Иначе → стандартный contrite TFT                          |

### Почему это работает против probe-стратегий

- **vs classifier**: classifier играет `D, D` в раундах 0-1. Anti-Probe
  ловит этот паттерн на раунде 2 → permanent D. Classifier пытается
  играть olive (C×5), но получает 0,0,0,0,0 = 0 очков за 5 раундов
  (мы играем D vs его C). После olive classifier видит нас как
  AllD-mostly, переключается в D навсегда. DD-trap до конца матча.
  Прогноз: мы получим ~215 (5 раундов exploit + 193 раунда DD =
  25 + 193 = 218), classifier ~199 (1+1 в первых двух + 0×5 olive +
  193×1 DD). Мы выходим из этого матча **выше**, чем classifier.

- **vs always_d**: тоже DD в первых раундах → permanent D. Получаем
  ~200, как обычный TFT.

- **vs Random**: P(opp играет D в обоих раундах 0,1) ≈ 0.25. В 25%
  матчей с Random мы триггеримся ошибочно. Цена этого: ~200 vs ~440
  (cTFT vs Random), потеря ~240 в матче, что распределяется по
  средне ~240*0.25/(17 матчей) = ~3.5 очка средней потери. Приемлемо.

- **vs cTFT/TFT/TF2T/omega/SM**: эти боты начинают с C → opp[0]=C
  → триггер не срабатывает → стандартный cTFT. Получаем близко к
  cTFT vs cTFT = 593.

- **vs Grim, Pavlov, AllC**: все начинают с C → не триггеримся,
  играем как cTFT.

### Цена ложного срабатывания при шуме

P(opp[0]=D | opp хотел C) = 0.02 (шум).
P(opp[1]=D | opp хотел C, наш round 0 был C) — зависит от стратегии:
- AllC: P=0.02 (только шум).
- TFT: P=0.04 (шум опа или шум на нашем round 0 → opp видит D и
  отвечает D, причём это уже не шум).

P(оба D у мирной стратегии) ≈ 0.02 × 0.04 = 0.0008.
За 17 ботов × 3 повтора = 51 матч, ожидаемое число ложных
срабатываний ~0.04. Раз в 25 турниров будем ошибочно лочить
одного дружелюбного партнёра. Невелико.

### Что мы НЕ ловим

- **`handshake`** играет `D, C, D, C, C` — opp[0]=D, opp[1]=C. НЕ
  триггер. Это OK: handshake тоже использует D в первом ходе, но
  как сигнатуру, не как probe. Триггер только на устойчивое DD.
- **`gradual` (Tester)** — играет C в первом ходе. Не триггер.
- **Random с C-C-сидом** — не триггер.

### Эволюционный смысл

Anti-Probe — это **контр-стратегия для classifier-семейства**.
Цель: убрать награду за probe-фазу. Если в популяции 1 anti_probe
и 1 classifier, classifier теряет 593 - ~218 = -375 в этом матче.
Распределённо по 17 матчам classifier-а: -22 очка среднего.
Это сдвигает classifier с #1 (~538) на ~516, выбивая его из топ-3.

В реальной политике аналог: страны/коалиции, которые **сразу
отвечают** на агрессивную дипломатию (probe-D), не давая
оппоненту перейти к фазе примирения (olive). Пример: жёсткие
ответы на территориальные провокации (Карибский кризис 1962:
блокада Кубы как немедленный D на ввоз советских ракет).
"""


def choose_move(my_history, opp_history):
    n = len(opp_history)

    # Раунд 0: nice first move.
    if n == 0:
        return "C"

    # Раунд 1: зеркалим opp[0] (TFT-первый-ответ).
    if n == 1:
        return opp_history[0]

    # Раунд 2+: проверяем probe-trigger.
    if opp_history[0] == "D" and opp_history[1] == "D":
        # Соперник начал с двух D подряд — probe-классификатор или AllD.
        # Постоянное D до конца матча.
        return "D"

    # Стандартный contrite TFT.
    last_opp = opp_history[-1]
    if last_opp == "C":
        return "C"

    # last_opp == "D" — решаем, отвечать или простить.
    if opp_history[-2] == "D":
        # Систематическое предательство.
        return "D"
    if my_history[-1] == "D":
        # Соперник, видимо, отзеркалил мой D.
        return "D"

    # opp[-2]=C, my[-1]=C, opp[-1]=D — одиночный D, кандидат на шум.
    window = opp_history[-min(5, n):]
    if window.count("D") >= 2:
        # В окне уже ≥2 D — это паттерн, не шум.
        return "D"
    # Прощаем шум.
    return "C"
