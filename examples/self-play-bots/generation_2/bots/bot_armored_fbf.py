"""Armored Firm-but-Fair — FBF с AllD-детектором.

Идея — взять Frean's FBF и устранить его единственную системную дыру:
катастрофические матчи против AllD (107) и Grim (140), где FBF
залипает в C-D-C-D пинг-понг и теряет ~300 очков.

Решение скопировано из Omega-TFT: считать долю D соперника в скользящем
окне последних `WIN` ходов; если она превышает порог `D_RATE_LIMIT`,
бот переключается в режим вечного D — «armor mode». Когда armor
включён, обратной дороги нет, чтобы не дать сопернику манипулировать
коротким окном.

### Параметры

- `WIN = 20` — окно для детектора. Шум 0.02 даёт ~0.4 ложных D на 20
  раундов, далеко от порога.
- `D_RATE_LIMIT = 0.7` — 14 D из 20. AllD/Grim перешагивает порог
  заведомо; TFT, cTFT, SM, gTFT — никогда (они дают ≤ 0.05 ложных D
  в нормальной игре).

### Базовая логика (FBF, Frean 1994)

Memory-1 со вероятностями (p1..p4) = (1, 0, 1, 1) по парам
(my_last, opp_last):

- (C, C) → C: «всё хорошо».
- (C, D) → D: «наказываю одиночное предательство».
- (D, C) → C: «я обманул, он не ответил — возвращаюсь в норму»
  (это и есть «fairness»: не доим доверчивых).
- (D, D) → C: «оба упёрлись, попробую первым выйти» (p4 = 1).

### Ожидаемое поведение vs пантеон

- **vs AllD = ~211** (вместо 107 у FBF). Через ~20 раундов armor
  включается, дальше играем D → P=1 каждый раунд. Прибыль ~100 очков.
- **vs Grim = ~280** (вместо 140 у FBF). Аналогично: пока шум не
  триггернёт Grim в режим AllD, играем FBF (CC). После триггера
  — armor через ~20 раундов. Прибыль ~140 очков.
- **vs AllC = ~590** (близко к FBF 599). Armor не сработает.
- **vs TFT/cTFT/SM/gTFT/TF2T = ~570-600.** Сохранение FBF-поведения.
- **vs Random = ~470 (вместо 565 у FBF).** Random даёт 50% D в окне.
  Не дотягивает до 0.7 → armor не включится. Поведение FBF
  сохраняется. Цифра должна остаться близкой к FBF.

  Поправка: 565 у FBF против Random — это потому, что FBF активно
  наказывает random-овские D (через CD→D), а random не реагирует.
  Armored FBF будет вести себя так же → ожидаем те же ~565.
- **vs Pavlov = ~572** (как FBF).
- **vs FBF/Armored FBF в self-play под шумом.** Та же CD/DC
  alternation, что и у обычного FBF. Armor не сработает: в alternation
  opp D-rate ≈ 0.5. Self-play ожидаем тот же ~542.

### Цена armor-а

Главный риск — ложное срабатывание. С `WIN=20` и `D_RATE_LIMIT=0.7`
для триггера нужно 14 D из 20 у соперника. Под шумом 0.02 у любого
TFT-форварда настоящих D почти нет; нужна реальная последовательная
агрессия. Этого не происходит у кооперативных стратегий.

### Гипотеза рейтинга

- Старый FBF: 491.67 (6-е место в Т10).
- Прибыль от armor: ~104 + ~140 = ~244 очка по двум матчам.
- 244 / 14 матчей ≈ +17 очков среднего.
- Ожидаемый рейтинг: ~508-510 → должен войти в топ-3.

Если попадёт в топ-3, это подтвердит, что главный «гвоздь» FBF —
именно отсутствие детектора непримиримых, а не сама структура (1, 0, 1, 1).

### Связь с реальным миром

Это «демократия с красной линией». Дефолт — диалог, прощение,
самовосстановление; но при систематическом нарушении норм со стороны
соперника (≥ 70% враждебных действий в скользящем окне 20 раундов)
включается режим «обороны». Аналог — НАТО ст. 5, санкционные пакеты,
«доктрина сдерживания». В отличие от чистого FBF (политика добрых
намерений) и чистого Grim (вечная злопамятность), armored FBF
сочетает кооперативный дефолт с порогом «непримиримости».
"""

WIN = 20
D_RATE_LIMIT = 0.70


def choose_move(my_history, opp_history):
    # Первый ход — кооперируем.
    if not my_history:
        return "C"

    n = len(opp_history)

    # --- Armor: если соперник стабильно D — переключаемся навсегда. ---
    if n >= WIN:
        window = opp_history[-WIN:]
        if window.count("D") >= int(D_RATE_LIMIT * WIN):
            return "D"

    # --- FBF memory-1: (1, 0, 1, 1) ---
    my_last = my_history[-1]
    opp_last = opp_history[-1]
    if my_last == "C" and opp_last == "C":
        return "C"
    if my_last == "C" and opp_last == "D":
        return "D"
    if my_last == "D" and opp_last == "C":
        return "C"
    # (D, D) → C: попытка выйти из тупика первым.
    return "C"
