"""Soft-Armored Firm-but-Fair — FBF с «мягким» AllD-детектором.

Прямой потомок `bot_armored_fbf`. Единственное отличие: когда armor
триггерится (d-rate соперника в окне 20 ходов ≥ 0.7), мы переключаемся
не в **вечный D**, а в **TFT-зеркало** (повторяем последний ход
соперника). Этот режим тоже без обратной дороги — оставаться в нём
безопасно, но он позволяет вернуться в CC, если соперник перестал
агрессивничать.

### Мотивация (см. LESSONS.md, Т11)

Armored FBF в Т11 (505.22) не пробил топ-3, и причина была не в
основной защите от AllD/Grim (там +173 очка), а в **двух ложных
срабатываниях**:

- vs Random: 403.67 (у обычного FBF — 565). Random случайно собирает
  d-rate > 0.7 на скользящем окне 20 → armor включается → вечный D →
  мы теряем доступ к T=5 на C-фазах Random.
- vs Gradual: 451.67 (у FBF — 510). Каскады gradual-а перешагивают
  порог 0.7 → armor → вечный D → теряем CC, который мог бы
  восстановиться через олив-бранч-C от gradual-а.

С мягким armor-ом:

- vs AllD: после триггера → TFT → AllD играет D → мы играем D → DD.
  Те же P=1 каждый раунд, что и у обычного armor-а. Прибыль ~100
  очков сохраняется.
- vs Grim: после триггера → TFT → Grim играет D (он уже в режиме
  AllD после первого шума) → мы играем D → DD. Прибыль ~140
  сохраняется.
- vs Random: после триггера → TFT → мы получаем 50% C / 50% D
  зеркально. Ожидаемые очки/раунд ≈ 0.25 × (3 + 0 + 5 + 1) = 2.25,
  значительно лучше, чем 1.0 у вечного D. Если armor не триггерится
  (часто), остаёмся в FBF.
- vs Gradual: после триггера → TFT → когда gradual предлагает
  олив-бранч C, мы тоже играем C → возвращение в CC. Восстановление
  кооперации возможно.

### Ожидаемые цифры

| Соперник     | armored_fbf (Т11) | soft_armored_fbf (прогноз) |
|--------------|-------------------|----------------------------|
| AllD         |   193.67          |   ~200                     |
| Grim         |   234.67          |   ~270 (вернёмся в CC если шум прервёт Grim) |
| Random       |   403.67          |   ~510                     |
| Gradual      |   451.67          |   ~500                     |
| Остальные    |   ~570-600        |   ~570-600 (FBF почти всегда) |

Ожидаемая прибавка: +50 + 50 ≈ +100 очков по двум матчам.
+100 / 14 ≈ +7 очков среднего рейтинга.
Прогноз: с 505.22 поднимется до ~512. В топ-3 нужно ≥ 510 — должен
попасть.

### Почему «без обратной дороги» в TFT-режиме

После того как мы заметили, что соперник систематически D, нет
смысла снова рисковать FBF-овским (D, C) → C (наивная попытка
кооперироваться, теряем S=0). TFT — это **минимально достаточная
защита**: безопасен против AllD, восстанавливается в CC против
любого, кто прекратил быть AllD.

### Связь с реальным миром

Это **«conditional engagement»** в международной политике:
после санкций (armor on) дипломатия не прекращается, но ведётся
по принципу «зеркала», без односторонних уступок. Если соперник
изменил поведение — возможен возврат к норме. Примеры:

- США vs Иран в 2010-х: санкции + JCPOA (2015) — отмена при
  условии остановки враждебных действий. Это «soft armor».
- США vs КНДР: санкции + готовность к диалогу при условии
  остановки тестов. То же.
- НАТО vs Россия после 2014: санкции (armor), но дипломатия
  продолжается (TFT, не AllD).

Контрпример «hard armor» — Куба 1962-2015: 50 лет вечного D без
обратной дороги. Сейчас известно, что это была политическая ошибка:
hard-armor-режим без условий возврата стоил дороже, чем стоил.
"""

WIN = 20
D_RATE_LIMIT = 0.70


def choose_move(my_history, opp_history):
    # Первый ход — кооперируем.
    if not my_history:
        return "C"

    n = len(opp_history)

    # --- Проверка: был ли armor когда-либо включён? ---
    # Armor включается, если когда-либо ранее окно из WIN ходов
    # давало d-rate >= D_RATE_LIMIT. Раз включился — мы навсегда в
    # TFT-режиме. Бот не имеет состояния между вызовами, поэтому
    # каждый раз делаем единственный линейный проход с скользящим
    # счётчиком D в окне (O(n), а не O(n*WIN)).
    armor_triggered = False
    if n >= WIN:
        threshold = int(D_RATE_LIMIT * WIN)
        d_count = sum(1 for x in opp_history[:WIN] if x == "D")
        if d_count >= threshold:
            armor_triggered = True
        else:
            for i in range(WIN, n):
                # Окно сдвигается на 1: добавляем opp_history[i],
                # выкидываем opp_history[i-WIN].
                if opp_history[i] == "D":
                    d_count += 1
                if opp_history[i - WIN] == "D":
                    d_count -= 1
                if d_count >= threshold:
                    armor_triggered = True
                    break

    if armor_triggered:
        # «Мягкий» armor: играем TFT (зеркалим последний ход соперника).
        return opp_history[-1]

    # --- FBF memory-1: (1, 0, 1, 1) ---
    my_last = my_history[-1]
    opp_last = opp_history[-1]
    if my_last == "C" and opp_last == "C":
        return "C"
    if my_last == "C" and opp_last == "D":
        return "D"
    if my_last == "D" and opp_last == "C":
        return "C"
    # (D, D) → C: попытка выйти из тупика первым.
    return "C"
