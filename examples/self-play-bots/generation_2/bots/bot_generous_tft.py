"""Generous Tit-for-Tat (gTFT).

Идея: классический TFT, но с маленькой вероятностью g=0.10 «прощает»
D соперника и всё равно играет C. На первом ходу — C.

Зачем:
- TFT катастрофически уязвим к шуму: один шумовой D запускает каскад
  D-C-D-C, потому что каждый отвечает на чужой случайный D настоящим D.
  Generous TFT периодически «гасит» этот каскад, не повторяя D, и обе
  стороны быстро возвращаются в CC.
- В отличие от TF2T, прощает не «всегда первый D», а с вероятностью.
  Это лишает «однократного эксплуатанта» вроде Pavlov-а гарантии:
  Pavlov больше не знает, что его одиночный D точно пройдёт безответно.
- На первом ходу — кооперируем (Аксельродовский принцип niceness).
- Параметр g=0.10 — компромисс. При g=0 это обычный TFT, при g→1 это
  AllC. Аксельрод-Новак-Сигмунд показывали, что g ≈ (1-T+R)/(R-S) для
  стандартной матрицы даёт устойчивую кооперацию; для нашей матрицы
  это ≈ (1-5+3)/(3-0) = -1/3, т.е. отрицательно, а значит для стандартной
  матрицы T=5,R=3,P=1,S=0 «оптимальной generosity» в этом смысле нет —
  выбираем эмпирическое g=0.10 как точку компромисса.

Свойства, которых ожидаю:
- gTFT vs gTFT: лучше, чем TFT vs TFT, потому что каскад от шума гасится
  ~раз в 10 раундов (одна сторона прощает).
- gTFT vs Pavlov: должно быть лучше TF2T vs Pavlov (412), потому что
  Pavlov-у тяжелее предсказать, пройдёт ли его одиночный D.
- gTFT vs AllD: чуть хуже TFT, потому что 10% времени всё равно отдаём
  C и получаем S=0. Ожидаемая потеря ~ g * 200 * 5 = 100 очков? Нет,
  на самом деле потеря только в S vs P = 1 очко за раз, т.е. ~g*200 = 20.
"""
import random

_rng = random.Random()
_FORGIVE_PROB = 0.10


def choose_move(my_history, opp_history):
    # Niceness: первый ход — кооперируем.
    if not opp_history:
        return "C"
    last_opp = opp_history[-1]
    if last_opp == "C":
        return "C"
    # Соперник сыграл D. С вероятностью g всё равно прощаем.
    if _rng.random() < _FORGIVE_PROB:
        return "C"
    return "D"
