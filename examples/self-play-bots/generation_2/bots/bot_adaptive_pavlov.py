"""Adaptive Pavlov (APavlov) — Pavlov, который умеет распознать AllD.

Идея (Li 2007, «How to design a strategy to win an IPD competition»):

Классический Pavlov (Win-Stay-Lose-Shift) великолепен сам с собой и
эффективно эксплуатирует прощающих кооператоров, но катастрофически
проваливается против упорного предателя: он попадает в цикл
C-D-C-D-C-D, ловит S=0 половину ходов (~115 очков за матч против AllD
в текущем составе). Адаптивный Pavlov закрывает эту дыру: распознаёт
устойчивого предателя и навсегда переходит в режим D.

Логика (stateless, классификация по глобальной доле D у соперника):

1. **Probe-фаза (первые 10 ходов): играем TFT.**
   Это безопасный дефолт — мы кооперируем первым, отвечаем D на D, но
   не пытаемся эксплуатировать. Длинный probe нужен, чтобы шумовой
   каскад в самом начале не запер нас в режиме PUNISH.

2. **После 10-го хода** считаем долю D у соперника по всей истории
   матча (`d_rate = opp_history.count('D') / len(opp_history)`):

   - `d_rate >= 0.85` — соперник почти всегда D → режим **PUNISH**
     (играем D). Это закрывает дыру Pavlov-а против AllD/Grim-после-
     триггера.
   - `d_rate <= 0.15` — соперник почти всегда C → режим **PAVLOV (WSLS)**.
     Здесь Pavlov максимально хорош: устойчив к самому себе, гасит
     шумовой D шифтом, аккуратно «доит» прощающих.
   - Иначе (0.15 < d_rate < 0.85) — соперник промежуточный
     (Random, шумящий TFT после каскада, и т.д.) → режим **TFT**
     (зеркало). Безопасно, не провоцирует эскалацию.

Почему именно глобальная доля, а не окно последних N ходов:

- В первой версии (окно из 6 ходов) шумовой каскад между двумя
  APavlov-ами навсегда запирал обоих в режиме PUNISH: окно показывало
  6 D из 6, оба продолжали D, окно никогда не очищалось. APavlov vs
  APavlov давал 363 очка — катастрофа в self-play.
- Глобальная доля «забывает» каскад медленно: даже 20 D подряд на
  фоне 80 C дают d_rate ~0.2 → PAVLOV → возвращение в кооперацию.
- Цена: реакция на смену поведения соперника медленнее, чем у окна.
  Это приемлемо: реальные «упорные предатели» (AllD, Grim-после-
  триггера) держат d_rate≈1 в любом представлении.

Эталонные ожидания (после правки, в текущем составе):

- vs AllC: PAVLOV → доим до ~770 (как у Pavlov-а).
- vs AllD: PUNISH с 11-го хода → ~205–215 (как у TFT).
- vs TFT/cTFT/gTFT/TF2T/SM: PAVLOV (партнёр в основном кооператор) →
  ~450–570, как у Pavlov-а, плюс лучше переживает шумовой каскад в
  начале благодаря длинному probe.
- vs Pavlov: PAVLOV (Pavlov мостьвсего тоже кооператор) → ~570 (как у
  Pavlov vs Pavlov).
- vs Random: d_rate ≈ 0.5 → TFT → ~450 (лучше Pavlov vs Random в
  шумной среде, потому что нет «лосинг-шифта», который сам создаёт
  шум).
- vs Grim: смешанная история. До триггера — PAVLOV кооперирует,
  пинает Grim шумом → Grim уходит в AllD → d_rate растёт → PUNISH.
  Ожидаемо плохо первые ~20–40 раундов (где-то теряем 50–80 очков), но
  потом DD стабильно по 1 очку. Конечный итог — лучше Pavlov vs Grim
  (174), хуже TFT vs Grim (228).
- vs самого себя: оба после probe оба классифицируют друг друга как
  кооператора (d_rate < 0.15 в чистом случае) → PAVLOV vs PAVLOV →
  ~570–580 (как Pavlov vs Pavlov).

Возможные слабости:

- **Период «слепоты» в первые 10 ходов.** В этот период мы играем
  чистый TFT и можем войти в шумовой каскад, ничего с ним не сделав.
  Цена ~5–10 очков за матч, окупается устойчивостью классификации.
- **Грубый порог.** Соперник на 50/50 (Random) попадает в TFT — мы
  его не эксплуатируем. Это сознательно: эксплуатация Random-а через
  PAVLOV даёт шумные очки, но не выигрыш по среднему.
- **Не учитывает «провокацию».** В отличие от cTFT, мы не различаем,
  кто начал. Классификация по доле проще и устойчивее, чем правила
  «кто кого спровоцировал».
- **Реакция на смену режима соперника медленная.** Если соперник
  играл 50 ходов как AllC, а потом резко переключился в AllD, мы
  ещё долго будем видеть d_rate < 0.85 (например, 50/100 = 0.5) и
  оставаться в TFT. Это цена «глобальной памяти».
"""


def choose_move(my_history, opp_history):
    # Первый ход — niceness, кооперируем.
    if not opp_history:
        return "C"
    n = len(opp_history)

    # Probe-фаза: первые 10 ходов играем TFT, безопасный дефолт.
    if n <= 10:
        return "D" if opp_history[-1] == "D" else "C"

    # Классификация по глобальной доле D у соперника.
    d_rate = opp_history.count("D") / n

    if d_rate >= 0.85:
        # PUNISH: соперник почти всегда D → играем D.
        return "D"

    if d_rate <= 0.15:
        # PAVLOV (WSLS): соперник почти всегда C → используем WSLS.
        last_me = my_history[-1]
        last_opp = opp_history[-1]
        # Win (CC=R или DC=T) → stay; Lose (DD=P или CD=S) → shift.
        if (last_me, last_opp) in (("C", "C"), ("D", "C")):
            return last_me
        return "D" if last_me == "C" else "C"

    # Промежуточный режим: TFT-зеркало.
    return "D" if opp_history[-1] == "D" else "C"
