"""Firm-but-Fair (Frean 1994).

Детерминированная memory-1 стратегия со вероятностями кооперации
(p1, p2, p3, p4) = (1, 0, 1, 1), где индекс — это пара (мой_прошлый,
ход_соперника_прошлый):

- (C, C) → C: «всё хорошо, продолжаем».
- (C, D) → D: «он меня обманул — наказываю».
- (D, C) → C: «я обманул его, но он не ответил мне D —
  возвращаюсь к кооперации» (это и есть «fairness»: не доим
  доверчивых).
- (D, D) → C: «оба упёрлись — попробую первым выйти из тупика».

Сравнение с Pavlov (WSLS): отличается только в (D, C). Pavlov там
играет D (продолжает доить), а FBF играет C. То есть FBF —
«моральный Pavlov»: не эксплуатирует одностороннюю кооперацию.

Свойства, которые ожидаются:
- vs AllC: после случайного шумового D → DC → C, баланс
  восстанавливается. FBF не доит AllC, в отличие от Pavlov.
- vs AllD: первый ход CD → S=0 → D (через CD→D). После DD играем C
  → опять CD → S=0 → D… Пинг-понг C/D, такой же как у Pavlov.
  ~50% получаем S=0. Слабость FBF против AllD.
- vs TFT: оба кооперируют. После шумового D у TFT → CD → D у нас
  → DD → C у обоих → CC. Восстановление за 2 раунда.
- Self-play под шумом: после одного шумового D одна сторона видит
  CD (играет D), другая — DC (играет C). Получаем (D,C). Следующий
  раунд: первая видит DC (играет C), вторая — CD (играет D).
  Получаем (C,D). Это **залипание в чередовании CD/DC** — каждый
  раунд один из нас получает T=5, другой S=0, среднее за раунд 2.5.
  Из этого залипания вытащит только новый шумовой ивент, который
  приведёт к DD у обоих → оба играют C → CC.

Поэтому ожидаемый рейтинг: середина пелотона. FBF — это памятник
красивой симметричной идее, которая страдает от шума за счёт
«морального» отказа эксплуатировать одностороннюю кооперацию.

Источник: Frean, M. R. (1994). "The prisoner's dilemma without
synchrony". Proc. R. Soc. B.
"""


def choose_move(my_history, opp_history):
    # Первый ход — кооперируем (как и весь TFT-пантеон).
    if not my_history:
        return "C"
    my_last = my_history[-1]
    opp_last = opp_history[-1]
    # Реализуем (1, 0, 1, 1) по таблице (my_last, opp_last):
    if my_last == "C" and opp_last == "C":
        return "C"
    if my_last == "C" and opp_last == "D":
        return "D"
    if my_last == "D" and opp_last == "C":
        return "C"
    # (D, D) → C: попытка выйти из взаимного предательства первым.
    return "C"
