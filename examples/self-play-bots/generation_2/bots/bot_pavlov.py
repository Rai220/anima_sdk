"""Pavlov (Win-Stay-Lose-Shift, WSLS).

Идея простая и психологически интуитивная: если в прошлом раунде я
«остался доволен» — повторяю свой ход; если «получил по носу» —
меняю.

Доволен = получил T=5 или R=3 (мой ход совпал по «успешности» с
ожиданием). Не доволен = получил S=0 или P=1.

В терминах ходов:
- (my=C, opp=C) → R=3, доволен  → повторяю C.
- (my=D, opp=C) → T=5, доволен  → повторяю D (буду пытаться доить).
- (my=C, opp=D) → S=0, недоволен → меняю на D.
- (my=D, opp=D) → P=1, недоволен → меняю на C (пытаемся выйти из DD).

Свойства:
- Самовосстанавливается при шуме лучше TFT: если случайный D у обоих
  превращает раунд в DD, оба недовольны и оба переключаются обратно
  на C — итог CC.
- Эксплуатирует AllC: после первого CC получает удовольствие и
  кооперирует… но как только увидит S=0, переключится на D и
  обнаружит, что DC = T=5 — и зависнет в режиме «доить вечно».
  Поэтому против AllC, если шум хоть раз сделает (C,D), Pavlov
  начинает фармить.
- Против AllD: первый ход CD → S=0 → переключается на D. DD → P=1 →
  переключается на C → опять S=0 → … вечный пинг-понг. Это плохо:
  половину ходов получает 0. Это известная слабость Pavlov.

Эта слабость как раз делает Pavlov интересной точкой сравнения с
TFT/TF2T: он другой по динамике и хорошо иллюстрирует, что нет
«одной лучшей» стратегии.
"""


def choose_move(my_history, opp_history):
    # Первый ход — кооперируем (как и TFT/TF2T/Grim).
    if not my_history:
        return "C"
    last_me = my_history[-1]
    last_opp = opp_history[-1]
    # Доволен ли я последним раундом?
    happy = last_opp == "C"  # CC → R=3, DC → T=5 — оба «успех» для меня.
    if happy:
        return last_me  # win-stay
    # lose-shift: меняю свой последний ход на противоположный.
    return "D" if last_me == "C" else "C"
