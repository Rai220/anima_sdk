"""Soft Majority — кооперация, пока соперник в сумме был не реже C, чем D.

Идея (классика, см. Mittal & Deb 2009 и обзоры Axelrod-tournaments):

- Первый ход — C (niceness).
- На каждом следующем ходу смотрим всю историю соперника. Если он
  сыграл C не реже, чем D (`count_C >= count_D`), — кооперируем; иначе
  играем D.

Почему это интересно:

- Все «зубастые» стратегии семейства TFT/Pavlov/Grim смотрят на
  последние 1–2 хода. Они отлично гасят локальные конфликты, но
  чувствительны к локальному шуму и к стратегиям, которые «жалят и
  убегают» (Pavlov, Tester и пр.).
- Soft Majority смотрит на **длинный горизонт**. Один шумовой D
  соперника почти не двигает баланс и не вызывает ответа. Это даёт
  очень стабильную кооперацию в среде, где большинство играет в
  основном C.
- Защита от AllD: уже после первого хода соперник имеет 1 D и 0 C —
  условие `C >= D` нарушено, мы переходим в D и не возвращаемся.
- Уязвимости:
  1. **Долгая «доимость» Pavlov-овским жалом.** Пока баланс не
     перевернулся, мы продолжаем кооперировать, отдавая T=5 за каждый
     D соперника. Расплата отсрочена.
  2. **Стохастический «жалитель» (Joss-подобный).** Бот, играющий
     ~60% C и ~40% D, очень долго остаётся в условии `C >= D` и
     может пастись на нас.

Эталонные ожидания (для текущего пантеона):

- vs AllC: оба всегда C, ~592 (минус шум). Стандарт.
- vs AllD: один-два хода CC/CD на старте, потом DD до конца. ~205,
  как у TFT.
- vs TFT, gTFT, cTFT, TF2T: после небольшой шумовой пляски — стабильное
  CC, ~580–600.
- vs Pavlov: ожидаем «доение» в первой половине матча и стабилизацию
  потом. Возможный счёт 350–450 в пользу Pavlov-а, нам — 350–450.
- vs Random: примерно 50/50 в истории, баланс почти везде C>=D или
  на грани. Скорее всего, флуктуирует. Ожидаемо ~440 за матч.
"""


def choose_move(my_history, opp_history):
    if not opp_history:
        # Niceness: первый ход — кооперируем.
        return "C"
    c_count = opp_history.count("C")
    d_count = len(opp_history) - c_count
    if c_count >= d_count:
        return "C"
    return "D"
