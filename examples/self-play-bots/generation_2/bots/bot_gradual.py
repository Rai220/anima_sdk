"""Gradual (Beaufils–Delahaye–Mathieu, 1996) — TFT с эскалацией ответа.

Идея, в одну строчку: каждый раз, когда соперник предал, наказать его
**N раз подряд**, где N = общее число его предательств за матч, после
чего отправить **2 хода C как сигнал примирения** ("олив-бранч") и
вернуться к кооперации. Внутри фазы наказания / примирения никаких
дополнительных триггеров: уже отвечаем.

Это та самая стратегия, которую Beaufils et al. показали как
последовательно обыгрывающую TFT в экологических симуляциях
(многоповторные турниры с отбором). Главное отличие от TFT:

- **Эскалация**. Первое предательство → 1 ответ D. Второе → 2 ответа D.
  Сотое → 100 D подряд. Это делает Gradual очень дорогой целью для
  систематических эксплуататоров: AllD получает короткое наказание в
  первом раунде, но к 10-му предательству Gradual уже не отпускает.
- **Сигнал примирения**. После эскалации шлём 2 C подряд. Если
  соперник был «случайно» (шум) и сейчас вернётся к C, мы заметим это
  и не будем дальше эскалировать. TFT в той же ситуации бесконечно
  зеркалит D.

В шумной среде у Gradual есть слабость: каждый случайный D соперника
наращивает счётчик N. Через 4 шумовых события N достигает 4, и
ошибочный ответ длится 4 раунда. Это плата за «эскалацию вместо
амнезии». Мы оставляем эту плату честно, чтобы понять, как
эскалация ведёт себя в noise=0.02. Если станет ясно, что эскалация
слишком дорогая — следующая итерация может ввести cap на N (например,
N ≤ 5) или окно «давность D».

Stateless-реализация: каждый раз мы перепроигрываем алгоритм Gradual
по `opp_history`, восстанавливая `pending_d` (сколько D осталось
отдать) и `pending_c` (сколько C-примирения осталось). После
восстановления состояния — выдаём следующий ход.

Связь с реальным миром:
- Эскалация наказания за повторяющееся предательство — это
  классическая «трёхступенчатая» дипломатия: предупреждение → санкция →
  жёсткая санкция. Не «одинаковая пощёчина за всё», а «накопительная
  доза».
- Олив-бранч из 2 C — это эквивалент «жеста доброй воли» после санкции,
  показывающего, что наказание было не местью, а откликом на конкретное
  поведение.
- Слабость к шуму = слабость к «случайным инцидентам» в реальной
  политике: накопление инцидентов делает дипломатический ответ всё
  жёстче, даже если каждый инцидент был случайным.
"""


def choose_move(my_history, opp_history):
    if not opp_history:
        return "C"

    pending_d = 0  # ещё столько раз D в этой фазе наказания
    pending_c = 0  # ещё столько раз C в фазе примирения (после D)

    # Восстанавливаем состояние, прокручивая историю с нуля.
    # На шаге i мы знаем opp_history[i] и сделали ход согласно
    # текущим pending_d / pending_c. Затем реагируем на opp_history[i],
    # обновляя pending_*.
    for i in range(len(opp_history)):
        # На шаге i мы тратили один счётчик (если был активен).
        if pending_d > 0:
            pending_d -= 1
        elif pending_c > 0:
            pending_c -= 1
        # Теперь смотрим, что сыграл соперник, и решаем, не запустить ли
        # новую фазу. Триггер: opp[i] == D и мы сейчас в режиме
        # «кооперации» (никаких pending не осталось).
        if opp_history[i] == "D" and pending_d == 0 and pending_c == 0:
            total_d = 0
            for j in range(i + 1):
                if opp_history[j] == "D":
                    total_d += 1
            pending_d = total_d
            pending_c = 2

    if pending_d > 0:
        return "D"
    if pending_c > 0:
        return "C"
    return "C"
