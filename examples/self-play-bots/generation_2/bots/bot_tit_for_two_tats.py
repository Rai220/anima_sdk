"""Tit-for-Two-Tats — прощающая версия TFT.

Идея: предательство в один ход может быть случайным шумом, а в два
подряд — уже сигнал. Мстим только если соперник предал ДВА раза
подряд. Это резко улучшает поведение в шумной среде:

- против самого себя один шумовой D не запускает каскад «D-C-D-C…»;
- против AllC ведёт себя как AllC;
- против AllD теряет на 1 ход больше, чем TFT (нужно два D, чтобы
  понять), но дальше зеркалит;
- против TFT при шуме гораздо стабильнее держит линию C.

Слабость: уязвим к стратегиям типа «D, D, C, C, D, D, C, C…» —
никогда не получит два D подряд при таком ритме. Но в нашем пантеоне
таких нет.
"""


def choose_move(my_history, opp_history):
    # Первые два хода — сотрудничаем.
    if len(opp_history) < 2:
        return "C"
    # Два последних хода соперника — оба D? Тогда мстим.
    if opp_history[-1] == "D" and opp_history[-2] == "D":
        return "D"
    return "C"
