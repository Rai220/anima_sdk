"""Contrite TFT (cTFT) — TFT, который умеет различать «шум» и «атаку».

Идея (Boyd 1989, упрощённая stateless-версия):

Классический TFT в шумной среде катастрофичен против самого себя: один
случайный D запускает каскад D-C-D-C. Generous TFT гасит каскад
случайным прощением; Tit-for-Two-Tats — детерминированным. Обе платят
цену против эксплуататоров (Pavlov, AllD).

Contrite TFT идёт другим путём: он смотрит **контекст** последнего
предательства соперника и решает, **выглядит ли это как шум**.

Правило (stateless, по последним 2 ходам):

1. Первый ход — C.
2. Если opp_last == C — играем C (обычный TFT).
3. opp_last == D — нужно решить, отвечать ли:
   a. Если предыдущий ход соперника тоже был D — он не оступился,
      он систематически предаёт. Играем D.
   b. Если мой предыдущий ход был D — соперник, видимо, ответил на
      моё предательство (TFT-зеркало). Не извиняемся, продолжаем D
      (стандартное TFT-зеркало).
   c. opp[-2] == C и my[-1] == C: соперник выпустил **одиночный D**
      после ряда взаимной кооперации, при этом я ничем его не
      провоцировал. Это типичная сигнатура шума. Но прежде чем
      простить, проверим окно последних 5 ходов: если соперник
      предавал в нём ≥2 раз, это не шум, это паттерн. Тогда играем D.
      Иначе — прощаем, играем C.

Почему именно так:

- Против AllD: opp всегда играет D, в окне всегда ≥2 D, никогда не
  извиняемся → играем как TFT, ~211 очков за матч.
- Против себя (cTFT vs cTFT) в шумной среде: одиночный шумовой D у
  любой стороны → другая прощает → каскад не запускается. Должно быть
  близко к TF2T vs TF2T = 587.
- Против TFT: шумовой D у TFT → cTFT прощает → TFT в следующий ход
  видит C от cTFT → отвечает C → возвращаемся в CC. Должно быть
  заметно лучше TFT vs TFT = 472.
- Против Pavlov: у Pavlov-а «жалящий» D всегда происходит после CC
  и не сопровождается провокацией с моей стороны. cTFT прощает первый
  D — Pavlov получает T=5. Но на следующий ход Pavlov либо продолжит
  D, либо переключится (зависит от того, как я ответил). Если Pavlov
  продолжит D, то в моей window опять ≥2 D → больше не прощаю.
- Против Grim после шумового удара по нам — Grim не прощается, мы
  теряем матч в любом случае; cTFT не делает ситуацию хуже.

Возможный риск: «AllD с маской», который чередует C-D-C-D-C-D —
будет ловить нас на C каждый второй ход. Защита через окно: за 5
ходов 2-3 D, окно не прощает после второго эпизода.
"""


def choose_move(my_history, opp_history):
    if not opp_history:
        return "C"
    last_opp = opp_history[-1]
    if last_opp == "C":
        return "C"
    # last_opp == "D" — решаем, отвечать или простить.
    n = len(opp_history)
    if n < 2:
        # Слишком мало контекста: один D в самом начале матча — отвечаем.
        return "D"
    prev_opp = opp_history[-2]
    if prev_opp == "D":
        # Соперник предавал и раньше — это паттерн, а не шум.
        return "D"
    # prev_opp == "C". Был кооперативный фон, теперь одиночный D.
    if my_history[-1] == "D":
        # Я только что сам сыграл D. Соперник, скорее всего, просто
        # отзеркалил. Не извиняюсь — продолжаю обычный TFT (D на D).
        return "D"
    # my_last == C, prev_opp == C, opp_last == D — выглядит как шум.
    # Финальный фильтр: проверяем окно последних 5 ходов соперника.
    # Если в нём уже было >=2 D — это не шум, это эксплуатация.
    window_n = min(5, n)
    recent_opp = opp_history[-window_n:]
    if recent_opp.count("D") >= 2:
        return "D"
    # Прощаем шум.
    return "C"
