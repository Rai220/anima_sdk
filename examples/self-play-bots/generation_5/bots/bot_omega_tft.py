"""Omega TFT — TFT с детектором шума и переключением в AllD против хищников.

Идея вдохновлена Slany & Kienreich (2007), "On some winning strategies for
the iterated prisoner's dilemma": если соперник систематически предаёт —
не пытаться его «исправить», а перейти на AllD. Если соперник предаёт
редко и кажется изолированно (или это симметричная TFT-вспышка от шума) —
прощать.

Алгоритм:

1. Первый ход — C.
2. Накопили хоть какую-то историю (n >= 5):
   - d_rate = доля D соперника по всей истории;
   - max_streak = максимальная длина подряд идущих D соперника;
   - если **d_rate >= 0.35 и max_streak >= 3** → классифицируем как
     «систематический эксплуататор» (AllD, Grim после триггера, Random
     с длинными хвостами), играем D.
   - если **d_rate >= 0.55** в принципе → играем D (для Random,
     где streak короткий, но плотность высокая).
3. Иначе пытаемся прощать:
   - если последний ход соперника = C, играем C;
   - если последний ход = D, но три предыдущих хода = C (изолированный D,
     похожий на шум) — прощаем, играем C;
   - иначе обычный TFT-ответ: D.

Что эта стратегия даёт по сравнению с предыдущими:

- Против **AllD/Grim** (d_rate=1, streak=∞) — мгновенно D. Не теряет
  очки на «попытках помириться» как Pavlov.
- Против **Random** (d_rate~0.5, streak~3-7) — после ~10 ходов
  классифицирует как эксплуататора, переключается в D и собирает
  T=5 на каждый C от Random. Это компенсирует слабое место gTFT/SoftMaj
  (Random).
- Против **AllC** (d_rate=0) — всегда C. Тот же RR=3/раунд.
- Против **TFT/gTFT/TFTT/SoftMaj** в шумной среде — d_rate низкий,
  каскады короткие, прощает изолированные D → восстанавливает CC.
- В **self-play против себя** — оба не накапливают много D, оба прощают,
  должен быть высокий результат.

Слабые места (заранее):

- Против **Pavlov** в шумной паре Pavlov может ненадолго эксплуатировать
  (как у SoftMaj). Посмотрим в эксперименте.
- Пороги (0.35, 0.55, окно 3) — эвристика. В другой популяции могут
  оказаться неоптимальны. Но мы не подгоняем под конкретного победителя,
  это были априорные значения.

Реальная аналогия: «принцип одного предупреждения, но с памятью». Сначала
пытаешься объяснить, что мы оба выигрываем от кооперации; если партнёр
системно идёт против — выходишь из игры в чистый D, не тратишь ресурс на
бесконечные попытки восстановления. Похоже на поведение крупного государства
в долгом конфликте: эпизодические инциденты прощают (они — «шум»),
систематическую враждебность — нет.
"""


def choose_move(my_history, opp_history):
    if not opp_history:
        return "C"

    n = len(opp_history)

    # Слишком мало данных — просто TFT.
    if n < 5:
        return "C" if opp_history[-1] == "C" else "D"

    d_count = opp_history.count("D")
    d_rate = d_count / n

    # Максимальный D-стрик.
    max_streak = 0
    cur_streak = 0
    for move in opp_history:
        if move == "D":
            cur_streak += 1
            if cur_streak > max_streak:
                max_streak = cur_streak
        else:
            cur_streak = 0

    # Систематический эксплуататор: высокая доля D и длинные пробежки.
    if d_rate >= 0.35 and max_streak >= 3:
        return "D"

    # Очень высокая плотность D даже при коротких стриках (Random-like).
    if d_rate >= 0.55:
        return "D"

    # Мягкий режим: TFT с прощением изолированного D.
    last = opp_history[-1]
    if last == "C":
        return "C"

    # last == "D". Изолированный ли это D? Смотрим на 3 предыдущих.
    if n >= 4 and opp_history[-4:-1].count("C") == 3:
        return "C"  # Похоже на шум — прощаем.

    return "D"  # Обычный ответ TFT.
