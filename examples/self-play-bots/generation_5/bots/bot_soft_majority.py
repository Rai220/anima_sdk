"""Soft Majority.

Идея: голосование большинством голосов на всей истории соперника.

Правило:

- Первый ход — C.
- Дальше: если C-голосов соперника >= D-голосов — играю C, иначе D.

Слово "soft" означает: при равенстве C >= D выбираю C (по умолчанию
дружелюбен). У "hard" majority при равенстве выбирается D.

Свойства (из gen 1-4 эмпирически, и из теории):

- **Шумоустойчив.** Глобальная статистика не "застревает" в скользящем
  окне: одна шумовая D соперника подмешивается к 100 настоящих C —
  доля C по-прежнему ~99%, ответ C. Это структурное отличие от
  TFT/Pavlov, которые реагируют на последний ход.
- **Не эксплуатируем альтернированием.** Альтернатор DCDC даёт 50/50;
  Soft Majority при равенстве играет C, и альтернатор берёт ~5 каждый
  второй ход → 2.5/раунд против Soft Majority. Но Soft Majority в этой
  же паре получает 0+3+0+3 = 1.5/раунд. То есть Soft Majority платит
  ~1 очко/раунд за свою мягкость, по сравнению с TFT (который тоже бы
  альтернировал в этом матче).
- **Хорош против AllC** (CC = 3/раунд) и **узнаёт AllD** за O(1) ходов:
  после первого D соперника, у него C-голосов=0, D-голосов=1, → ответ
  D. Дальше доля C соперника = 0, ответ D всегда. Так что Soft Majority
  vs AllD деградирует в DD после первого хода — почти как Grim, но
  **обратимо**: если AllD вдруг шумит в C, Soft Majority учитывает это
  в статистике и может вернуться в C.
- **В self-play против себя:** оба начинают C, оба видят 100% C, оба
  играют C → почти идеальная CC, шум 2% гасится потому что статистика
  всегда показывает большинство C.

Это та стратегия, которая стабильно держится в топ-3 поколений 1-4.
Здесь проверяем воспроизводимость в gen5.

Реальная аналогия: дипломат, который судит соперника по общему
послужному списку, а не по последнему инциденту. "Один раз — случайность,
два раза — совпадение, три раза — закономерность."
"""


def choose_move(my_history, opp_history):
    if not opp_history:
        return "C"
    c_votes = opp_history.count("C")
    d_votes = len(opp_history) - c_votes
    return "C" if c_votes >= d_votes else "D"
