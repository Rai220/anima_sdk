"""Prober — стратегия активного тестирования соперника.

Классическая стратегия Mathieu & Delahaye, противоположная пассивному
Omega TFT: вместо накопления статистики о сопернике делает явный «тест»
в самом начале матча, чтобы определить тип соперника, и потом действует.

Алгоритм:

1. Первые три хода — фиксированный паттерн **D, C, C** (так называемый
   probing pattern). Это «провокация»: смотрим, как соперник на неё
   реагирует.

2. После третьего хода — решение:
   - если соперник на ходах 2 и 3 сыграл **C, C** (то есть НЕ отомстил
     за наше изначальное D) → классифицируем как «прощающий простак»
     (AllC, generous-боты без памяти, мягкий TFTT). Дальше играем
     **AllD** — эксплуатируем до конца матча.
   - иначе (соперник наказал нашу D хотя бы один раз) → классифицируем
     как «зубастый, способен мстить». Дальше играем **TFT**.

3. Шумовой фильтр: один шумовой D на ходе 2 или 3 не должен сломать
   диагноз. Поэтому требуем СТРОГО два C подряд для «эксплуатации».
   Любое D от соперника в первых трёх ходах включает TFT-режим. Это
   консервативная политика: лучше упустить AllC, чем нарваться на
   разозлённого TFT.

Что Prober прощупывает:

- AllC: будет CCC в ответ → нас определят как простака → AllD до конца.
  Prober в этом матче собирает ~5×197 ≈ 985 очков. Это близко к AllD-vs-AllC.
- TFT/gTFT/TFTT/SoftMaj/Omega/Pavlov/Grim: после нашего D на ходе 1
  они в большинстве случаев ответят D хотя бы один раз. Prober уйдёт
  в TFT-режим и постарается восстановить кооперацию.
- Random: 50/50 даст D хотя бы раз с вероятностью ~75%. Prober почти
  всегда уйдёт в TFT-режим, что в шумной партии с Random не сильно
  лучше AllD.
- AllD: ответит DDD. Prober в TFT-режиме — что эквивалентно AllD против
  AllD.

Слабые места (заранее):

- Начальный D даёт сопернику −1 очко в первом раунде и +5 ему. То есть
  Prober в первом раунде УЖЕ в дефиците 5−0=5 очков относительно того,
  если бы начал с C. Это «стоимость диагностики».
- Против ботов, способных «увидеть» начальный D и навсегда обидеться
  (Grim), Prober не восстанавливает CC — теряет ~595 очков относительно
  CC.
- Против AllC выигрывает огромный куш, но в популяции с одним AllC
  это всего +500 очков на 9 матчей → +55/матч в среднем. Не факт, что
  компенсирует потери против Grim.

Реальная аналогия: **дипломатическая провокация / тестовый шар**. Запускают
«пробный» жёсткий шаг (санкции, заявление, военные учения у границы),
чтобы посмотреть, как партнёр реагирует. Если партнёр сглатывает —
дальше эскалация. Если ответ есть — переходят к торгу. Это рисковая
стратегия: можно безвозвратно испортить долгую отношения ради информации.

Ожидание для турнира: средний/нижний результат. Prober ценен НЕ как
победитель, а как **тестер популяции**: показывает, кто из ботов
действительно умеет наказать за начальное предательство, а кто наивно
прощает «потому что один D — это шум».
"""


def choose_move(my_history, opp_history):
    n = len(my_history)

    # Фиксированный probing pattern: D, C, C.
    if n == 0:
        return "D"
    if n == 1:
        return "C"
    if n == 2:
        return "C"

    # На третьем ходе уже есть opp_history длиной 3.
    # Соперник «простак»? Только если оба его хода 2 и 3 = C (т.е. не
    # отомстил за наш стартовый D).
    opp_first_three = opp_history[:3]
    # opp_first_three[0] — реакция на наш D на ходе 1.
    # opp_first_three[1] — реакция на наш C на ходе 2.
    # opp_first_three[2] — реакция на наш C на ходе 3.

    # Если соперник НЕ играл D ни разу в первых трёх ходах → AllD-режим.
    if "D" not in opp_first_three:
        return "D"

    # Иначе TFT-режим: копируем последний ход соперника.
    return "C" if opp_history[-1] == "C" else "D"
