"""Generous Tit-for-Tat (gTFT).

Как TFT, но прощает каждое D соперника с фиксированной малой
вероятностью. Это разрывает шумовые каскады D-C-D-C, в которые
застревает строгий TFT при noise > 0.

Параметр прощения подбирается так, чтобы:

- против настоящего предателя gTFT всё-таки наказывал (иначе
  превращается в AllC и эксплуатируется),
- против шумового искажения хотя бы один из двух партнёров рано или
  поздно «прощал», возвращая пару в CC.

Эмпирически 0.1 — рабочее значение для шума 0.02 на 200 раундов. Это
примерно один акт прощения на ~10 D соперника, чего хватает, чтобы
сбивать каскады, но не хватает, чтобы стать жертвой AllD.

Реальная аналогия: дипломатия с короткой памятью. «Один раз спустил —
не значит, что не помнишь, но мстить за каждый чих не будешь».
"""

import random


FORGIVE_P = 0.1


def choose_move(my_history, opp_history):
    if not opp_history:
        return "C"
    last = opp_history[-1]
    if last == "D" and random.random() < FORGIVE_P:
        return "C"
    return last
