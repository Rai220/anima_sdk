"""Contrite Tit-for-Tat (CTFT).

Литература: Sugden 1986, Wu & Axelrod 1995. TFT с поправкой на шум:
бот отслеживает «standing» (репутацию) обеих сторон.

Идея «standing»:
- по умолчанию обе стороны в good standing;
- после раунда сторона переходит в bad standing, если сыграла D
  при том что (а) соперник одновременно сыграл C и (б) соперник был
  в good standing перед раундом. Это «беспричинное» D — то, что
  выглядит как умышленное предательство или шумовая ошибка;
- если же D игралось против соперника в bad standing — это
  оправданная кара, standing не портится.

Решение:
- если соперник сыграл D на прошлом ходе И я был в good standing
  перед этим ходом → его D неоправданное → отвечаю D;
- иначе (соперник C, либо я был «виноват» и его D — это законное
  наказание) → играю C.

Гипотеза: CTFT гасит шумовые каскады TFT/TFT — после моей шумовой D
я перехожу в bad standing, соперник (если он CTFT/TFT-like) играет
D, я НЕ ретализирую, отношения восстанавливаются за 1-2 хода. В
этом отличие от TFTT (который ждёт два D подряд независимо от
причин) и от Pavlov (который реагирует на свой собственный счёт).
"""


def choose_move(my_history, opp_history):
    if not opp_history:
        return "C"

    n = len(opp_history)
    # standing[t] = standing ПЕРЕД раундом t. Начальный = "good".
    my_st = ["good"] * (n + 1)
    op_st = ["good"] * (n + 1)
    for t in range(n):
        m = my_history[t]
        o = opp_history[t]
        # Я перехожу в bad, если сыграл D против C соперника, и
        # соперник был в good standing (т.е. его C — не «вынужденное»
        # извинение, и моё D беспричинно).
        if m == "D" and o == "C" and op_st[t] == "good":
            my_st[t + 1] = "bad"
        else:
            my_st[t + 1] = "good"
        # Симметрично для соперника.
        if o == "D" and m == "C" and my_st[t] == "good":
            op_st[t + 1] = "bad"
        else:
            op_st[t + 1] = "good"

    opp_last = opp_history[-1]
    if opp_last == "D" and my_st[-1] == "good":
        # Беспричинное D от соперника → ответ D (TFT-ретализация).
        return "D"
    # Иначе: либо C, либо его D — оправданная кара за мой шум.
    return "C"
