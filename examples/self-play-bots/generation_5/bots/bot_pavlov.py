"""Pavlov / Win-Stay, Lose-Shift (WSLS).

Третья крупная семья IPD-стратегий после TFT-семьи и Grim-семьи.
Описана Нowak & Sigmund (1993) как победитель эволюционных
симуляций при достаточном шуме.

Правило:

- Первый ход — C.
- Если в прошлом раунде «выиграл» (получил R=3 за CC или T=5 за DC) —
  повтори свой прошлый ход (win → stay).
- Если в прошлом раунде «проиграл» (получил P=1 за DD или S=0 за CD) —
  смени ход (lose → shift).

Таблица переходов (мой ход → следующий ход), в зависимости от пары:

    (C, C)  R=3  win   → C  (повтор)
    (C, D)  S=0  lose  → D  (смена)
    (D, C)  T=5  win   → D  (повтор: продолжаю эксплуатировать)
    (D, D)  P=1  lose  → C  (смена: пробую вылезти из взаимного DD)

Свойства:

- **Самокоррекция в self-play.** Одна шумовая D в паре Pavlov-Pavlov
  приводит к (D,C) или (C,D) → один ход рассинхрона → возврат в CC
  за 2 хода. Без длинного каскада.
- **Эксплуатирует AllC.** После (C,C) → C, но если соперник всегда C,
  Pavlov не "переключится" сам — он останется в CC. Но если шум
  выбьет (D,C), он останется в D, и AllC станет жертвой до следующего
  шумового переключения. В среднем Pavlov берёт с AllC ~3.5-4 за раунд.
- **Уязвим против AllD.** После (C,D) → D. (D,D) → C. (C,D) → D. …
  Бесконечный цикл D-C-D-C-…, отдаёт T=5 каждый второй ход. Это
  главная слабость классического Pavlov.

Реальная аналогия: ребёнок учится на немедленной обратной связи —
если повезло, повторяет; если получил по носу, пробует другое.
В дипломатии — «политика реактивной коррекции»: пока работает —
держим курс; перестало работать — меняем.
"""


def choose_move(my_history, opp_history):
    if not my_history:
        return "C"
    last_me = my_history[-1]
    last_opp = opp_history[-1]
    # Win = соперник C (я получил R=3 или T=5).
    won = last_opp == "C"
    if won:
        return last_me  # win-stay
    # lose-shift
    return "D" if last_me == "C" else "C"
