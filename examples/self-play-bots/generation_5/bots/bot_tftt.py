"""Tit-for-Two-Tats (TFTT, или TF2T).

Идея Аксельрода: отвечать D только после ДВУХ предательств соперника
подряд. На первый ход — C. Один одиночный D соперника прощается без
наказания.

Это _детерминированный_ способ выжить в шуме (в отличие от
стохастического gTFT). Логика:

- Изолированная шумовая D соперника (≈2% случаев на ход) гасится
  сама собой, без каскада. Чтобы получить наказание, шум должен
  выбить TWO подряд (вероятность 0.02² = 0.0004 на ход).
- Против настоящего AllD — после первых двух D переключается в D
  навсегда. То есть теряем 2*5=10 очков «на разогрев», но не
  становимся жертвой.
- Против TFT — взаимная C, шумовые D гасятся односторонне (TFTT
  прощает первую D, TFT возвращает C). Каскад короче.

Цена: TFTT эксплуатируем стратегиями вида "D-C-D-C-D-C…" — они
получают T=5 каждый второй ход и не нарываются на наказание.
В обычной популяции таких ботов почти нет, поэтому цена низка.

Реальная аналогия: «дать второй шанс, но не третий». Юридический
аналог — рецидив: первое нарушение могут простить, второе — нет.
"""


def choose_move(my_history, opp_history):
    if len(opp_history) < 2:
        return "C"
    if opp_history[-1] == "D" and opp_history[-2] == "D":
        return "D"
    return "C"
